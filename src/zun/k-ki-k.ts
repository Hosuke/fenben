import type { 筆具 } from '../bi.js';
import { 菩薩坐身, 上臂常勢, 前臂 } from './pusa.js';
import { 側拳, 臂釧, 腕釧 } from '../bujian/index.js';

export const 金剛喜 = (bi: 筆具): void => 菩薩坐身(bi, {
  胸瓔滴z: 30.0,
  臂(bi, 筆) {
    const { 骨, 細, 陰, 陽 } = 筆;
    // 上臂常勢；前臂自肘內收而上，各抵胸側（肘有帽）
    骨(() => { 上臂常勢(筆, -1, 40.0); 上臂常勢(筆, 1, 40.0); });
    骨(() => 陽(() => 前臂(bi, 筆, [-13.7, 40.1], [-5.6, 35.9],
      { 寬根: 2.5, 寬梢: 1.9, 弓: -0.4, 肘帽: true })));
    骨(() => 陰(() => 前臂(bi, 筆, [13.7, 40.1], [5.6, 35.9],
      { 寬根: 2.5, 寬梢: 1.9, 弓: 0.4, 肘帽: true })));
    // 二拳並胸前（兩拳相就而不觸，指背向前）
    側拳(bi, -3.5, 34.6, 0.48, 1.25);
    側拳(bi, 3.5, 34.6, -0.48, 1.25, -1);
    // 嚴
    臂釧(bi, -1, 12.1, 14.5, 33.4); 臂釧(bi, 1, 12.1, 14.5, 33.4);
    細(() => {
      腕釧(bi, [[-6.3, 35.5, -5.7, 36.7, -4.9, 37.5], [-5.5, 35.1, -4.9, 36.3, -4.1, 37.1]], [-5.3, 36.4]);
      腕釧(bi, [[6.3, 35.5, 5.7, 36.7, 4.9, 37.5], [5.5, 35.1, 4.9, 36.3, 4.1, 37.1]], [5.3, 36.4]);
    });
  },
});
