// ─────────────────────────────────────────────────────────────────────────────
// 立菩薩身 · 立像共基（2026-07-09 親筆，立像之制第一基）
//
// 立像百二十指全格（無坐像折算）：頂0…臍48・陰藏60・胯64・股底88・膝92・
// 脛底116・踵120（錨悉出 liangdu 縱剖）。框用 執筆立（bi.ts）——呼者所受之
// bi 為坐框，此基**自建立框**於同 ctx/R，故落筆簿之筆可逕掛。
// 上身承菩薩坐身之制（頸肩軀幹胸腹條帛瓔珞頭面冠髻皆同其律）；
// 下身立姿：裙裳自腰垂踝、兩腿之分中褶、膝環褶、露足雙立仰蓮座。
// 兩臂留白由逐尊親運（臂 callback，受立框之 bi 與筆）。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具, 運筆具 } from '../bi.js';
import { 運筆, 執筆立 } from '../bi.js';
import {
  月輪, 頭光, 菩薩開臉, 點睛, 垂埵耳, 頸三道, 寶髻, 垂髮, 天髻,
  頸瓔, 胸瓔, 臂釧,
} from '../bujian/index.js';
import { 三峰寶冠 } from './pusa.js';

export interface 立菩薩相參 {
  臂: (bi: 筆具, 筆: 運筆具) => void;
  冠?: ((bi: 筆具) => void) | '無';
  髻?: '寶髻' | '天髻';
  胸瓔滴z?: number;
  月輪?: boolean;                       // 預設無（漢傳脅侍）；金剛界立尊開之
  條帛?: boolean;
}

export function 立菩薩身(呼bi: 筆具, 參: 立菩薩相參): void {
  const bi = 執筆立(呼bi.ctx, 呼bi.R);   // 立框自建（同 ctx 同 R，惟頂高提）
  const { A, dot, dim } = bi;
  const 筆 = 運筆(bi);
  const { M, L, C, Qk, S, 骨, 衣, 細, 逸, 陰, 陽 } = 筆;
  const 髻 = 參.髻 ?? '寶髻';

  // ── 光 ────────────────────────────────────────────────────────────────────
  if (參.月輪) 月輪(bi, { 心z: 60, 半徑: 64, 環距: 1.4 });   // 立像滿含之輪（金剛界立尊）
  頭光(bi);

  // ── 天衣：自肩後垂至膝側，外颺而回（立像之長帶）──────────────────────────
  dim(0.6, () => 衣(() => 逸(() => {
    M(-12.8, 30.5); C(-16.5, 44, -17.5, 62, -15.8, 78);
    C(-15.0, 85, -16.2, 91, -14.6, 95.5); S();
    M(-14.6, 95.5); Qk(-17.8, 97.5, -16.6, 100.5); S();
    M(12.8, 30.5); C(16.5, 44, 17.5, 62, 15.8, 78);
    C(15.0, 85, 16.2, 91, 14.6, 95.5); S();
    M(14.6, 95.5); Qk(17.8, 97.5, 16.6, 100.5); S();
  })));

  // ── 蓮座：立蓮一列（足下仰瓣）──────────────────────────────────────────────
  衣(() => {
    dim(0.75, () => { bi.E(0, 118.4, 11.5, 1.35); });
    const 瓣 = (px: number, tipZ: number, hw: number, 傾 = 0) => {
      M(px - hw, 124.2); C(px - hw * 0.95, 121.6, px + 傾 - hw * 0.35, tipZ + 0.5, px + 傾, tipZ);
      C(px + 傾 + hw * 0.35, tipZ + 0.5, px + hw * 0.95, 121.6, px + hw, 124.2); S();
    };
    for (const [px, k] of [[-8.6, -0.7], [-4.3, -0.3], [0, 0], [4.3, 0.3], [8.6, 0.7]] as const)
      瓣(px, 118.9, 2.6, k);
    dim(0.8, () => { M(-9.8, 125.4); Qk(0, 126.4, 9.8, 125.4); S(); });
  });

  // ── 軀幹（承坐身之制：頸柱肩丘軀側；腰下接立姿之胯）─────────────────────
  骨(() => {
    M(-2.65, 20.9); Qk(-2.95, 22.6, -2.9, 24.1); S();
    M(2.65, 20.9); Qk(2.95, 22.6, 2.9, 24.1); S();
    for (const d of [-1, 1] as const) {
      M(d * 2.9, 24.1); C(d * 5.8, 24.3, d * 9.0, 24.8, d * 11.2, 25.8);
      C(d * 12.6, 26.5, d * 13.35, 27.4, d * 13.4, 28.6); S();
    }
    陽(() => { M(-10.9, 32.4); C(-10.1, 36.5, -9.7, 40.5, -10.1, 46.5); Qk(-10.3, 49.2, -10.5, 51.0); S(); });
    陰(() => { M(10.9, 32.4); C(10.1, 36.5, 9.7, 40.5, 10.1, 46.5); Qk(10.3, 49.2, 10.5, 51.0); S(); });
  });
  細(() => {
    M(-2.4, 25.7); Qk(-6.0, 26.3, -9.4, 25.9); S();
    M(2.4, 25.7); Qk(6.0, 26.3, 9.4, 25.9); S();
    dim(0.5, () => {
      M(-5.6, 37.6); Qk(-3.9, 38.7, -2.0, 38.95); S();
      M(5.6, 37.6); Qk(3.9, 38.7, 2.0, 38.95); S();
    });
    M(0, 47.5); L(0, 48.4); S(); A(0, 48.0, 0.4, Math.PI * 0.25, Math.PI * 0.75);
  });

  // ── 裙裳：束腰結帶，垂踝——立姿之衣（兩腿之分・膝環褶・裾波）──────────────
  衣(() => {
    M(-10.4, 50.4); Qk(0, 49.5, 10.4, 50.4); S();
    M(-10.6, 51.6); Qk(0, 50.7, 10.6, 51.6); S();
    A(-3.4, 51.1, 0.6);
    逸(() => {
      M(-3.2, 51.8); Qk(-3.9, 54.2, -3.0, 56.6); S();
      M(-3.7, 51.8); Qk(-4.8, 54.0, -4.4, 56.2); S();
    });
    // 裙郭：微收於膝而展於裾（立姿之流）
    陽(() => { M(-10.5, 51.2); C(-10.2, 66, -9.4, 84, -9.8, 98); Qk(-10.0, 106, -9.2, 113.6); S(); });
    陰(() => { M(10.5, 51.2); C(10.2, 66, 9.4, 84, 9.8, 98); Qk(10.0, 106, 9.2, 113.6); S(); });
    // 裾：波狀一緣（讓足之中段）
    M(-9.2, 113.6); C(-7.0, 115.2, -5.6, 114.2, -4.6, 115.0); S();
    M(9.2, 113.6); C(7.0, 115.2, 5.6, 114.2, 4.6, 115.0); S();
    // 兩腿之分：中褶雙線（自胯下至裾，隨腿而行）
    逸(() => {
      M(-0.5, 57); C(-0.8, 72, -0.5, 90, -0.8, 108); Qk(-0.9, 111, -0.6, 113.8); S();
      dim(0.7, () => { M(0.7, 60); C(0.5, 75, 0.8, 92, 0.5, 106); S(); });
    });
    // 膝環褶（z 92 膝錨）與股間張力褶
    逸(() => {
      M(-8.6, 91.2); Qk(-4.9, 93.4, -1.4, 91.8); S();
      M(8.6, 91.6); Qk(4.9, 93.8, 1.4, 92.2); S();
      dim(0.7, () => {
        M(-8.9, 70); Qk(-5.4, 72.2, -1.8, 70.6); S();
        M(8.9, 72); Qk(5.4, 74.2, 1.8, 72.6); S();
      });
    });
  });

  // ── 露足：雙足外撇，立於蓮上（踵116→趾120）───────────────────────────────
  衣(() => {
    for (const d of [-1, 1] as const) {
      M(d * 2.2, 115.4); C(d * 2.6, 116.8, d * 3.6, 117.8, d * 5.6, 118.3);
      Qk(d * 6.9, 118.6, d * 7.4, 118.2); S();
      M(d * 1.6, 115.6); Qk(d * 1.4, 117.2, d * 2.0, 118.5); S();
      M(d * 2.0, 118.5); Qk(d * 4.4, 119.3, d * 7.2, 118.6); S();
      細(() => {
        for (const t of [0, 1, 2, 3]) {
          bi.A(d * (6.6 - t * 0.85), 118.35 - t * 0.12, 0.34 - t * 0.03, Math.PI * 1.05, Math.PI * 0.1);
        }
        dim(0.7, () => { M(d * 2.6, 116.6); Qk(d * 3.6, 116.9, d * 4.6, 116.8); S(); });
      });
    }
  });

  // ── 條帛：斜貫（中段讓胸前之手，承坐身兩段之路）──────────────────────────
  if (參.條帛 !== false) {
    dim(0.7, () => 衣(() => 逸(() => {
      M(9.7, 26.3); C(7.4, 28.6, 3.9, 32.2, 0.5, 36.0); S();
      M(10.6, 27.2); C(8.4, 29.4, 4.9, 33.0, 1.4, 36.8); S();
      M(-4.2, 41.2); C(-6.1, 43.8, -8.3, 46.6, -9.7, 49.0); S();
      M(-3.3, 42.4); C(-5.2, 44.8, -7.1, 47.4, -8.5, 49.8); S();
    })));
  }

  // ── 臂：逐尊親筆（受立框之 bi 與筆）──────────────────────────────────────
  參.臂(bi, 筆);

  // ── 嚴・頭面・冠髻・點睛 ──────────────────────────────────────────────────
  頸瓔(bi);
  胸瓔(bi, { 滴z: 參.胸瓔滴z ?? 30.7 });
  菩薩開臉(bi, '雞子');
  垂埵耳(bi);
  頸三道(bi);
  if (參.冠 !== '無') (參.冠 ?? 三峰寶冠)(bi);
  if (髻 === '天髻') 天髻(bi, 參.冠 === '無'); else 寶髻(bi, 參.冠 ? 0 : 1.7);
  垂髮(bi);
  點睛(bi);
  void dot; void 臂釧;
}
