// ─────────────────────────────────────────────────────────────────────────────
// 安底羅大將 · 藥師十二神將（antera|h）——天部武將忿怒形・八搩立像
// 儀軌（yigui.ts antera.h，待核候審）：鉞（東洋畫題綜覽〔單源待核〕；一說
// 拂子並陳）——長柄鉞植地，月刃向外；左手垂拳；勢自運候典。
// 杆軸 x=-16.35（肩甲頭光之外）；月刃全在頭光外，無所鑿。
// 十二支＝申（例1）。申冠小識（圓耳之猴）。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 神將身, 武拳, 執植臂, 垂臂 } from './shinsho.js';

export const 安底羅 = (bi: 筆具): void => 神將身(bi, {
  臂(bi, 筆) {
    const { M, L, C, Qk, S, 衣, 細 } = 筆;
    // 鉞：長杆植岩，月刃向外（觀者左）
    衣(() => {
      M(-15.9, 96.6); L(-15.9, 3.4); S();
      M(-16.8, 96.6); L(-16.8, 3.4); S();
      M(-15.9, 96.6); Qk(-16.35, 97.3, -16.8, 96.6); S();   // 石突
      bi.A(-16.35, 2.9, 0.52);                               // 頂鈕
      // 月刃：外弧大張，內弧淺附
      M(-16.9, 3.6); C(-19.6, 3.0, -21.6, 4.5, -21.7, 7.0); S();
      M(-21.7, 7.0); C(-21.6, 9.5, -19.6, 11.0, -16.9, 10.4); S();
      M(-16.9, 3.6); C(-17.95, 5.0, -18.15, 8.8, -16.9, 10.4); S();
      細(() => {
        M(-17.4, 4.0); C(-19.4, 3.6, -20.9, 4.9, -21.05, 7.0); S();
        M(-21.05, 7.0); C(-20.9, 9.1, -19.4, 10.4, -17.4, 10.0); S();
      });
      // 銎箍二（刃束於杆）
      M(-15.65, 4.4); L(-17.05, 4.4); S();
      M(-15.65, 9.6); L(-17.05, 9.6); S();
    });
    // 右臂探杆（蔽內建），拳握之；左垂拳
    const { 拳 } = 執植臂(bi, 筆, -1);
    武拳(bi, 筆, 拳[0], 拳[1], { 轉: -Math.PI / 2 });
    const 左 = 垂臂(bi, 筆, 1);
    武拳(bi, 筆, 左.拳[0], 左.拳[1], { 轉: Math.PI * 0.94, 鏡: -1, 比: 0.96 });
  },
  冠識(bi, 筆) {
    // 申冠小識：圓耳之猴——面輪・雙圓耳・眉弧
    const { M, Qk, S, 細 } = 筆;
    細(() => bi.dim(0.8, () => {
      bi.A(0, 8.6, 0.6);
      bi.A(-0.85, 8.35, 0.26); bi.A(0.85, 8.35, 0.26);
      M(-0.3, 8.4); Qk(0, 8.2, 0.3, 8.4); S();
    }));
  },
});
