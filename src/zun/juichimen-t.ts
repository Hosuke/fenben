// ─────────────────────────────────────────────────────────────────────────────
// 十一面觀自在 · 胎藏蘇悉地院（juichimen|t）——菩薩形・十搩立像
// 儀軌（yigui.ts juichimen.t，待核候審；docs/考據/十一面千手降三世.md
// T1071 玄奘譯逐字）：「左手執紅蓮花軍持，展右臂以掛數珠及作施無畏手。
// 其像作十一面，當前三面作慈悲相，左邊三面作瞋怒相，右邊三面作白牙上出相，
// 當後一面作暴惡大笑相，頂上一面作佛面像。」
// 面塔：本面（前三之中）＋上層四（左瞋二右牙二之近者）＋中層三（前慈一
// 左瞋一右牙一之遠者）＋頂佛一；後一大笑面隱（注）——正視可見十面＋隱一
// ＝十一，塔上排布自運候典；蘇悉地院坐立臂數〔待核〕取 T1071 立像。
// 立像百二十指全格（lipusa 共基）。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 立菩薩身 } from './lipusa.js';
import { 前臂 } from './pusa.js';
import { 手勢, 指寬 } from '../bujian/zhi.js';
import { 臂釧, 腕釧 } from '../bujian/index.js';

export const 十一面觀自在 = (bi0: 筆具): void => 立菩薩身(bi0, {
  月輪: false,
  髻: '無',   // 面塔即其頂（共基寶髻讓之——Codex 審 P2）
  臂(bi, 筆) {
    const { M, L, C, Qk, S, 骨, 衣, 細, 逸, 陰, 陽 } = 筆;
    const { A, dot, dim } = bi;
    // ── 尊左臂（觀者右）：屈肘腹前，執紅蓮軍持（瓶）───────────────────────
    骨(() => 陰(() => {
      M(13.4, 28.6); C(14.5, 32.2, 14.9, 37.0, 14.5, 42.0); S();
      M(10.9, 32.4); C(11.3, 35.6, 11.5, 39.0, 11.3, 42.0); S();
    }));
    骨(() => 陰(() => 前臂(bi, 筆, [14.1, 42.6], [7.2, 47.4], { 寬根: 2.4, 寬梢: 1.8, 弓: -0.2, 蔽: true, 肘帽: true })));
    // 軍持（kundika）：長頸鼓腹注口，紅蓮一枝自口出——先蔽其位（裙線讓瓶）
    {
      const { ctx, u, Y } = bi;
      ctx.save(); ctx.globalCompositeOperation = 'destination-out';
      ctx.beginPath();
      ctx.ellipse(6.4 * u, Y(49.4), 2.6 * u, 4.6 * u, 0, 0, 7);
      ctx.fill(); ctx.restore();
    }
    衣(() => {
      // 瓶身：鼓腹
      M(4.6, 48.6); C(4.0, 50.4, 4.4, 52.4, 5.9, 53.2); S();
      M(8.2, 48.6); C(8.8, 50.4, 8.4, 52.4, 6.9, 53.2); S();
      M(5.9, 53.2); Qk(6.4, 53.45, 6.9, 53.2); S();
      // 長頸與注口
      M(5.9, 48.4); L(6.05, 45.9); S(); M(6.9, 48.4); L(6.75, 45.9); S();
      M(5.75, 45.75); Qk(6.4, 45.35, 7.05, 45.75); S();
      M(6.15, 45.5); L(6.1, 44.6); S();
      細(() => { M(5.05, 48.9); Qk(6.4, 48.35, 7.75, 48.9); S(); });
      // 紅蓮：莖自瓶口斜上，瓣開
      逸(() => { M(6.3, 44.9); C(5.8, 42.8, 6.2, 40.8, 5.7, 38.9); S(); });
      M(4.2, 38.6); Qk(5.7, 39.7, 7.2, 38.6); S();
      M(4.2, 38.6); Qk(4.6, 36.6, 5.7, 36.1); S();
      M(7.2, 38.6); Qk(6.8, 36.6, 5.7, 36.1); S();
      細(() => { M(5.7, 39.1); L(5.7, 37.1); S(); M(4.95, 38.35); Qk(5.15, 37.4, 5.65, 36.9); S(); M(6.45, 38.35); Qk(6.25, 37.4, 5.75, 36.9); S(); });
    });
    // 執瓶之手：掌自前臂末覆瓶頸側（拇內四指外）
    {
      const s = 手勢(bi, 7.0, 47.5, 0.95, -1);
      s.衣(() => {
        for (const [i, 根a] of [-0.8, -0.2, 0.4, 1.0].entries()) {
          const 鏈 = s.指(根a, 0.3, Math.PI / 2 + 0.35 + i * 0.04, [1.35 - Math.abs(i - 1.3) * 0.14, 1.0 - Math.abs(i - 1.3) * 0.1], [0, 0.3]);
          s.畫(鏈, 指寬.根, 指寬.梢, { 紋: true });
        }
        const 拇 = s.指(-1.3, -0.5, Math.PI / 2 - 0.5, [1.1, 0.85], [0, -0.22]);
        s.畫(拇, 指寬.拇根, 指寬.拇梢, { 紋: true });
      });
    }
    // ── 尊右臂（觀者左）：展臂垂而外舒，施無畏掌（掌心向外）掛數珠 ────────
    骨(() => 陽(() => {
      M(-13.4, 28.6); C(-14.8, 32.8, -15.6, 38.4, -15.5, 44.5); S();
      M(-10.9, 32.4); C(-11.6, 36.4, -12.0, 40.8, -12.1, 44.5); S();
      M(-15.5, 44.5); C(-15.4, 49.5, -15.0, 54.0, -14.4, 58.0); S();
      M(-12.1, 44.5); C(-12.2, 49.0, -12.4, 53.2, -12.7, 56.6); S();
    }));
    // 施無畏掌（下垂展掌，五指舒，掌心向外＝觀者見掌）
    {
      const s = 手勢(bi, -13.6, 61.4, 1.0);
      s.衣(() => {
        s.M(-1.5, -2.8); s.Qk(-1.9, -1.0, -1.6, 0.6); s.S();      // 掌緣外
        s.M(1.2, -2.75); s.Qk(1.6, -1.2, 1.4, 0.4); s.S();        // 掌緣內
        for (const [i, 根a] of [-1.05, -0.35, 0.35, 1.05].entries()) {
          const 鏈 = s.指(根a, 0.3, Math.PI / 2 - 0.1 + i * 0.06,
            [1.65 - Math.abs(i - 1.4) * 0.18, 1.25 - Math.abs(i - 1.4) * 0.14], [0, 0.2]);
          s.畫(鏈, 指寬.根, 指寬.梢, { 紋: true });
        }
        const 拇 = s.指(-1.62, -0.7, Math.PI / 2 + 0.6, [1.3, 1.0], [0, -0.2]);
        s.畫(拇, 指寬.拇根, 指寬.拇梢, { 紋: true });
        s.細(() => {
          s.M(-0.9, -1.5); s.Qk(0, -1.9, 0.85, -1.45); s.S();     // 掌丘一線
          s.M(-0.55, -0.6); s.Qk(0.1, -0.95, 0.6, -0.5); s.S();
        });
      });
    }
    // 數珠：掛於腕，垂珠成環（「掛數珠」）
    細(() => {
      M(-14.6, 58.2); C(-15.4, 60.4, -15.2, 62.8, -14.2, 64.6); S();
      M(-12.5, 57.6); C(-11.9, 59.8, -12.0, 62.2, -12.9, 64.2); S();
      M(-14.2, 64.6); Qk(-13.55, 65.3, -12.9, 64.2); S();
      for (const t of [0, 1, 2, 3, 4, 5, 6]) {
        const a = Math.PI * (0.25 + t * 0.09);
        dot(-13.55 + Math.cos(a) * 1.65, 61.2 + Math.sin(a) * 3.5, 0.17);
      }
    });
    // 嚴
    臂釧(bi, 1, 12.1, 14.5, 33.4); 臂釧(bi, -1, 12.0, 14.3, 31.8);
    細(() => {
      腕釧(bi, [[-15.2, 57.4, -14.2, 57.9, -13.2, 57.5], [-15.1, 58.4, -14.1, 58.9, -13.1, 58.5]], [-14.1, 58.4]);
      腕釧(bi, [[8.9, 45.9, 8.0, 46.4, 7.1, 46.0], [8.8, 46.9, 7.9, 47.4, 7.0, 47.0]], [7.9, 46.9]);
    });
    // ── 面塔十一面（冠上；本面之外十面，後一大笑面隱注）─────────────────────
    const 蔽圓 = (x: number, z: number, r: number) => {
      const { ctx, u, Y } = bi;
      ctx.save(); ctx.globalCompositeOperation = 'destination-out';
      ctx.beginPath(); ctx.arc(x * u, Y(z), r * u, 0, 7); ctx.fill(); ctx.restore();
    };
    // 相之三式：慈=垂目微笑；瞋=怒眉圓睛；牙=上出小牙
    const 塔面 = (x: number, z: number, r: number, 相: '慈' | '瞋' | '牙' | '佛') => {
      蔽圓(x, z, r * 0.98);
      衣(() => A(x, z, r));
      細(() => {
        if (相 === '瞋') {
          M(x - r * 0.55, z - r * 0.28); L(x - r * 0.12, z - r * 0.1); S();
          M(x + r * 0.55, z - r * 0.28); L(x + r * 0.12, z - r * 0.1); S();
          dot(x - r * 0.3, z + r * 0.05, 0.11); dot(x + r * 0.3, z + r * 0.05, 0.11);
          M(x - r * 0.25, z + r * 0.5); Qk(x, z + r * 0.32, x + r * 0.25, z + r * 0.5); S();
        } else if (相 === '牙') {
          M(x - r * 0.5, z - r * 0.15); Qk(x - r * 0.28, z - r * 0.3, x - r * 0.08, z - r * 0.15); S();
          M(x + r * 0.5, z - r * 0.15); Qk(x + r * 0.28, z - r * 0.3, x + r * 0.08, z - r * 0.15); S();
          M(x - r * 0.3, z + r * 0.42); Qk(x, z + r * 0.58, x + r * 0.3, z + r * 0.42); S();
          M(x - r * 0.15, z + r * 0.48); L(x - r * 0.18, z + r * 0.22); S();   // 白牙上出
          M(x + r * 0.15, z + r * 0.48); L(x + r * 0.18, z + r * 0.22); S();
        } else {
          M(x - r * 0.5, z - r * 0.1); Qk(x - r * 0.28, z - r * 0.22, x - r * 0.08, z - r * 0.1); S();
          M(x + r * 0.5, z - r * 0.1); Qk(x + r * 0.28, z - r * 0.22, x + r * 0.08, z - r * 0.1); S();
          M(x - r * 0.25, z + r * 0.42); Qk(x, z + r * 0.6, x + r * 0.25, z + r * 0.42); S();
        }
        if (相 === '佛') { M(x - r * 0.4, z - r * 0.8); Qk(x, z - r * 1.25, x + r * 0.4, z - r * 0.8); S(); }
        else { M(x - r * 0.42, z - r * 0.82); Qk(x, z - r * 1.15, x + r * 0.42, z - r * 0.82); S(); }
      });
    };
    // 下層四（近）：左瞋二・右牙二（尊左＝x＋瞋怒，尊右＝x−白牙）
    塔面(3.1, 3.1, 0.8, '瞋'); 塔面(1.15, 2.5, 0.82, '瞋');
    塔面(-3.1, 3.1, 0.8, '牙'); 塔面(-1.15, 2.5, 0.82, '牙');
    // 中層三（遠）：前慈一・左瞋一・右牙一
    塔面(2.0, 0.6, 0.75, '瞋'); 塔面(0, 0.3, 0.78, '慈'); 塔面(-2.0, 0.6, 0.75, '牙');
    // 上層慈一＋頂佛一（後一大笑面在後，隱）
    塔面(0, -1.7, 0.75, '慈');
    塔面(0, -3.7, 0.82, '佛');
    衣(() => { A(0, -5.05, 0.4); });   // 頂佛肉髻
  },
});
