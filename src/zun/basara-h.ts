// ─────────────────────────────────────────────────────────────────────────────
// 伐折羅大將 · 藥師十二神將（basara|h）——天部武將忿怒形・八搩立像
// 儀軌（yigui.ts basara.h，待核候審）：右手振揚寶劍（nippon.com＋small-life
// 二源），左手垂拳〔文闕自運候典〕；十二支＝戌（例1）。
// 身承神將共基（shinsho.ts）；寶劍直身雙刃斜揚（θ=-0.32 鋒上外指），
// 劍貫頭光以蔽方先鑿；戌冠小識（垂耳之犬）。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 神將身, 蔽方, 武拳, 舉臂, 垂臂 } from './shinsho.js';

export const 伐折羅 = (bi: 筆具): void => 神將身(bi, {
  臂(bi, 筆) {
    const { M, L, Qk, S, 衣, 細 } = 筆;
    // 局變：劍軸自拳斜出（θ=-0.32，鋒向上外）
    const c = Math.cos(-0.32), s = Math.sin(-0.32);
    const p = (x: number, z: number): [number, number] =>
      [-13.9 + x * c - z * s, 12.25 + x * s + z * c];
    // 蔽帶：頭光讓劍
    蔽方(bi, [p(-1.5, 2.4), p(1.5, 2.4), p(1.2, -15.8), p(-1.2, -15.8)]);
    衣(() => {
      // 刃：雙緣收鋒，中脊一線
      M(...p(-0.62, 1.2)); L(...p(-0.34, -13.4)); S();
      M(...p(0.62, 1.2)); L(...p(0.34, -13.4)); S();
      M(...p(-0.34, -13.4)); Qk(...p(-0.1, -14.7), ...p(0, -15.3)); S();
      M(...p(0.34, -13.4)); Qk(...p(0.1, -14.7), ...p(0, -15.3)); S();
      細(() => { M(...p(0, 0.9)); L(...p(0, -12.9)); S(); });
      // 鍔：一文字雙線
      M(...p(-1.55, 1.5)); Qk(...p(0, 0.95), ...p(1.55, 1.5)); S();
      M(...p(-1.55, 2.1)); Qk(...p(0, 1.55), ...p(1.55, 2.1)); S();
      // 柄與頭（拳覆其中段）
      M(...p(-0.5, 2.2)); L(...p(-0.5, 3.15)); S();
      M(...p(0.5, 2.2)); L(...p(0.5, 3.15)); S();
      { const [px, pz] = p(0, 3.6); bi.A(px, pz, 0.55); }
    });
    // 臂上舉，拳與劍同軸；左垂拳
    const { 拳 } = 舉臂(bi, 筆, -1);
    武拳(bi, 筆, 拳[0], 拳[1], { 轉: -0.32 });
    const 左 = 垂臂(bi, 筆, 1);
    武拳(bi, 筆, 左.拳[0], 左.拳[1], { 轉: Math.PI * 0.94, 鏡: -1, 比: 0.96 });
  },
  冠識(bi, 筆) {
    // 戌冠小識：垂耳之犬——二垂耳・吻・鼻
    const { M, Qk, S, 細 } = 筆;
    細(() => bi.dim(0.8, () => {
      M(-1.05, 8.0); Qk(-1.5, 8.7, -1.05, 9.3); S();
      M(1.05, 8.0); Qk(1.5, 8.7, 1.05, 9.3); S();
      M(-0.55, 8.1); Qk(0, 7.8, 0.55, 8.1); S();
      M(-0.55, 8.85); Qk(0, 9.35, 0.55, 8.85); S();
      bi.A(0, 8.5, 0.14);
    }));
  },
});
