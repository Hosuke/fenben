// 儀軌（yigui.ts kubira.h，待核候審）：甲冑持械忿怒相（仏像ワールド），
// 持物無定式依作例——取太刀〔單源待核〕；十二支＝亥（主採一系注異說）。
// 身承神將共基（shinsho.ts，自此尊首筆熔出）；本尊專相：太刀高舉（刀先落，
// 拳覆柄；刀貫頭光處以蔽方先鑿——首將「刀犯頭光」之疵今治）、叉腰拳、亥冠小識。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 神將身, 蔽方, 武拳, 舉臂, 叉腰臂 } from './shinsho.js';

export const 宮毘羅 = (bi: 筆具): void => 神將身(bi, {
  臂(bi, 筆) {
    const { M, L, C, Qk, S, 衣, 細, 逸 } = 筆;
    // 太刀：直身微反，鋒上——自拳上出。先鑿刀身之帶（頭光環讓刀）再落筆
    蔽方(bi, [[-15.3, 10.2], [-11.9, 10.0], [-9.7, -9.4], [-12.4, -9.8]]);
    衣(() => {
      M(-13.1, 8.2); C(-12.4, 2.6, -11.5, -3.0, -10.4, -8.2); S();
      M(-14.1, 8.1); C(-13.5, 2.6, -12.7, -3.0, -11.6, -8.4); S();
      逸(() => { M(-10.4, -8.2); Qk(-10.7, -9.6, -11.6, -8.4); S(); });
      細(() => { M(-13.4, 6.4); C(-12.8, 1.4, -12.1, -3.4, -11.2, -7.4); S(); });
      // 鍔：橫一段
      M(-14.8, 9.1); Qk(-13.6, 8.3, -12.4, 9.0); S();
      M(-14.8, 9.9); Qk(-13.6, 9.2, -12.4, 9.8); S();
      // 柄：貫拳而下（拳蔽自斷其中段），柄頭小收
      M(-14.45, 9.9); L(-14.62, 14.1); S();
      M(-13.15, 9.8); L(-13.32, 14.0); S();
      M(-14.62, 14.1); Qk(-13.97, 14.55, -13.32, 14.0); S();
    });
    // 臂上舉（肘帽橋筆內建），拳握柄殿後（蔽以斷柄）
    const { 拳 } = 舉臂(bi, 筆, -1);
    武拳(bi, 筆, 拳[0], 拳[1]);
    // 尊左：叉腰拳按胯
    叉腰臂(bi, 筆, 1);
  },
  冠識(bi, 筆) {
    // 亥冠小識：額上獸首簡形（十二支主採一系〔待核〕）——一吻二耳
    const { M, L, Qk, S, 細 } = 筆;
    細(() => bi.dim(0.8, () => {
      M(-1.1, 8.9); Qk(0, 8.1, 1.1, 8.9); S();
      M(-0.55, 8.35); Qk(0, 8.7, 0.55, 8.35); S();
      M(-1.15, 8.75); L(-1.6, 7.9); S(); M(1.15, 8.75); L(1.6, 7.9); S();
    }));
  },
});
