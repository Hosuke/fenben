// ─────────────────────────────────────────────────────────────────────────────
// 毘羯羅大將 · 藥師十二神將（bikara|h）——天部武將忿怒形・八搩立像
// 儀軌（yigui.ts bikara.h，待核候審）：「高くかざした右手に三鈷杵、左手は腰」
// （諸ブログ綜合〔單源系〕＋興福寺東金堂三鈷杵互證）。
// 三鈷杵斜執（θ=-0.5）：上鈷穿頭光環處蔽方先鑿，下鈷外傾避前臂；
// 鬼目蓮弁帯之制承五鈷杵三昧耶（型制）。十二支＝子（例1）。子冠小識。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 神將身, 蔽方, 武拳, 舉臂, 叉腰臂 } from './shinsho.js';

export const 毘羯羅 = (bi: 筆具): void => 神將身(bi, {
  臂(bi, 筆) {
    const { M, L, C, Qk, S, 衣, 細 } = 筆;
    // 局變：杵軸自拳斜出（θ=-0.62，上鈷向上外、下鈷離頰）
    const c = Math.cos(-0.62), s = Math.sin(-0.62);
    const p = (x: number, z: number): [number, number] =>
      [-13.9 + x * c - z * s, 12.25 + x * s + z * c];
    // 蔽帶：頭光環讓上鈷
    蔽方(bi, [p(-1.2, -0.6), p(1.2, -0.6), p(1.2, -6.5), p(-1.2, -6.5)]);
    衣(() => {
      for (const e of [-1, 1] as const) {
        // 蓮弁帯（把之兩肩）
        M(...p(-0.85, e * 1.55)); Qk(...p(0, e * 1.95), ...p(0.85, e * 1.55)); S();
        M(...p(-0.7, e * 2.2)); Qk(...p(0, e * 2.55), ...p(0.7, e * 2.2)); S();
        // 中鈷：直而收鋒
        M(...p(-0.26, e * 2.6)); L(...p(-0.15, e * 5.7)); S();
        M(...p(0.26, e * 2.6)); L(...p(0.15, e * 5.7)); S();
        M(...p(-0.15, e * 5.7)); Qk(...p(0, e * 6.15), ...p(0.15, e * 5.7)); S();
        // 脇鈷二：嘴形內彎抱中鈷
        M(...p(-0.9, e * 2.5)); C(...p(-1.42, e * 3.5), ...p(-1.3, e * 4.6), ...p(-0.48, e * 5.3)); S();
        M(...p(0.9, e * 2.5)); C(...p(1.42, e * 3.5), ...p(1.3, e * 4.6), ...p(0.48, e * 5.3)); S();
      }
    });
    // 臂上舉，拳握把（鬼目沒於拳中）；左叉腰
    const { 拳 } = 舉臂(bi, 筆, -1);
    武拳(bi, 筆, 拳[0], 拳[1], { 轉: -0.62 });   // 拳杵同軸
    叉腰臂(bi, 筆, 1);
  },
  冠識(bi, 筆) {
    // 子冠小識：尖吻之鼠——雙圓耳・尖吻・鬚
    const { M, L, S, 細 } = 筆;
    細(() => bi.dim(0.8, () => {
      bi.A(-0.6, 7.95, 0.24); bi.A(0.6, 7.95, 0.24);
      M(-0.5, 8.2); L(0, 9.0); S(); M(0.5, 8.2); L(0, 9.0); S();
      M(-0.3, 8.72); L(-0.9, 8.62); S(); M(0.3, 8.72); L(0.9, 8.62); S();
    }));
  },
});
