// 結跏趺坐——「両手を腹前に置き定印を結ぶ」（法隆寺本）＋「hands forming
// the 法界定印, golden wheel on the lap」（onmark）＋京博＋仏法紹隆寺四源。
// 智拳印屬大日金輪（七獅白蓮座），混同禁。七寶圍繞屬別尊一字金輪曼荼羅，
// 星曼荼羅以北斗九曜十二宮廿八宿代之——故本筆唯尊身。親筆。
