import type { 筆具 } from '../bi.js';
import { 菩薩坐身, 前臂 } from './pusa.js';

export const 北斗星君 = (bi0: 筆具): void => 菩薩坐身(bi0, {
  月輪: false,
  冠: '無',
  髻: '天髻',   // 冠無而髻沉（天髻髮座之制——寶髻於冠無時浮頂之誤避之）
  臂(bi, 筆) {
    const { M, L, C, Qk, S, 骨, 衣, 細, 陰, 陽 } = 筆;
    // 笏：上圓下方，斜倚當胸（先落，手覆其緣）
    衣(() => {
      M(-1.7, 42.4); L(-1.15, 31.6); S();                       // 左緣
      M(1.7, 42.6); L(1.35, 31.7); S();                         // 右緣
      M(-1.15, 31.6); Qk(0.1, 30.9, 1.35, 31.7); S();           // 上圓
      M(-1.7, 42.4); L(1.7, 42.6); S();                         // 下方
      細(() => { M(-1.2, 33.4); Qk(0.05, 32.9, 1.25, 33.5); S(); });
    });
    // 兩臂拱執：肘於側，前臂內收，兩手覆笏之兩緣（腕距合律）
    骨(() => {
      陽(() => {
        M(-13.4, 28.6); C(-14.6, 31.2, -15.0, 34.4, -14.4, 37.6); S();
        M(-10.9, 32.4); C(-11.3, 34.2, -11.4, 35.9, -11.1, 37.4); S();
      });
      陰(() => {
        M(13.4, 28.6); C(14.6, 31.2, 15.0, 34.4, 14.4, 37.6); S();
        M(10.9, 32.4); C(11.3, 34.2, 11.4, 35.9, 11.1, 37.4); S();
      });
    });
    骨(() => 陽(() => 前臂(bi, 筆, [-14.0, 38.2], [-3.6, 38.6], { 寬根: 2.3, 寬梢: 1.7, 弓: 0.18, 蔽: true, 肘帽: true })));
    骨(() => 陰(() => 前臂(bi, 筆, [14.0, 38.2], [3.6, 38.9], { 寬根: 2.3, 寬梢: 1.7, 弓: -0.18, 蔽: true, 肘帽: true })));
    // 兩手：覆掌自腕出，四指鉤笏緣（指越緣而握，後落故覆笏上）
    for (const d of [-1, 1] as const) {
      衣(() => {
        const bz = 38.1 + (d > 0 ? 0.3 : 0);
        M(d * 3.5, bz - 1.05); Qk(d * 2.5, bz - 1.35, d * 1.6, bz - 1.2); S();   // 掌背緣
        M(d * 3.5, bz + 1.0); Qk(d * 2.6, bz + 1.25, d * 1.9, bz + 1.1); S();    // 掌下緣
        for (const t of [0, 1, 2, 3]) {
          M(d * (2.15 - t * 0.06), bz - 1.15 + t * 0.62);
          Qk(d * (1.15 - t * 0.05), bz - 1.28 + t * 0.62, d * (0.85 - t * 0.05), bz - 0.85 + t * 0.62); S();   // 四指鉤緣
        }
      });
    }
  },
});
