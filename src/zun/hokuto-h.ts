// （法隆寺本 [bo]）＋京博——白色円相內坐、持笏。本筆取**菩薩形持笏**
// （源所明許之一式）；官人形（束帶幞頭）別式候圖版目驗。
// 七星一制：逐星別相（服色持物之異）源未及，故共此一筆——貪狼・巨門・
// 祿存・文曲・廉貞・武曲・破軍七鍵同登（破軍不動明王式忿怒乃仏法紹隆寺
// 作例依存〔並陳〕，本筆不採）。笏：兩手拱執當胸，上圓下方。親筆。
