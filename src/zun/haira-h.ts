// ─────────────────────────────────────────────────────────────────────────────
// 波夷羅大將 · 藥師十二神將（haira|h）——天部武將忿怒形・八搩立像
// 儀軌（yigui.ts haira.h，待核候審）：弓矢（興福寺東金堂作例——新藥師寺
// 作例闕，或即1931補作軀）——左手執弓（弛而未張），右手持矢垂之；勢自運候典。
// 弓外傾（弦離臂緣寸許不相犯）；矢桿垂於體側拳握其中。十二支＝辰（例1）。
// 辰冠小識（岐角之龍）。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 神將身, 武拳, 執植臂, 垂臂 } from './shinsho.js';

export const 波夷羅 = (bi: 筆具): void => 神將身(bi, {
  臂(bi, 筆) {
    const { M, L, C, S, 衣, 細, 逸 } = 筆;
    // 弓：一體之幹過拳而弓，弦直懸兩弭
    衣(() => {
      M(20.6, 24.6); C(20.9, 32.2, 18.9, 40.2, 16.35, 45.8); S();   // 上幹
      M(16.35, 45.8); C(13.8, 51.4, 13.5, 58.8, 15.2, 66.2); S();   // 下幹
      M(20.35, 24.35); L(21.1, 23.3); S();                           // 上弭
      M(15.0, 66.35); L(15.75, 67.5); S();                           // 下弭
      細(() => bi.dim(0.75, () => { M(20.9, 24.15); L(15.55, 66.9); S(); }));  // 弦
    });
    // 左臂探弓（蔽內建鑿弓幹於臂後），拳握弓弝；右垂臂持矢
    const { 拳 } = 執植臂(bi, 筆, 1);
    武拳(bi, 筆, 拳[0], 拳[1], { 轉: Math.PI / 2, 鏡: -1 });
    // 矢：桿垂體側，羽上鏃下，拳握其中
    衣(() => 細(() => {
      M(-14.05, 49.8); L(-14.45, 64.6); S();
      逸(() => {
        M(-14.02, 50.9); L(-13.5, 52.2); S();
        M(-14.06, 52.1); L(-13.55, 53.4); S();
        M(-14.12, 50.9); L(-14.62, 52.3); S();
      });
      M(-14.42, 63.6); L(-14.72, 65.9); S();
      M(-14.48, 63.6); L(-14.15, 65.8); S();
    }));
    const 右 = 垂臂(bi, 筆, -1);
    武拳(bi, 筆, 右.拳[0], 右.拳[1], { 轉: Math.PI * 0.97 });
  },
  冠識(bi, 筆) {
    // 辰冠小識：岐角之龍——雙岐角・昂吻
    const { M, L, Qk, S, 細 } = 筆;
    細(() => bi.dim(0.8, () => {
      M(-0.4, 8.75); L(-0.75, 7.7); S(); M(-0.75, 7.7); L(-1.15, 7.95); S();
      M(0.4, 8.75); L(0.75, 7.7); S(); M(0.75, 7.7); L(1.15, 7.95); S();
      M(-0.35, 9.0); Qk(0, 9.45, 0.35, 9.0); S();
    }));
  },
});
