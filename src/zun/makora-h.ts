// ─────────────────────────────────────────────────────────────────────────────
// 摩虎羅大將 · 藥師十二神將（makora|h）——天部武將忿怒形・八搩立像
// 儀軌（yigui.ts makora.h，待核候審）：斧（一說〔單源待核〕；東洋畫題綜覽
// 作無持物並陳）——右手高舉短柄斧（髯刃向外），左手垂拳；勢自運候典。
// 斧首過頭光環處蔽方先鑿（五角鑿域覆柄與刃）；十二支＝卯（例1）。
// 卯冠小識（長耳之兔）。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 神將身, 蔽方, 武拳, 舉臂, 垂臂 } from './shinsho.js';

export const 摩虎羅 = (bi: 筆具): void => 神將身(bi, {
  臂(bi, 筆) {
    const { M, L, C, Qk, S, 衣, 細 } = 筆;
    // 蔽域：頭光環讓斧（柄帶＋刃域一體五角）
    蔽方(bi, [[-13.0, 10.8], [-14.9, 10.8], [-20.8, 9.8], [-20.8, 2.4], [-13.0, 2.4]]);
    衣(() => {
      // 柄：短而直，拳下無餘
      M(-14.35, 10.8); L(-14.35, 3.3); S();
      M(-13.45, 10.7); L(-13.45, 3.3); S();
      M(-14.35, 3.3); Qk(-13.9, 2.85, -13.45, 3.3); S();   // 柄頂圓收
      M(-14.5, 4.15); L(-13.3, 4.15); S();                  // 銎箍
      // 髯刃：上緣平出，刃弧大張，下緣垂鬚回收（鬚口一缺）
      M(-14.4, 3.7); C(-16.1, 3.1, -17.9, 3.2, -19.0, 4.1); S();
      M(-19.0, 4.1); C(-20.3, 5.2, -20.5, 7.5, -19.4, 9.6); S();
      M(-19.4, 9.6); C(-18.4, 9.55, -17.4, 9.3, -16.6, 9.4); S();
      M(-16.6, 9.4); Qk(-16.2, 8.9, -15.8, 9.35); S();   // 鬚口
      M(-15.8, 9.35); Qk(-15.0, 9.5, -14.4, 8.8); S();
      細(() => {
        // 刃稜貼邊（厚板之相，不作中空之隔）
        M(-18.65, 4.55); C(-19.75, 5.5, -19.9, 7.35, -18.95, 9.1); S();
        // 鋲二
        bi.A(-15.5, 5.1, 0.28); bi.A(-15.5, 7.8, 0.28);
      });
    });
    // 臂上舉，拳握柄；左垂拳
    const { 拳 } = 舉臂(bi, 筆, -1);
    武拳(bi, 筆, 拳[0], 拳[1]);
    const 左 = 垂臂(bi, 筆, 1);
    武拳(bi, 筆, 左.拳[0], 左.拳[1], { 轉: Math.PI * 0.94, 鏡: -1, 比: 0.96 });
  },
  冠識(bi, 筆) {
    // 卯冠小識：長耳之兔——雙長耳上竪・頭弧
    const { M, Qk, S, 細 } = 筆;
    細(() => bi.dim(0.8, () => {
      M(-0.5, 8.85); Qk(-0.75, 7.8, -0.4, 7.2); S();
      M(-0.4, 7.2); Qk(-0.18, 7.9, -0.28, 8.8); S();
      M(0.5, 8.85); Qk(0.75, 7.8, 0.4, 7.2); S();
      M(0.4, 7.2); Qk(0.18, 7.9, 0.28, 8.8); S();
      M(-0.5, 8.95); Qk(0, 9.3, 0.5, 8.95); S();
    }));
  },
});
