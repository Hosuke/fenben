// ─────────────────────────────────────────────────────────────────────────────
// 頞儞羅大將 · 藥師十二神將（anira|h）——天部武將忿怒形・八搩立像
// 儀軌（yigui.ts anira.h，待核候審）：鏡（東洋畫題綜覽「鏡、伐月廬」
// 〔單源待核〕，新藥師寺作例文闕）——右手舉鏡照面，左手垂拳；勢自運候典。
// 鏡盤過頭光環處蔽方先鑿（八角鑿域擬圓）；十二支＝未（例1）。
// 未冠小識（捲角之羊）。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 神將身, 蔽方, 武拳, 舉臂, 垂臂 } from './shinsho.js';

export const 頞儞羅 = (bi: 筆具): void => 神將身(bi, {
  臂(bi, 筆) {
    const { M, L, S, 衣, 細 } = 筆;
    // 蔽域：頭光環讓鏡盤（八角擬圓 r3.05）
    const cx = -13.9, cz = 6.4, r = 3.05, k = r * 0.7071;
    蔽方(bi, [[cx + r, cz], [cx + k, cz + k], [cx, cz + r], [cx - k, cz + k],
              [cx - r, cz], [cx - k, cz - k], [cx, cz - r], [cx + k, cz - k]]);
    // 鏡：盤雙環・中鈕・短柄入拳
    衣(() => {
      bi.A(-13.9, 6.4, 2.6);
      細(() => { bi.A(-13.9, 6.4, 2.05); bi.A(-13.9, 6.4, 0.35); });
      M(-14.25, 9.0); L(-14.25, 10.8); S();
      M(-13.55, 9.0); L(-13.55, 10.8); S();
    });
    // 臂上舉，拳握鏡柄；左垂拳
    const { 拳 } = 舉臂(bi, 筆, -1);
    武拳(bi, 筆, 拳[0], 拳[1]);
    const 左 = 垂臂(bi, 筆, 1);
    武拳(bi, 筆, 左.拳[0], 左.拳[1], { 轉: Math.PI * 0.94, 鏡: -1, 比: 0.96 });
  },
  冠識(bi, 筆) {
    // 未冠小識：捲角之羊——雙捲角・吻
    const { M, C, Qk, S, 細 } = 筆;
    細(() => bi.dim(0.8, () => {
      M(-0.35, 8.15); C(-1.05, 7.75, -1.5, 8.35, -0.9, 8.95); S();
      M(-0.9, 8.95); Qk(-0.62, 9.12, -0.45, 8.9); S();
      M(0.35, 8.15); C(1.05, 7.75, 1.5, 8.35, 0.9, 8.95); S();
      M(0.9, 8.95); Qk(0.62, 9.12, 0.45, 8.9); S();
      M(-0.3, 8.95); Qk(0, 9.3, 0.3, 8.95); S();
    }));
  },
});
