/** 蔽方：多邊形鑿域（destination-out）——械杆貫頭光、拳覆柄之屬先鑿後筆 */
export function 蔽方(bi: 筆具, 點: ReadonlyArray<readonly [number, number]>): void {
  const { ctx, u, Y } = bi;
  ctx.save(); ctx.globalCompositeOperation = 'destination-out';
  ctx.beginPath();
  點.forEach(([x, z], i) => { if (i) ctx.lineTo(x * u, Y(z)); else ctx.moveTo(x * u, Y(z)); });
  ctx.closePath(); ctx.fill(); ctx.restore();
}

/** 武拳：神將之拳（甲手短碩，郭閉三稜）——θ 轉拳向，鏡 反手。先鑿足印再落筆 */
export function 武拳(bi: 筆具, 筆: 運筆具, cx: number, cz: number,
  參: { 比?: number; 鏡?: 1 | -1; 轉?: number; 蔽?: boolean } = {}): void {
  const { 比 = 1, 鏡 = 1, 轉 = 0, 蔽 = true } = 參;
  const c = Math.cos(轉), s = Math.sin(轉);
  const p = (x: number, z: number): [number, number] =>
    [cx + (x * 鏡 * c - z * s) * 比, cz + (x * 鏡 * s + z * c) * 比];
  if (蔽) 蔽方(bi, [p(-2.1, -1.65), p(1.9, -2.05), p(2.3, 1.35), p(-1.7, 1.75)]);
  const { M, C, Qk, S, 衣, 細 } = 筆;
  const 落 = (f: (x: number, z: number) => void) => (x: number, z: number) => { const [px, pz] = p(x, z); f(px, pz); };
  const m = 落((x, z) => M(x, z));
  衣(() => {
    m(-2.0, -1.35);
    { const [x1, z1] = p(-2.4, -0.05), [x2, z2] = p(-2.0, 1.25), [x3, z3] = p(-0.8, 1.65); C(x1, z1, x2, z2, x3, z3); }
    { const [x1, z1] = p(0.6, 2.05), [x2, z2] = p(1.9, 1.45), [x3, z3] = p(2.0, 0.15); C(x1, z1, x2, z2, x3, z3); }
    { const [x1, z1] = p(2.0, -1.05), [x2, z2] = p(1.1, -1.65); Qk(x1, z1, x2, z2); }
    { const [x1, z1] = p(-0.5, -2.15), [x2, z2] = p(-2.0, -1.35); Qk(x1, z1, x2, z2); }
    S();
    細(() => {
      for (const t of [0, 1, 2]) {
        m(-1.3 + t * 1.05, -1.55 + t * 0.1);
        const [x1, z1] = p(-1.55 + t * 1.05, -0.65), [x2, z2] = p(-1.2 + t * 1.05, 0.35 + t * 0.1);
        Qk(x1, z1, x2, z2); S();
      }
    });
  });
}

