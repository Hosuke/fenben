  // 尊左臂（畫面右）：垂——前臂下行至膝上之鈴
  const 左上臂: 肢 = { a: 節之(12.4, 28.6, 1), b: 節之(15.2, 46.3, 2), ra: 2.6, rb: 2.1 };
