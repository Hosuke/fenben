  // 股（趺坐）：胯出而至膝；右股承右足故深大
  const 左股: 肢 = { a: 節之(8.8, 53.5, 3), b: 節之(20.5, 61.8, 5), ra: 5.0, rb: 4.3 };
  const 右股: 肢 = { a: 節之(-8.8, 53.5, 4), b: 節之(-20.5, 61.8, 6), ra: 5.0, rb: 4.3 };
  // 脛：右脛在前（右足仰於左股），左脛在後
  const 右脛: 肢 = { a: 節之(-21.0, 62.4, 7), b: 節之(4.5, 60.9, 9), ra: 3.1, rb: 2.4 };
