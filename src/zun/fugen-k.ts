  月輪, 頭光, 蓮華座, 菩薩開臉, 點睛, 垂埵耳, 頸三道, 寶髻, 垂髮,
  頸瓔, 胸瓔, 臂釧, 腕釧, 握拳橫, 握拳覆, 五鈷杵, 五鈷鈴,
