  五鈷杵(bi, -1.6, 36.4, 0.2, 1.2);
  握拳橫(bi, -1.6, 36.5, 1, true);
  細(() => { M(-3.3, 37.3); L(-4.3, 37.6); S(); M(-3.2, 35.8); L(-4.2, 35.6); S(); });
  五鈷鈴(bi, 15.4, 60.6, 1, false);
  握拳覆(bi, 15.3, 57.5, 0.85, true);

  // ── 嚴：頸瓔胸瓔臂釧腕釧（位隨骨）────────────────────────────────────────
