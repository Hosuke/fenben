import type { 筆具 } from '../bi.js';
import { 菩薩坐身, 上臂常勢, 前臂 } from './pusa.js';
import { 觸地印, 側拳, 臂釧, 腕釧 } from '../bujian/index.js';

export const 金剛波羅蜜 = (bi: 筆具): void => 菩薩坐身(bi, {
  髻: '天髻',
  冠: '無',
  臂(bi, 筆) {
    const { M, C, S, 骨, 細, 陰, 陽 } = 筆;
    // 尊右臂：垂而觸地（承阿閦之杆）
    骨(() => 陽(() => {
      M(-13.4, 28.6); C(-14.6, 31.6, -15.3, 36.5, -15.4, 42.2); S();
      M(-15.4, 42.2); C(-16.9, 46.6, -19.0, 52.6, -20.5, 58.3); S();
      M(-10.9, 32.2); C(-11.8, 36.5, -12.4, 41.0, -12.6, 44.0); S();
      M(-12.6, 44.0); C(-14.0, 48.4, -16.8, 53.8, -18.8, 58.0); S();
    }));
    觸地印(bi, -21.2, 62.6, 0.78, false);
    // 尊左臂：前臂上收，拳當胸
    骨(() => { 上臂常勢(筆, 1, 40.2); });
    骨(() => 陰(() => 前臂(bi, 筆, [13.7, 40.3], [5.4, 36.6],
      { 寬根: 2.5, 寬梢: 1.9, 弓: 0.4, 肘帽: true })));
    側拳(bi, 3.6, 35.4, -0.45, 1.15, -1);
    // 嚴
    臂釧(bi, -1, 12.1, 14.5, 33.4); 臂釧(bi, 1, 12.1, 14.5, 33.4);
    細(() => {
      腕釧(bi, [[-17.4, 55.4, -16.6, 56.2, -15.8, 56.9], [-18.1, 56.2, -17.3, 57.0, -16.5, 57.7]], [-17.0, 56.4]);
      腕釧(bi, [[6.4, 36.2, 5.8, 37.4, 5.0, 38.2], [5.6, 35.8, 5.0, 37.0, 4.2, 37.8]], [5.4, 37.1]);
    });
  },
});
