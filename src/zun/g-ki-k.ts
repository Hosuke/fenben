// 金剛嬉 · 金剛界（g-ki|k）——天女形・二拳置膝（嬉戲供養之天女）
// 儀軌（yigui.ts g-ki.k，待核候審）：兩手作拳置於膝股（shimma 成身会明記
// 「膝に置く」；bukkou「腰の側」異說並陳；SAT p.1057 No.26 親驗偏置膝）。
// 天髻無冠。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 菩薩坐身, 上臂常勢, 前臂 } from './pusa.js';
import { 側拳, 臂釧, 腕釧 } from '../bujian/index.js';

export const 金剛嬉 = (bi: 筆具): void => 菩薩坐身(bi, {
  髻: '天髻',
  冠: '無',
  臂(bi, 筆) {
    const { 骨, 細, 陰, 陽 } = 筆;
    骨(() => { 上臂常勢(筆, -1, 40.2); 上臂常勢(筆, 1, 40.2); });
    // 兩前臂垂而微外，拳置股際
    骨(() => 陽(() => 前臂(bi, 筆, [-13.8, 40.3], [-14.6, 51.4],
      { 寬根: 2.4, 寬梢: 1.8, 弓: 0.3, 肘帽: false })));
    骨(() => 陰(() => 前臂(bi, 筆, [13.8, 40.3], [14.6, 51.4],
      { 寬根: 2.4, 寬梢: 1.8, 弓: -0.3, 肘帽: false })));
    側拳(bi, -14.8, 53.8, -Math.PI / 2, 1.08);
    側拳(bi, 14.8, 53.8, Math.PI / 2, 1.08, -1);
    // 嚴
    臂釧(bi, -1, 12.1, 14.5, 33.4); 臂釧(bi, 1, 12.1, 14.5, 33.4);
    細(() => {
      腕釧(bi, [[-15.9, 50.8, -14.8, 51.3, -13.7, 50.9], [-15.8, 51.7, -14.7, 52.2, -13.6, 51.8]], [-14.7, 51.7]);
      腕釧(bi, [[15.9, 50.8, 14.8, 51.3, 13.7, 50.9], [15.8, 51.7, 14.7, 52.2, 13.6, 51.8]], [14.7, 51.7]);
    });
  },
});
