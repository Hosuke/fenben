// 金剛牙 · 金剛界（k-ge|k）——菩薩形・二拳向外（摧伏之尊，牙在口不在手）
// 儀軌（yigui.ts k-ge.k，待核候審）：兩手作拳向外當胸（shimma 成身会明記＋
// bukkou；『金剛頂経』口持金剛牙）。拳背向外——側拳鏡向反於喜。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 菩薩坐身, 上臂常勢, 前臂 } from './pusa.js';
import { 側拳, 臂釧, 腕釧 } from '../bujian/index.js';

export const 金剛牙 = (bi: 筆具): void => 菩薩坐身(bi, {
  臂(bi, 筆) {
    const { 骨, 細, 陰, 陽 } = 筆;
    骨(() => { 上臂常勢(筆, -1, 40.0); 上臂常勢(筆, 1, 40.0); });
    骨(() => 陽(() => 前臂(bi, 筆, [-13.7, 40.1], [-6.0, 36.1],
      { 寬根: 2.5, 寬梢: 1.9, 弓: -0.4, 肘帽: true })));
    骨(() => 陰(() => 前臂(bi, 筆, [13.7, 40.1], [6.0, 36.1],
      { 寬根: 2.5, 寬梢: 1.9, 弓: 0.4, 肘帽: true })));
    // 二拳向外當胸（指背向外側——鏡向與喜相反，兩拳間地稍寬）
    側拳(bi, -3.9, 34.8, 0.48, 1.2, -1);
    側拳(bi, 3.9, 34.8, -0.48, 1.2);
    // 嚴
    臂釧(bi, -1, 12.1, 14.5, 33.4); 臂釧(bi, 1, 12.1, 14.5, 33.4);
    細(() => {
      腕釧(bi, [[-6.9, 35.7, -6.3, 36.9, -5.5, 37.7], [-6.1, 35.3, -5.5, 36.5, -4.7, 37.3]], [-5.9, 36.6]);
      腕釧(bi, [[6.9, 35.7, 6.3, 36.9, 5.5, 37.7], [6.1, 35.3, 5.5, 36.5, 4.7, 37.3]], [5.9, 36.6]);
    });
  },
});
