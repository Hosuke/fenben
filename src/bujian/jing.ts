// 舉身光：漢傳龕形光——上尖如蓮瓣之全身光郭，雙緣（外骨內淡），
// 承丈光相之意（頭光另件已備，兩光相套為全）。
// 如意雲：雲頭雙渦一丘、雲尾一拖——漢式如意雲之簡體，唐卡上界之座。
// 律：光雲皆景，線淡於尊（dim），不與尊爭；位置比例由構圖層給。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 運筆 } from '../bi.js';

export interface 舉身光參 {
  底z?: number;    // 光郭之底（沒於座後，預設 62）
  頂z?: number;    // 光郭之尖（預設 -3，出頭光之上）
  半闊?: number;   // 底部半闊（預設 19.5）
  淡?: number;
}

// 舉身光：龕形雙緣，尖頂圓底，先落於尊後（構圖層當先呼此再呼白描）
export function 舉身光(bi: 筆具, 參: 舉身光參 = {}): void {
  const { 底z = 62, 頂z = -3, 半闊 = 19.5, 淡 = 0.5 } = 參;
  const { M, C, S, 衣, 細 } = 運筆(bi);
  const 中z = 底z * 0.42 + 頂z * 0.58;
  bi.dim(淡, () => {
    衣(() => {
      for (const d of [-1, 1] as const) {
        M(d * 半闊, 底z);
        C(d * (半闊 + 3.2), 中z + 14, d * (半闊 - 4.5), 中z - 16, 0, 頂z); S();
      }
    });
    細(() => {
      for (const d of [-1, 1] as const) {
        M(d * (半闊 - 2.2), 底z);
        C(d * (半闊 + 0.7), 中z + 13, d * (半闊 - 6.0), 中z - 14, 0, 頂z + 2.6); S();
      }
    });
  });
}

// 如意雲：心 (x,z)，比＝縮放，鏡＝-1 尾向反——雲頭雙渦一丘，雲尾一拖
export function 如意雲(bi: 筆具, x: number, z: number, 比 = 1, 鏡: 1 | -1 = 1, 淡 = 0.55): void {
  const { M, Qk, C, S, 衣, 細, 逸 } = 運筆(bi);
  const X = (a: number) => x + a * 比 * 鏡, Z = (b: number) => z + b * 比;
  bi.dim(淡, () => 衣(() => {
    // 雲頭：左渦→頂丘→右渦→底線相銜
    M(X(-2.7), Z(0.55));
    C(X(-3.5), Z(-0.6), X(-2.3), Z(-1.65), X(-1.1), Z(-1.0)); S();
    M(X(-1.1), Z(-1.0)); C(X(-0.6), Z(-1.9), X(0.9), Z(-2.0), X(1.35), Z(-1.05)); S();
    M(X(1.35), Z(-1.05)); C(X(2.5), Z(-1.6), X(3.45), Z(-0.45), X(2.55), Z(0.5)); S();
    M(X(2.55), Z(0.5)); Qk(X(1.4), Z(1.1), X(0.15), Z(0.9)); S();
    M(X(0.15), Z(0.9)); Qk(X(-1.5), Z(1.25), X(-2.7), Z(0.55)); S();
    // 渦心二旋（弧角隨鏡而翻，半徑取絕——Codex 審所正）
    細(() => {
      const 渦 = (a: number, b: number, r: number, a0: number, a1: number) => {
        if (鏡 === 1) bi.A(X(a), Z(b), r * Math.abs(比), a0, a1);
        else bi.A(X(a), Z(b), r * Math.abs(比), Math.PI - a1, Math.PI - a0);
      };
      渦(-1.85, -0.35, 0.5, Math.PI * 0.3, Math.PI * 1.9);
      渦(1.85, -0.3, 0.46, Math.PI * 1.2, Math.PI * 2.75);
    });
    // 雲尾：一拖二筆（逸，出鋒）
    逸(() => {
      M(X(2.9), Z(0.35)); C(X(4.6), Z(0.5), X(5.8), Z(0.15), X(7.0), Z(0.45)); S();
      M(X(3.1), Z(0.85)); C(X(4.5), Z(1.0), X(5.4), Z(0.75), X(6.2), Z(0.95)); S();
    });
  }));
}
