// ── 掌立之基：立掌向外（施無畏上／與願下） ───────────────────────────────────
function 掌立(bi: 筆具, x: number, z: number, 比: number, 向: '上' | '下'): void {
  const σ = 向 === '上' ? 1 : -1;
  const s = 手勢(bi, x, z, 比 * σ);   // 比負＝點對稱旋半周：上式一稿兩用
  // 掌郭：右緣拇丘、左緣小指丘（右手掌向外，拇在右）
  s.骨(() => {
    s.M(-1.6, 4.55); s.C(-2.45, 3.3, -2.6, 1.6, -2.1, -1.3); s.S();
    s.M(1.5, 4.6); s.C(2.5, 3.6, 2.8, 2.5, 2.62, 1.35); s.S();
    s.M(2.42, 0.5); s.Qk(2.25, -0.6, 1.98, -1.4); s.S();
  });
  // 五指：舒伸微扇（頭指之側拇指開）
  const 目: Array<[number, number, number, number[], number, number]> = [
    [1.65, -1.5, 0.10, [1.9, 1.4, 1.02], 0.92, 0.70],
    [0.55, -1.68, 0.02, [2.15, 1.6, 1.16], 指寬.根, 指寬.梢],
    [-0.55, -1.62, -0.05, [2.0, 1.5, 1.08], 指寬.根, 指寬.梢],
    [-1.6, -1.42, -0.15, [1.6, 1.15, 0.85], 指寬.小根, 指寬.小梢],
  ];
  s.衣(() => {
    for (const [根a, 根b, 斜, 長, w0, w1] of 目) {
      const 鏈 = s.指(根a, 根b, -Math.PI / 2 + 斜, 長, [0, 斜 * 0.5, 斜 * 0.6]);
      s.畫(鏈, w0, w1, { 紋: true });
    }
    // 拇指：自拇丘開張
    const 拇 = s.指(2.5, 1.25, -Math.PI / 2 + 0.58, [2.0, 1.62], [0, -0.18]);
    s.畫(拇, 指寬.拇根, 指寬.拇梢, { 紋: true, 甲: Math.abs(比) >= 2 });
  });
  // 指丫：根間三谷
  s.細(() => {
    for (const [ya, yb] of [[1.1, -1.5], [0.0, -1.6], [-1.08, -1.45]] as const)
      s.弧(ya, yb, 0.22, Math.PI * 1.1, Math.PI * 1.9);
  });
  // 掌之丘壑：三紋收於掌內，不犯拇丘（名从俗，線依犍陀羅之工）
  s.細(() => {
    s.M(-2.0, -0.9); s.C(-0.9, -1.35, 0.5, -1.45, 1.42, -1.2); s.S();
    s.M(1.55, -0.35); s.C(0.6, -0.05, -0.6, 0.15, -1.7, 0.55); s.S();
    s.M(1.5, -0.7); s.C(1.45, 0.7, 1.1, 2.1, 0.45, 3.2); s.S();
  });
  // 腕
  s.骨(() => { s.M(-1.65, 4.6); s.L(-1.95, 5.75); s.S(); s.M(1.55, 4.65); s.L(1.8, 5.8); s.S(); });
  s.細(() => { s.M(-1.4, 4.85); s.Qk(0, 5.25, 1.35, 4.9); s.S(); });
}

