// ═══ 拳基：正觀之拳（背向觀者，拇在左）——金剛拳・忿怒拳・期剋印之基 ═════════
interface 拳相 {
  豎頭?: boolean;   // 頭指獨豎（期剋）
  豎小?: boolean;   // 小指並豎（忿怒）
  鈎?: number;      // 豎指之鈎（0 直 → 1 深鈎）
  拇: '入' | '壓';  // 入＝大指入掌四指握之（金剛拳）；壓＝大指押中無名甲上
}

function 拳基(bi: 筆具, x: number, z: number, 比: number, 相: 拳相): void {
  const s = 手勢(bi, x, z, 比);
  const 鈎 = 相.鈎 ?? 0;
  // 拳郭：圓渾之塊（圓滿之相——弧不露稜）
  s.骨(() => {
    s.M(-2.55, -1.2);
    s.C(-1.7, -2.05, 0.4, -2.2, 1.6, -1.6);
    s.C(2.5, -1.15, 2.8, -0.3, 2.7, 0.5);
    s.C(2.6, 1.4, 1.9, 2.05, 0.9, 2.25);
    s.C(-0.5, 2.5, -1.8, 2.3, -2.3, 1.5);
    s.Qk(-2.65, 0.4, -2.55, -1.2); s.S();
  });
  // 掌骨之峰：脊上四丘（丘足踏脊，弧不越郭）
  s.細(() => {
    for (const [ka, kb] of [[-1.72, -1.28], [-0.6, -1.52], [0.55, -1.48], [1.45, -1.12]] as const)
      s.弧(ka, kb, 0.3, Math.PI * 1.2, Math.PI * 1.85);
  });
  // 四指之列（左頭右小——右拳背觀，拇在左）：豎者豎，餘者屈為柱
  const 列: Array<[string, number]> = [['頭', -1.8], ['中', -0.7], ['無名', 0.4], ['小', 1.45]];
  s.衣(() => {
    for (const [名, kx] of 列) {
      if (相.豎頭 && 名 === '頭') {
        const 鏈 = s.指(kx + 0.2, -1.3, -Math.PI / 2 + 0.06, [1.9, 1.5, 1.05],
          [0, 0.34 * 鈎, 0.52 * 鈎]);
        s.畫(鏈, 指寬.根, 指寬.梢, { 紋: true, 甲: Math.abs(比) >= 2 });
        continue;
      }
      if (相.豎小 && 名 === '小') {
        const 鏈 = s.指(kx + 0.15, -1.15, -Math.PI / 2 - 0.06, [1.5, 1.1, 0.8],
          [0, -0.3 * 鈎, -0.46 * 鈎]);
        s.畫(鏈, 指寬.小根, 指寬.小梢, { 紋: true, 甲: Math.abs(比) >= 2 });
        continue;
      }
      // 屈指之柱：本節見面，中節內折（畫其可見）
      const 鏈 = s.指(kx, -1.3, Math.PI / 2 - 0.07, [1.5, 1.15], [0, 0.22]);
      s.畫(鏈, 0.95, 0.86, { 紋: true });
      s.細(() => { s.弧(kx - 0.1, 1.55, 0.28, Math.PI * 0.15, Math.PI * 0.85); });
    }
    if (相.拇 === '入') {
      // 大指入掌：拇丘之腹與頭指覆拇之折——拇不可見
      s.M(-2.45, 0.55); s.Qk(-2.05, 1.6, -1.15, 1.95); s.S();
      s.細(() => { s.M(-1.9, 0.0); s.Qk(-1.68, 0.75, -1.82, 1.4); s.S(); });
    } else {
      // 大指押中指無名指甲上：橫過拳前
      const 拇 = s.指(-2.35, 0.6, -0.22, [1.6, 1.38], [0, 0.3]);
      s.畫(拇, 指寬.拇根, 指寬.拇梢, { 紋: true, 甲: Math.abs(比) >= 2 });
    }
  });
  // 腕：近直而相隨
  s.骨(() => { s.M(-1.7, 2.35); s.L(-1.82, 3.6); s.S(); s.M(1.5, 2.3); s.L(1.62, 3.55); s.S(); });
  s.細(() => { s.M(-1.45, 2.6); s.Qk(-0.2, 3.0, 1.2, 2.6); s.S(); });
}

// ═══ 六種拳（大日經疏卷十三，疏文原數六） ════════════════════════════════════

// 金剛拳：大指入於掌中，餘四指握之
export function 金剛拳(bi: 筆具, x: number, z: number, 比 = 1): void {
  拳基(bi, x, z, 比, { 拇: '入' });
}

// 忿怒拳：金剛拳而豎頭指小指，二鈎相向
export function 忿怒拳(bi: 筆具, x: number, z: number, 比 = 1): void {
  拳基(bi, x, z, 比, { 拇: '壓', 豎頭: true, 豎小: true, 鈎: 0.55 });
}

// 蓮華拳（胎拳）：四指握，大指壓頭指外側節（壓式；立式並傳〔待核定本〕）
export function 蓮華拳(bi: 筆具, x: number, z: number, 比 = 1): void {
  const s = 手勢(bi, x, z, 比);
  // 側觀（拇側向觀者）：背之脊自腕隆至頭指本節，頭指捲為螺
  s.骨(() => {
    s.M(-2.6, -1.4); s.C(-1.4, -2.05, 0.3, -2.1, 1.55, -1.5);
    s.C(2.45, -1.05, 2.75, -0.15, 2.35, 0.6);
    s.Qk(1.95, 1.2, 1.2, 1.35); s.S();
    // 掌根至小指緣：下緣圓滿，收就螺尾
    s.M(-2.6, -1.3); s.C(-3.0, -0.2, -2.7, 1.0, -1.8, 1.65);
    s.C(-1.1, 2.1, -0.1, 2.2, 0.75, 1.9); s.Qk(1.05, 1.78, 1.2, 1.35); s.S();
  });
  s.衣(() => {
    // 頭指之螺：中節末節內旋一線（畫其可見）
    s.M(1.6, -0.7); s.C(2.1, -0.3, 2.1, 0.35, 1.6, 0.7);
    s.Qk(1.15, 0.95, 0.7, 0.85); s.S();
    // 中指本節：頭指之後微出
    s.逸(() => { s.M(0.9, -1.9); s.Qk(1.7, -1.72, 2.2, -1.15); s.S(); });
    // 大指：自拇丘斜上，末節壓頭指外側節
    const 拇 = s.指(-1.1, 1.3, -0.9, [1.9, 1.5], [0, 0.65]);
    s.畫(拇, 指寬.拇根, 指寬.拇梢, { 紋: true, 甲: Math.abs(比) >= 2 });
  });
  // 節紋：頭指本節之折
  s.細(() => { s.M(2.15, -1.0); s.Qk(2.42, -0.6, 2.3, -0.2); s.S(); });
  // 腕：左出二線近平
  s.骨(() => { s.M(-2.6, -1.4); s.L(-3.75, -1.5); s.S(); s.M(-2.7, 0.9); s.L(-3.85, 1.0); s.S(); });
  s.細(() => { s.M(-2.9, -0.95); s.Qk(-3.15, -0.1, -2.95, 0.65); s.S(); });
}

// ── 縛基：兩掌相叉之郭（內縛外縛共之） ──────────────────────────────────────
function 縛郭(bi: 筆具, x: number, z: number, 比: number): void {
  for (const 鏡 of [-1, 1]) {
    const s = 手勢(bi, x, z, 比, 鏡);
    s.骨(() => {
      s.M(4.35, 0.4); s.C(4.1, -0.9, 3.0, -1.75, 1.55, -1.8);
      s.Qk(0.7, -1.8, 0.08, -1.42); s.S();
      s.M(4.35, 0.4); s.C(3.9, 1.7, 2.3, 2.6, 0.05, 2.65); s.S();
    });
    s.骨(() => { s.M(4.3, 0.15); s.L(5.5, 0.05); s.S(); s.M(3.7, 1.95); s.L(4.8, 2.25); s.S(); });
    s.細(() => { s.M(4.45, 0.55); s.Qk(4.75, 1.15, 4.35, 1.8); s.S(); });
  }
}

// 內縛：十指交於掌內——外惟見兩背之丘與本節入縫之列
export function 內縛(bi: 筆具, x: number, z: number, 比 = 1): void {
  縛郭(bi, x, z, 比);
  const 中 = 手勢(bi, x, z, 比);
  中.細(() => { 中.M(0, -1.35); 中.Qk(0.06, -0.3, 0, 0.78); 中.S(); });
  // 本節之列：左右各四，斜沒入中縫（左先右後，右壓左）
  for (const 鏡 of [-1, 1]) {
    const s = 手勢(bi, x, z, 比, 鏡);
    s.衣(() => {
      for (const [根a, 根b] of [[3.2, -1.12], [2.35, -1.45], [1.5, -1.58], [0.72, -1.5]] as const) {
        const 鏈 = s.指(根a, 根b, Math.PI - 1.05, [0.9, 0.8], [0, 0.3]);
        s.畫(鏈, 0.88, 0.78, { 紋: false });
      }
    });
  }
}

// 外縛：十指交於掌外，指頭露外——八指相叉橫臥兩背，二拇交於下
export function 外縛(bi: 筆具, x: number, z: number, 比 = 1): void {
  縛郭(bi, x, z, 比);
  const L = 手勢(bi, x, z, 比, -1), R = 手勢(bi, x, z, 比, 1);
  // 相叉之織：八指人字相錯於兩背之上，梢向彼側而沒——逐對一左一右相壓
  const 列: Array<[number, number, number, number[], number, number]> = [
    [0.55, -1.5, 0.42, [1.75, 1.35], 1.0, 0.78],
    [1.55, -1.32, 0.55, [1.8, 1.4], 1.05, 0.8],
    [2.5, -0.95, 0.68, [1.6, 1.25], 0.9, 0.68],
  ];
  for (let i = 0; i < 列.length; i++) {
    const [根a, 根b, 斜, 長, w0, w1] = 列[i];
    for (const s of i % 2 ? [R, L] : [L, R]) {
      s.衣(() => {
        const 鏈 = s.指(根a, 根b, Math.PI - 斜, 長, [0, 0.28]);
        s.畫(鏈, w0, w1, { 紋: false, 甲: Math.abs(比) >= 2 });
      });
    }
  }
  // 小指之梢：外緣微露一對（畫其可見，弗使網密）
  for (const s of [L, R]) {
    s.衣(() => {
      const 鏈 = s.指(3.2, -0.5, Math.PI - 0.8, [1.15, 0.9], []);
      s.畫(鏈, 0.78, 0.6, { 紋: false, 甲: Math.abs(比) >= 2 });
    });
  }
  // 二拇：交於下緣，右壓左
  for (const s of [L, R]) {
    s.衣(() => {
      const 拇 = s.指(-2.55, 1.6, -0.35, [1.5, 1.15], [0, 0.26]);
      s.畫(拇, 指寬.拇根, 指寬.拇梢, { 紋: false, 甲: Math.abs(比) >= 2 });
    });
  }
}

// ── 豎拳直指之核：下拳豎一指、上拳握之（智拳印・如來拳共之） ─────────────────
// 直指：'頭'＝左拳豎頭指（智拳印）；'拇'＝左蓮華拳豎大指（如來拳）
function 拳持拳(bi: 筆具, x: number, z: number, 比: number, 直指: '頭' | '拇'): void {
  const s = 手勢(bi, x, z, 比);
  const 指w = 直指 === '頭' ? [1.0, 0.9] : [1.2, 1.05];
  // 直指：兩拳之隙見一節
  s.衣(() => {
    const 鏈 = s.指(0, 1.35, -Math.PI / 2, [1.75, 1.7], [0, 0.02]);
    s.畫(鏈, 指w[0], 指w[1], { 紋: true });
  });
  // 下拳（左）：背向前，頂開於直指之根
  s.骨(() => {
    s.M(-2.9, 2.1); s.C(-3.15, 3.3, -2.75, 4.35, -1.55, 4.9);
    s.C(-0.1, 5.55, 2.0, 5.2, 2.75, 4.2);
    s.C(3.2, 3.3, 3.05, 2.2, 2.55, 1.6);
    s.Qk(1.35, 1.05, 0.62, 1.1); s.S();
    s.M(-0.62, 1.15); s.Qk(-1.7, 1.2, -2.9, 2.1); s.S();
  });
  // 下拳四指之柱（本節見面）與拇
  s.衣(() => {
    for (const kx of [-1.95, -0.95, 0.95, 1.9]) {
      const 鏈 = s.指(kx, 1.6, Math.PI / 2 - 0.05 * Math.sign(kx), [1.4, 1.1], [0, 0.2]);
      s.畫(鏈, 0.92, 0.84, { 紋: true });
    }
    if (直指 === '頭') {
      // 左拇縱傍頭指之根
      const 拇 = s.指(-2.5, 2.6, -Math.PI / 2 + 0.32, [1.5, 1.3], [0, -0.15]);
      s.畫(拇, 指寬.拇根, 指寬.拇梢, { 紋: true, 甲: Math.abs(比) >= 2 });
    }
  });
  // 上拳（右）：橫握——四指裹於直指，背見四列
  s.骨(() => {
    s.M(-3.2, -4.4); s.C(-3.45, -3.0, -2.6, -1.95, -1.1, -1.75);
    s.C(0.3, -1.55, 2.0, -1.9, 2.8, -2.9);
    s.C(3.5, -3.9, 3.3, -5.0, 2.5, -5.75);
    s.C(1.3, -6.6, -1.2, -6.5, -2.3, -5.7);
    s.Qk(-3.05, -5.2, -3.2, -4.4); s.S();
  });
  s.衣(() => {
    // 裹指四列：弧隨拳轉，端貼於郭（端必有著）；節紋逐列小弧相錯
    const 列: Array<[number, number, number]> = [
      [2.45, -2.5, -1.75], [2.75, -3.35, -2.0], [2.7, -4.2, -2.05], [2.3, -5.05, -1.85],
    ];
    for (const [x0, r, x1] of 列) {
      s.M(x0, r); s.C(1.15, r - 0.34, -0.55, r - 0.34, x1, r + 0.06); s.S();
    }
    s.細(() => {
      s.M(1.05, -2.75); s.Qk(0.95, -3.05, 1.1, -3.55); s.S();
      s.M(-0.85, -3.75); s.Qk(-0.95, -4.05, -0.82, -4.5); s.S();
    });
    if (直指 === '頭') {
      // 頭指之尖出於拳頂，右拇覆壓其上
      s.弧(0, -6.15, 0.5, Math.PI * 1.05, Math.PI * 1.95);
      s.細(() => { s.弧(0.05, -6.02, 0.28, Math.PI * 1.25, Math.PI * 1.85); });
      s.M(2.3, -5.5); s.Qk(1.25, -6.25, 0.4, -6.2); s.S();
      s.M(2.55, -4.85); s.Qk(1.5, -5.42, 0.78, -5.6); s.S();
    } else {
      // 如來拳：左拇全入右拳，右拇自斜覆其前（納於郭內）
      const 拇 = s.指(2.55, -3.0, Math.PI - 0.72, [1.7, 1.4], [0, -0.28]);
      s.畫(拇, 指寬.拇根, 指寬.拇梢, { 紋: true, 甲: Math.abs(比) >= 2 });
    }
  });
  // 腕：下拳自下來二線近直；上拳之臂自右下來二線相隨
  s.骨(() => {
    s.M(-1.9, 5.05); s.L(-2.05, 6.3); s.S(); s.M(1.9, 5.1); s.L(2.05, 6.3); s.S();
    s.M(3.42, -4.3); s.L(4.72, -3.95); s.S(); s.M(3.05, -2.7); s.L(4.4, -2.35); s.S();
  });
  s.細(() => {
    s.M(-1.55, 5.35); s.Qk(0, 5.8, 1.55, 5.4); s.S();
    s.M(3.65, -3.95); s.Qk(3.95, -3.35, 3.7, -2.85); s.S();
  });
}

// 如來拳：左蓮華拳豎大指，右金剛拳握之（右上左下）
export function 如來拳(bi: 筆具, x: number, z: number, 比 = 1): void {
  拳持拳(bi, x, z, 比, '拇');
}

