// 部件 · 手——逐印逐指之地（造像標準 9.3；繁筆有所專施之首）
//
// 出典：儀軌「印法」字段逐指之述；手之量度：掌縱七指廣五指、中指五指、
// 巨指四指兩節（T1419 p.0943c10–16）；指形取飛天之柔（敦煌肢體語言：
// 指節有律而梢自揚）。
// 四式：握拳橫・握拳覆（簡拳，候他尊）；執杵手・執鈴手（逐指，2026-07-05）。
// 律：骨線立掌郭，衣線走指，細線點甲；位置隨尊之骨。皆親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 運筆 } from '../bi.js';

// 握拳橫：拳背向前橫執一軸（杵）；心 (x,z)，軸自拳中過
export function 握拳橫(bi: 筆具, x: number, z: number, 比 = 1, 輕 = false): void {
  const { M, Qk, C, S, 骨, 衣, 細 } = 運筆(bi);
  const X = (dx: number) => x + dx * 比, Z = (dz: number) => z + dz * 比;
  const 郭 = 輕 ? 衣 : 骨;
  // 拳郭：閉而圓
  郭(() => {
    M(X(-1.45), Z(-1.15)); C(X(-1.75), Z(-0.1), X(-1.6), Z(0.9), X(-0.8), Z(1.35));
    C(X(0.2), Z(1.85), X(1.35), Z(1.55), X(1.75), Z(0.8));
    C(X(2.1), Z(0.0), X(1.95), Z(-1.0), X(1.4), Z(-1.45));
    C(X(0.5), Z(-2.1), X(-0.9), Z(-1.95), X(-1.45), Z(-1.15)); S();
  });
  衣(() => {
    // 指根之脊：拳上一弧
    M(X(-1.1), Z(-1.3)); Qk(X(0.3), Z(-1.7), X(1.3), Z(-1.15)); S();
    // 指縫：一道（多則亂郭）
    細(() => { M(X(0.35), Z(-1.45)); Qk(X(0.48), Z(-0.45), X(0.36), Z(0.65)); S(); });
    // 拇指：自左緣抱軸而上
    M(X(-1.5), Z(0.1)); Qk(X(-0.9), Z(-0.55), X(-0.1), Z(-0.7)); S();
  });
}

// 握拳覆：自上覆執一豎柄（鈴）；心 (x,z)＝拳心，柄自拳下出
export function 握拳覆(bi: 筆具, x: number, z: number, 比 = 1, 輕 = false): void {
  const { M, Qk, C, S, 骨, 衣, 細 } = 運筆(bi);
  const X = (dx: number) => x + dx * 比, Z = (dz: number) => z + dz * 比;
  const 郭 = 輕 ? 衣 : 骨;
  // 拳郭：覆掌之丘
  郭(() => {
    M(X(-1.7), Z(0.35)); C(X(-1.75), Z(-0.6), X(-1.0), Z(-1.3), X(0.05), Z(-1.3));
    C(X(1.1), Z(-1.3), X(1.85), Z(-0.6), X(1.8), Z(0.4));
    Qk(X(1.7), Z(1.1), X(1.1), Z(1.3)); S();
    M(X(1.1), Z(1.3)); Qk(X(0), Z(1.55), X(-1.15), Z(1.25));
    Qk(X(-1.65), Z(1.0), X(-1.7), Z(0.35)); S();
  });
  衣(() => {
    // 指節之列：覆掌前緣四拱
    for (const fx of [-1.05, -0.35, 0.35, 1.05])
      bi.A(X(fx), Z(1.05), 0.34 * 比, Math.PI * 1.05, Math.PI * 0.05);
    // 拇指：側抱柄
    M(X(-1.6), Z(0.5)); Qk(X(-1.0), Z(1.15), X(-0.25), Z(1.3)); S();
  });
}

// 執杵手：右手橫執杵軸（軸傾 θ），拳背向觀者——四指裹軸逐見其節，
// 拇指反扣軸下，指梢與拇指尖相就（飛天之柔：指帶微弧不僵直）
export function 執杵手(bi: 筆具, x: number, z: number, θ = 0, 比 = 1): void {
  const { M, Qk, C, S, 骨, 衣, 細, 逸 } = 運筆(bi);
  const cos = Math.cos(θ), sin = Math.sin(θ);
  const P = (a: number, b: number): [number, number] =>
    [x + (a * cos - b * sin) * 比, z + (a * sin + b * cos) * 比];
  const 至M = (a: number, b: number) => { const [px, pz] = P(a, b); M(px, pz); };
  const 至Q = (ca: number, cb: number, a: number, b: number) => {
    const [cx, cz] = P(ca, cb), [px, pz] = P(a, b); Qk(cx, cz, px, pz);
  };
  const 至C = (c1a: number, c1b: number, c2a: number, c2b: number, a: number, b: number) => {
    const [x1, z1] = P(c1a, c1b), [x2, z2] = P(c2a, c2b), [px, pz] = P(a, b);
    C(x1, z1, x2, z2, px, pz);
  };
  // 掌背：自腕至指根之脊（骨線立形）
  骨(() => {
    至M(-2.75, -0.85); 至Q(-2.0, -1.55, -1.15, -1.62); S();   // 腕上緣→指根脊起
    至M(-2.8, 0.35); 至Q(-2.55, 0.9, -2.15, 1.05); S();       // 腕下緣→拇指根
  });
  衣(() => {
    // 線描畫其可見：四指裹於軸後乃隱，所見者掌背之丘、指根之脊、
    // 軸下微露之四梢、橫過之拇指——解剖知其隱而不畫其隱
    // 指根之脊：橫貫掌背前緣
    至M(-1.15, -1.62); 至Q(0.15, -1.85, 1.42, -1.55); S();
    // 掌背右緣：脊末收向軸
    至M(1.42, -1.55); 至Q(1.62, -1.1, 1.55, -0.55); S();
    // 四梢：軸下微露（各一小弧，梢向掌心回）
    逸(() => {
      for (const t of [0, 1, 2, 3]) {
        const a0 = 1.15 - t * 0.62;
        至M(a0 + 0.22, 0.72); 至Q(a0 + 0.1, 1.18 - t * 0.03, a0 - 0.22, 1.28 - t * 0.05); S();
      }
      // 拇指：自根沿軸下橫過，與食指梢相就
      至M(-2.15, 1.05); 至C(-1.45, 1.52, -0.6, 1.6, 0.05, 1.34); S();
    });
    // 腕節一線
    細(() => { 至M(-2.95, -0.5); 至Q(-3.15, 0.0, -3.0, 0.5); S(); });
  });
}

// 執鈴手：左手自上覆執柄——掌背向上，四指自脊而降各見其節，拇指內傍
export function 執鈴手(bi: 筆具, x: number, z: number, 比 = 1): void {
  const { M, Qk, C, S, 骨, 衣, 細, 逸 } = 運筆(bi);
  const X = (dx: number) => x + dx * 比, Z = (dz: number) => z + dz * 比;
  // 掌背之丘（骨線）：自腕向前隆起
  骨(() => {
    M(X(-0.5), Z(-1.35)); C(X(0.6), Z(-1.5), X(1.5), Z(-1.1), X(1.75), Z(-0.35)); S();
    M(X(-1.5), Z(-0.9)); Qk(X(-1.05), Z(-1.25), X(-0.5), Z(-1.35)); S();
  });
  衣(() => {
    // 指根之脊
    M(X(1.75), Z(-0.35)); Qk(X(1.55), Z(0.45), X(1.05), Z(0.9)); S();
    // 四指自脊而降各一筆，弧向柄心，梢出鋒（甲節候大圖）
    逸(() => {
      for (const t of [0, 1, 2, 3]) {
        const a0 = 1.35 - t * 0.62, 長 = 1 - Math.abs(t - 1.2) * 0.07;
        M(X(a0), Z(0.15 + t * 0.12));
        Qk(X(a0 - 0.14), Z(0.9 * 長 + t * 0.1), X(a0 - 0.42), Z(1.48 * 長 + t * 0.08)); S();
      }
      // 拇指：內傍柄，一弧而抵
      M(X(-1.5), Z(-0.9)); C(X(-1.75), Z(-0.2), X(-1.5), Z(0.6), X(-0.95), Z(1.0)); S();
    });
    // 腕節
    細(() => { M(X(-1.3), Z(-1.5)); Qk(X(-0.7), Z(-1.75), X(-0.1), Z(-1.7)); S(); });
  });
}
