// ─────────────────────────────────────────────────────────────────────────────
// 部件 · 嚴（頸瓔・胸瓔・臂釧・腕釧）——菩薩形之莊嚴具
//
// 出典：菩薩形瓔珞臂釧腕釧（造像標準五章四形之制；大日儀軌出典引
// 「宝冠、瓔珞、腕釧、臂釧などを着け…菩薩と同様の姿」）。逐件有結構：
// 瓔有弧有珠有滴，釧為雙環鑲珠。律：細線綴珠，位置隨尊之骨可參。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 運筆 } from '../bi.js';

// 頸瓔：一弧綴珠，貼頸
export function 頸瓔(bi: 筆具): void {
  const { M, Qk, S, 細 } = 運筆(bi);
  細(() => {
    M(-2.9, 25.3); Qk(0, 26.3, 2.9, 25.3); S();
    for (const [bx, bz] of [[-2.0, 25.7], [-0.85, 26.0], [0.85, 26.0], [2.0, 25.7]] as const) bi.dot(bx, bz, 0.22);
  });
}

// 胸瓔：垂弧至胸，綴珠，中懸一滴（滴z＝滴珠中心，可參以讓開胸前結印之地）
export function 胸瓔(bi: 筆具, 參: { 滴z?: number } = {}): void {
  const { 滴z = 30.7 } = 參;
  const { M, C, S, 細 } = 運筆(bi);
  細(() => {
    M(-3.7, 25.9); C(-4.4, 27.4, -3.0, 29.4, 0, 30.1);
    C(3.0, 29.4, 4.4, 27.4, 3.7, 25.9); S();
    for (const [bx, bz] of [[-3.6, 27.3], [-2.3, 28.9], [2.3, 28.9], [3.6, 27.3]] as const) bi.dot(bx, bz, 0.24);
    bi.dot(0, 滴z, 0.28);
  });
}

// 臂釧：雙環繞臂，環間一珠——一側之釧；d＝±1，x0 內緣、x1 外緣、z 上環之高
export function 臂釧(bi: 筆具, d: -1 | 1, x0 = 12.0, x1 = 14.3, z = 33.6): void {
  const { M, Qk, S, 細 } = 運筆(bi);
  const xm = (x0 + x1) / 2;
  細(() => {
    M(d * x0, z); Qk(d * xm, z + 0.5, d * x1, z - 0.1); S();
    M(d * (x0 + 0.05), z + 1.1); Qk(d * xm, z + 1.6, d * (x1 + 0.05), z + 1.0); S();
    bi.dot(d * xm, z + 1.05, 0.2);
  });
}

// 腕釧：雙環過腕，環間一珠——環各以 [x0,z0,cx,cz,x1,z1] 二次曲線與臂勢相順
export function 腕釧(
  bi: 筆具,
  環: ReadonlyArray<readonly [number, number, number, number, number, number]>,
  珠: readonly [number, number],
): void {
  const { M, Qk, S, 細 } = 運筆(bi);
  細(() => {
    for (const [x0, z0, cx, cz, x1, z1] of 環) { M(x0, z0); Qk(cx, cz, x1, z1); S(); }
    bi.dot(珠[0], 珠[1], 0.18);
  });
}
