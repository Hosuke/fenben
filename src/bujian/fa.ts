// ─────────────────────────────────────────────────────────────────────────────
// 部件 · 髮（寶髻・垂髮）——菩薩形之髮制
//
// 出典：菩薩形垂髮戴冠（造像標準五章四形之制）；鬢髮 S 波行於耳後、
// 拂肩而歇末有小卷，依鎌倉白描図樣之勢（Met 44845 一系對勘 2026-07-04；
// SAT図像抄 p.1052 成身會諸尊同此制）。律：細線尖出，縷有波。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 運筆 } from '../bi.js';

// 寶髻：冠上高髻（頂 z≈0.6–2.5）
export function 寶髻(bi: 筆具): void {
  const { M, Qk, C, S, 衣, 細 } = 運筆(bi);
  衣(() => {
    M(-1.55, 2.4); C(-1.75, 0.6, 1.75, 0.6, 1.55, 2.4); S();
    細(() => { M(-0.55, 2.0); Qk(-0.25, 1.15, 0.5, 1.05); S(); });
  });
}

// 垂髮：每側二縷，行於耳後，縷有 S 波，長短相參，末有小卷
export function 垂髮(bi: 筆具): void {
  const { M, Qk, C, S, 衣, 細 } = 運筆(bi);
  衣(() => {
    for (const d of [-1, 1] as const) {
      細(() => {
        M(d * 5.5, 9.5); C(d * 7.2, 10.4, d * 7.85, 11.6, d * 7.5, 13.2);
        C(d * 7.2, 14.5, d * 7.75, 15.8, d * 7.5, 17.2);
        C(d * 7.25, 18.7, d * 7.95, 20.2, d * 7.6, 21.6);
        Qk(d * 8.4, 23.2, d * 9.5, 24.3); S();
        bi.A(d * 9.65, 24.7, 0.38, Math.PI * 1.25, Math.PI * 0.55);
        M(d * 5.35, 9.8); C(d * 6.75, 11.0, d * 7.2, 12.4, d * 6.9, 13.9);
        C(d * 6.65, 15.3, d * 7.25, 16.7, d * 7.0, 18.2);
        Qk(d * 6.8, 19.7, d * 7.05, 20.9);
        Qk(d * 7.55, 22.3, d * 8.3, 23.4); S();
        bi.A(d * 8.45, 23.8, 0.32, Math.PI * 1.25, Math.PI * 0.55);
      });
    }
  });
}
