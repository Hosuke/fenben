// ─────────────────────────────────────────────────────────────────────────────
// 部件 · 髮（寶髻・垂髮）——菩薩形之髮制
//
// 出典：菩薩形垂髮戴冠（造像標準五章四形之制）；鬢髮 S 波行於耳後、
// 拂肩而歇末有小卷，依鎌倉白描図樣之勢（Met 44845 一系對勘 2026-07-04；
// SAT図像抄 p.1052 成身會諸尊同此制）。律：細線尖出，縷有波。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 運筆 } from '../bi.js';

// 寶髻：冠上高髻（頂 z≈0.6–2.5）。沉＝下就冠頂之距——三峰冠者髻根當就中峰
// （沉≈1.7，髻乃有根不懸浮）；五化佛冠者化佛自承髻底（沉 0，如薩埵）。
export function 寶髻(bi: 筆具, 沉 = 0): void {
  const { M, Qk, C, S, 衣, 細 } = 運筆(bi);
  衣(() => {
    M(-1.55, 2.4 + 沉); C(-1.75, 0.6 + 沉, 1.75, 0.6 + 沉, 1.55, 2.4 + 沉); S();
    細(() => { M(-0.55, 2.0 + 沉); Qk(-0.25, 1.15 + 沉, 0.5, 1.05 + 沉); S(); });
  });
}

// 天髻：供養天女之髻——高髻聳於頂，側各一鬟（成身會內四供養之制，
// SAT図像抄 巻1 p.1057 四天女可證：束髮高聳、雙鬟傍髻，不作菩薩寶髻）。
// 髮座＝真：無冠之尊必開——顱髮之郭封頂、髮際、束髮帶綴珠，髻乃有根
// （冠帶封顱者〔如四波羅蜜戴冠〕勿開，冠自為髻座）。
export function 天髻(bi: 筆具, 髮座 = false): void {
  const { M, Qk, C, S, 衣, 細 } = 運筆(bi);
  const o = 髮座 ? -0.95 : 0;   // 有座則髻根就座（束帶之上），無座就冠頂
  if (髮座) {
    衣(() => {
      // 顱髮之郭：自兩鬢際攏上，收於髻根——頂乃封
      M(-5.1, 9.45); C(-4.85, 7.9, -3.9, 6.85, -2.15, 6.6); S();
      M(5.1, 9.45); C(4.85, 7.9, 3.9, 6.85, 2.15, 6.6); S();
    });
    細(() => {
      // 髮際：中峰微尖
      M(-4.9, 9.6); C(-2.7, 8.8, -0.8, 8.7, 0, 8.85);
      C(0.8, 8.7, 2.7, 8.8, 4.9, 9.6); S();
      // 束髮之帶：髻根雙道，中綴一珠
      M(-2.15, 6.6); Qk(0, 6.0, 2.15, 6.6); S();
      M(-2.05, 7.25); Qk(0, 6.7, 2.05, 7.25); S();
      bi.dot(0, 6.33, 0.22);
      // 髮縷：自際攏向髻根（梳理之痕）
      bi.dim(0.7, () => {
        M(-3.6, 9.0); C(-3.0, 8.2, -2.5, 7.6, -1.8, 7.1); S();
        M(3.6, 9.0); C(3.0, 8.2, 2.5, 7.6, 1.8, 7.1); S();
        M(0, 8.95); Qk(-0.15, 8.1, -0.25, 7.35); S();
      });
    });
  }
  衣(() => {
    // 髻身：束而上聳，頂圓微前傾
    M(-2.0, 7.5 + o); C(-2.6, 5.2 + o, -1.7, 2.9 + o, 0.1, 2.7 + o);
    C(1.8, 2.9 + o, 2.6, 5.3 + o, 2.0, 7.5 + o); S();
    // 雙鬟：髻側各一環（髮繞成鬟，環有內外緣）
    M(-1.9, 6.1 + o); C(-4.1, 4.9 + o, -4.5, 7.0 + o, -2.2, 7.4 + o); S();
    M(1.9, 6.1 + o); C(4.1, 4.9 + o, 4.5, 7.0 + o, 2.2, 7.4 + o); S();
    細(() => {
      M(-2.7, 5.9 + o); Qk(-3.3, 6.0 + o, -3.2, 6.6 + o); S();
      M(2.7, 5.9 + o); Qk(3.3, 6.0 + o, 3.2, 6.6 + o); S();
      // 髮縷：髻身二道順勢
      M(-1.2, 7.3 + o); Qk(-0.6, 5.2 + o, -1.0, 3.5 + o); S();
      M(1.2, 7.3 + o); Qk(0.6, 5.2 + o, 1.0, 3.5 + o); S();
    });
  });
}

// 垂髮：每側二縷，**行於耳後**（耳廓之外讓一分——五地皆髮不掩耳），
// 縷有 S 波，長短相參，末有小卷；耳後之段斷筆讓耳（線描無遮之律）
export function 垂髮(bi: 筆具): void {
  const { M, Qk, C, S, 衣, 細 } = 運筆(bi);
  衣(() => {
    for (const d of [-1, 1] as const) {
      細(() => {
        // 外縷：自鬢起，繞耳外緣（±7.05）之外行，拂肩而歇
        M(d * 5.6, 9.4); C(d * 7.5, 10.2, d * 8.15, 11.6, d * 7.9, 13.4);
        C(d * 7.7, 14.9, d * 8.05, 16.2, d * 7.8, 17.6);
        C(d * 7.55, 19.0, d * 8.1, 20.4, d * 7.8, 21.7);
        Qk(d * 8.5, 23.3, d * 9.6, 24.4); S();
        bi.A(d * 9.75, 24.8, 0.38, Math.PI * 1.25, Math.PI * 0.55);
        // 內縷：於耳上入耳後（斷筆），出於垂埵之下再續
        M(d * 5.45, 9.7); C(d * 6.5, 10.6, d * 7.0, 11.4, d * 7.05, 12.2); S();
        M(d * 6.6, 19.3); Qk(d * 6.75, 20.3, d * 7.1, 21.1);
        Qk(d * 7.6, 22.4, d * 8.35, 23.5); S();
        bi.A(d * 8.5, 23.9, 0.32, Math.PI * 1.25, Math.PI * 0.55);
      });
    }
  });
}
