// 出典：現図成身會諸尊皆住月輪（白月輪，細環；SAT図像抄 巻1 p.1052 成身會
// 中央輪可證：尊各有輪，中尊重環）；頭光雙環細筆，繞首含冠。
// 丈光相之經據兩說並陳（造像標準四章），畫面折算：頭光半徑以含冠為度。
// 律：等寬細筆（光非衣紋，不施鋒），淡墨行之。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 運筆 } from '../bi.js';

export interface 月輪參 { 心z?: number; 半徑?: number; 環距?: number; 淡?: number }
export interface 頭光參 { 心z?: number; 半徑?: number; 環距?: number; 淡?: number }

// 月輪：白月輪滿含全躯，細雙環
export function 月輪(bi: 筆具, 參: 月輪參 = {}): void {
  const { 心z = 39, 半徑 = 41.2, 環距 = 1.2, 淡 = 0.4 } = 參;
  const { 細 } = 運筆(bi);
  bi.dim(淡, () => 細(() => { bi.A(0, 心z, 半徑); bi.A(0, 心z, 半徑 + 環距); }));
}

// 頭光：細雙環居首，含冠而有餘
export function 頭光(bi: 筆具, 參: 頭光參 = {}): void {
  const { 心z = 11, 半徑 = 14.6, 環距 = 1.2, 淡 = 0.5 } = 參;
  const { 細 } = 運筆(bi);
  bi.dim(淡, () => 細(() => { bi.A(0, 心z, 半徑); bi.A(0, 心z, 半徑 + 環距); }));
}
