// ─────────────────────────────────────────────────────────────────────────────
// 部件 · 持物（五鈷杵・五鈷鈴）——金剛部器仗之首
//
// 出典：金剛薩埵持物「右手は五鈷杵を胸前に持ち、左手は五鈷鈴を握って」
// （日本大百科全書）；五鈷之制：中股直而側股內彎相合，中有鬼目珠、蓮帶；
// 鈴者杵柄半截安於鐘身（三鈷半杵為柄）。形參 Met 44845 図樣・SAT図像抄。
// 四攝（鉤索鎖鈴）與十六大菩薩諸器仗候此檔續補。
// 律：衣線立器郭，細線綴節；杵可傾（θ 弧度），皆親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 運筆 } from '../bi.js';

// 五鈷杵：橫執之杵，心 (x,z)，軸傾 θ（弧度，+ 則畫面左端上揚），長約十指
export function 五鈷杵(bi: 筆具, x: number, z: number, θ = 0, 比 = 1): void {
  const { M, L, C, S, 衣, 細, 逸 } = 運筆(bi);
  const cos = Math.cos(θ), sin = Math.sin(θ);
  // 軸座標→格網：a 沿軸（畫面右為＋），b 垂軸
  const P = (a: number, b: number): [number, number] =>
    [x + (a * cos - b * sin) * 比, z + (a * sin + b * cos) * 比];
  const 至 = (f: (x: number, z: number) => void, a: number, b: number) => { const [px, pz] = P(a, b); f(px, pz); };
  const 彎 = (a0: number, b0: number, a1: number, b1: number, a2: number, b2: number, a3: number, b3: number) => {
    const [x0, z0] = P(a0, b0), [x1, z1] = P(a1, b1), [x2, z2] = P(a2, b2), [x3, z3] = P(a3, b3);
    M(x0, z0); C(x1, z1, x2, z2, x3, z3); S();
  };
  衣(() => {
    for (const d of [-1, 1] as const) {
      // 蓮帶：拳緣之外二短豎（握中則中珠沒於拳，帶乃杵之可見之節）
      至(M, d * 2.0, -0.5); 至(L, d * 2.0, 0.5); S();
      至(M, d * 2.4, -0.62); 至(L, d * 2.4, 0.62); S();
      // 中股：直而出鋒，尖出於側股之外
      逸(() => { 至(M, d * 2.6, 0); 至(L, d * 6.0, 0); S(); });
      // 側股：細筆開叉而內合，尖幾觸中股（爪相合之勢）
      細(() => {
        彎(d * 2.6, -0.55, d * 3.8, -1.05, d * 5.0, -0.8, d * 5.6, -0.15);
        彎(d * 2.6, 0.55, d * 3.8, 1.05, d * 5.0, 0.8, d * 5.6, 0.15);
      });
    }
  });
}

// 五鈷鈴：口向下之鈴，鐘身心 (x,z)；柄（三鈷半杵）在上——執之者拳，
// 柄沒於拳中則以 柄:false 略之（線描無遮，透見則亂）
export function 五鈷鈴(bi: 筆具, x: number, z: number, 比 = 1, 柄 = true): void {
  const { M, L, C, Qk, S, 衣, 細, 逸 } = 運筆(bi);
  const X = (dx: number) => x + dx * 比, Z = (dz: number) => z + dz * 比;
  衣(() => {
    if (柄) {
      // 柄：中股上出，側股相合，下有蓮帶
      逸(() => { M(X(0), Z(-2.0)); L(X(0), Z(-3.4)); S(); });
      M(X(-0.5), Z(-2.0)); C(X(-1.0), Z(-2.9), X(-0.55), Z(-3.15), X(-0.12), Z(-3.25)); S();
      M(X(0.5), Z(-2.0)); C(X(1.0), Z(-2.9), X(0.55), Z(-3.15), X(0.12), Z(-3.25)); S();
      細(() => { M(X(-0.7), Z(-1.9)); Qk(X(0), Z(-2.25), X(0.7), Z(-1.9)); S(); });
    }
    // 鐘身：肩圓而下張
    M(X(-0.75), Z(-1.8)); C(X(-1.7), Z(-1.3), X(-2.0), Z(0.3), X(-2.05), Z(1.5)); S();
    M(X(0.75), Z(-1.8)); C(X(1.7), Z(-1.3), X(2.0), Z(0.3), X(2.05), Z(1.5)); S();
    // 口緣：雙線
    M(X(-2.05), Z(1.5)); Qk(X(0), Z(1.95), X(2.05), Z(1.5)); S();
    細(() => { M(X(-1.9), Z(1.1)); Qk(X(0), Z(1.5), X(1.9), Z(1.1)); S(); });
    // 帶飾：肩下綴珠
    細(() => { for (const bx of [-1.1, 0, 1.1]) bi.dot(X(bx), Z(-0.55), 0.16 * 比); });
  });
}
