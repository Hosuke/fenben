// ─────────────────────────────────────────────────────────────────────────────
// 部件 · 足（仰足）——趺坐所現之足心（繁筆有所專施之二）
//
// 出典：足之量度「足底竪一搩（12指）、厚二指、四指俱三節」「生指根處寬六指、
// 脚心中寬五指、踵底寬四指」「足踵縱四指」（T1419 p.0944c，親驗本地經文）；
// 千輻輪相（相好：坐像足心向上時施，居掌心）；足趺高滿相。
// 坐像前視之仰足有縮短，取可見長約九指半而比例從經（踵寬：心寬：根寬＝4:5:6）。
// 律：衣線立足郭，細線走趾點輪；骨相圓滿——踵圓趾腴，弧不露稜。親筆。
// ─────────────────────────────────────────────────────────────────────────────
import type { 筆具 } from '../bi.js';
import { 運筆 } from '../bi.js';

// 仰足：踵心 (x,z)，趾向畫面右；長＝踵端至趾端（默認 9.5 指）
export function 仰足(bi: 筆具, x: number, z: number, 長 = 9.5): void {
  const { M, L, Qk, C, S, 衣, 細 } = 運筆(bi);
  const k = 長 / 9.5;
  const X = (dx: number) => x + dx * k, Z = (dz: number) => z + dz * k;
  衣(() => {
    // 踵：圓滿之踵（縱四指之比），一弧成瘤
    bi.A(X(0), Z(0), 1.5 * k, Math.PI * 0.28, Math.PI * 1.82);
    // 足底上緣（近趾側微昇——足趺高滿）：踵→弓→蹠丘→趾根
    M(X(-0.15), Z(-1.42)); C(X(2.2), Z(-1.75), X(4.4), Z(-1.7), X(6.2), Z(-1.35));
    Qk(X(7.1), Z(-1.15), X(7.6), Z(-0.85)); S();
    // 足底下緣：踵→心之寬→趾根之收
    M(X(0.2), Z(1.45)); C(X(2.4), Z(1.6), X(4.8), Z(1.45), X(6.6), Z(1.05));
    Qk(X(7.3), Z(0.85), X(7.7), Z(0.6)); S();
    // 蹠丘之界：趾根一弧（掌與趾之分）
    細(() => { M(X(7.0), Z(-1.0)); Qk(X(7.5), Z(-0.1), X(7.15), Z(0.75)); S(); });
    細(() => {
      // 五趾：拇趾腴而圓（惟二節），餘四趾以次而斂、各見一節一甲
      bi.E(X(8.35), Z(-0.85), 0.72 * k, 0.6 * k);
      bi.dot(X(8.62), Z(-0.88), 0.14 * k);                       // 拇甲
      for (const [i, dz] of [-0.12, 0.5, 1.02, 1.44].entries()) {
        const 基r = 0.4 - i * 0.035;                              // 未縮之半徑（Z 內自乘 k）
        bi.A(X(8.5 - i * 0.28), Z(dz), 基r * k, Math.PI * 1.1, Math.PI * 0.9);
        bi.dot(X(8.66 - i * 0.28), Z(dz), 0.09 * k);             // 甲
        M(X(8.14 - i * 0.28), Z(dz - 基r * 0.5));                // 節線一道
        L(X(8.02 - i * 0.28), Z(dz + 基r * 0.4)); S();
      }
      // 千輻輪：足心之輪（轂・輻八・輞）
      bi.A(X(3.9), Z(-0.1), 0.95 * k);
      bi.dot(X(3.9), Z(-0.1), 0.16 * k);
      for (let i = 0; i < 8; i++) {
        const a = (Math.PI * 2 * i) / 8 + Math.PI / 8;
        M(X(3.9 + Math.cos(a) * 0.3), Z(-0.1 + Math.sin(a) * 0.3));
        L(X(3.9 + Math.cos(a) * 0.82), Z(-0.1 + Math.sin(a) * 0.82)); S();
      }
      // 踵下一折（足與股之際）
      bi.dim(0.7, () => { M(X(-0.9), Z(1.35)); Qk(X(-1.3), Z(0.5), X(-1.15), Z(-0.4)); S(); });
    });
  });
}
