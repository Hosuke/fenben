import type { 筆具 } from '../bi.js';
import { 運筆 } from '../bi.js';
import { 面部 } from '../liangdu.js';

export type 面輪型 = '滿月' | '雞子';

// 開臉：面輪＋眉眼鼻口＋白毫——不含瞳（點睛另筆殿後）
export function 菩薩開臉(bi: 筆具, 型: 面輪型 = '雞子'): void {
  const { M, L, Qk, C, S, 骨, 細, 以, 陰, 陽 } = 運筆(bi);
  const W_OUT = bi.W_OUT;
  const 眼 = 面部.眼, 眉 = 面部.眉, 鼻 = 面部.鼻, 口 = 面部.口;

  // ── 面輪：橫十二指（±6），雞子上豐下斂、滿月頰頦俱圓；
  //    陰陽線寬：光自左上——左緣陽輕、右緣陰重，頰乃有立體 ──
  骨(() => {
    if (型 === '雞子') {
      陽(() => {
        M(-5.3, 9.2); C(-5.75, 11.6, -5.8, 13.8, -5.15, 16.2);
        C(-4.55, 18.4, -3.2, 19.9, 0, 20.55); S();
      });
      陰(() => {
        M(0, 20.55); C(3.2, 19.9, 4.55, 18.4, 5.15, 16.2);
        C(5.8, 13.8, 5.75, 11.6, 5.3, 9.2); S();
      });
    } else {
      陽(() => {
        M(-5.45, 9.4); C(-5.9, 11.8, -5.85, 14.4, -5.1, 16.8);
        C(-4.4, 18.8, -3.0, 20.0, 0, 20.5); S();
      });
      陰(() => {
        M(0, 20.5); C(3.0, 20.0, 4.4, 18.8, 5.1, 16.8);
        C(5.85, 14.4, 5.9, 11.8, 5.45, 9.4); S();
      });
    }
  });
  // 二重頦：頤下一弧（頦廣四指，弧稱其廣）
  細(() => bi.dim(0.7, () => { M(-1.6, 19.5); Qk(0, 19.95, 1.6, 19.5); S(); }));
  // 頰肉：顴下淡弧各一（稍見肉——面頰之立體不在線多，在此一轉）
  細(() => bi.dim(0.42, () => {
    M(-3.75, 15.3); Qk(-4.15, 16.1, -3.7, 16.9); S();
    M(3.75, 15.3); Qk(4.15, 16.1, 3.7, 16.9); S();
  }));

  細(() => {
    // ── 眉如初月：前梢近中（±0.375），中高（弧揚），長四指，中寬二麥漸細 ──
    for (const d of [-1, 1] as const) {
      M(d * 眉.前梢x, 眉.起z); Qk(d * 2.35, 眉.起z - 0.92, d * (眉.前梢x + 眉.長), 眉.起z + 0.2); S();
      bi.dim(0.85, () => {
        M(d * (眉.前梢x + 0.15), 眉.起z + 眉.中寬 * 0.9);
        Qk(d * 2.35, 眉.起z - 0.92 + 眉.中寬, d * (眉.前梢x + 眉.長 - 0.12), 眉.起z + 0.22); S();
      });
    }
    // ── 眼：長弓入鬢——弓背深而末梢微挑（經數不動，秀氣自弧出）──
    以(W_OUT * 0.19, () => {
      for (const d of [-1, 1] as const) {
        M(d * 眼.大眼角x, 眼.中線z + 0.05);
        Qk(d * 2.85, 眼.中線z - 0.46, d * 眼.小眼角x, 眼.中線z - 0.04);
        S();
        // 眼尾一挑（尖正對耳孔，挑不離經）
        M(d * 眼.小眼角x, 眼.中線z - 0.04);
        Qk(d * (眼.小眼角x + 0.18), 眼.中線z - 0.12, d * (眼.小眼角x + 0.3), 眼.中線z - 0.26); S();
      }
    });
    for (const d of [-1, 1] as const) {
      M(d * (眼.大眼角x + 0.06), 眼.中線z + 0.16);
      Qk(d * 2.85, 眼.中線z + 0.12, d * (眼.小眼角x - 0.06), 眼.中線z + 0.06); S();
      // 黑珠：瞼縫中微露一弧（瞳候點睛）
      bi.A(d * 2.85, 眼.中線z - 0.08, 0.52, Math.PI * 0.12, Math.PI * 0.88);
      // 眼胞：上瞼上一褶，闊三指
      bi.dim(0.75, () => { M(d * 1.35, 眼.中線z - 0.78); Qk(d * 2.85, 眼.中線z - 1.12, d * 4.4, 眼.中線z - 0.68); S(); });
    }
    // ── 鼻：山根寬一指自目間起，樑至準頭，翼回而孔藏 ──
    M(-0.5, 13.25); C(-0.56, 14.2, -0.62, 15.0, -0.78, 15.55);
    Qk(-鼻.總寬 / 2, 15.98, -0.82, 16.28); S();
    M(0.5, 13.25); C(0.56, 14.2, 0.62, 15.0, 0.78, 15.55);
    Qk(鼻.總寬 / 2, 15.98, 0.82, 16.28); S();
    // 鼻底：柱寬半指，兩傍微弧（孔圓且藏，不點孔）
    M(-鼻.柱寬 / 2, 16.3); Qk(0, 16.42, 鼻.柱寬 / 2, 16.3); S();
    // ── 人中：闊三分指之一，鼻柱根至閉口 ──
    bi.dim(0.8, () => {
      M(-口.人中闊 / 2, 16.5); Qk(-口.人中闊 / 2 - 0.05, 17.0, -口.人中闊 / 2 - 0.12, 17.32); S();
      M(口.人中闊 / 2, 16.5); Qk(口.人中闊 / 2 + 0.05, 17.0, 口.人中闊 / 2 + 0.12, 17.32); S();
    });
    // ── 口：長四指，角上翹，上唇頻婆中顯，下唇六足 ──
    M(-1.7, 17.18); C(-1.0, 16.95, -0.4, 16.86, -0.16, 16.96);
    Qk(0, 17.02, 0.16, 16.96);
    C(0.4, 16.86, 1.0, 16.95, 1.7, 17.18); S();                   // 上唇緣（頻婆形，貼縫）
    以(W_OUT * 0.17, () => {                                       // 合線獨重：長四指、梢微翹
      M(-口.長 / 2, 口.縫z); Qk(-1.0, 口.縫z + 0.16, 0, 口.縫z + 0.13);
      Qk(1.0, 口.縫z + 0.16, 口.長 / 2, 口.縫z); S();
    });
    M(-0.75, 18.32); Qk(0, 18.55, 0.75, 18.32); S();               // 下唇緣（長六足）
    bi.dim(0.55, () => { M(-1.15, 18.66); Qk(0, 18.95, 1.15, 18.66); S(); }); // 唇下承影
  });
  // ── 白毫：底盤廣一指，右旋 ──
  bi.dot(0, 面部.白毫.z, 0.3);
  細(() => bi.A(0, 面部.白毫.z, 面部.白毫.徑 / 2));
}

// 點睛：瞳者眸子也，黑珠五分取中——殿後之末筆（開眼即開光）
export function 點睛(bi: 筆具): void {
  const 眼 = 面部.眼;
  bi.dot(-2.9, 眼.中線z - 0.03, 眼.瞳徑 / 2 + 0.04);
  bi.dot(2.9, 眼.中線z - 0.03, 眼.瞳徑 / 2 + 0.04);
}

// 耳：洞門正對小眼角（±6,14），尖齊眉中，垂底十九，廣二指，垂埵穿璫
export function 垂埵耳(bi: 筆具): void {
  const { M, Qk, C, S, 衣, 細 } = 運筆(bi);
  const 耳 = 面部.耳;
  衣(() => {
    for (const d of [-1, 1] as const) {
      // 外輪：自鬢際起，過尖，沿外緣而下，收於垂
      M(d * 5.3, 13.1); C(d * 5.5, 12.35, d * 6.4, 12.15, d * 6.75, 耳.尖z + 0.5);
      C(d * (5.9 + 耳.廣 / 2 + 0.15), 14.3, d * 6.9, 15.5, d * 6.5, 16.5);
      C(d * 6.2, 17.3, d * 6.3, 18.2, d * 5.95, 耳.垂底z); S();
      // 垂埵內緣回收
      Qk(d * 5.5, 耳.垂底z + 0.32, d * 5.35, 耳.垂底z - 0.55);
      S();
      細(() => {
        M(d * 5.7, 13.15); Qk(d * 6.3, 13.55, d * 6.1, 14.7); S();     // 耳輪內折
        bi.A(d * 5.55, 耳.洞門z + 0.4, 0.32, 0, 7);                     // 耳珠（洞門傍）
        bi.A(d * 5.75, 耳.垂底z + 0.55, 0.58); bi.dot(d * 5.75, 耳.垂底z + 1.2, 0.2); // 耳璫
      });
    }
  });
}

// 頸三道：依經距（頷根下一足半、再一指一足、再二指）
export function 頸三道(bi: 筆具): void {
  const { M, Qk, S, 細 } = 運筆(bi);
  const [上, 中, 下] = 面部.頸紋;
  細(() => {
    M(-2.1, 上); Qk(0, 上 + 0.6, 2.1, 上); S();
    M(-2.5, 中); Qk(0, 中 + 0.68, 2.5, 中); S();
    bi.dim(0.8, () => { M(-2.85, 下); Qk(0, 下 + 0.75, 2.85, 下); S(); });
  });
}
