export type 面輪型 = '滿月' | '雞子';

// 開臉：面輪＋五官（簡筆）——不含瞳（點睛另筆殿後）
export function 菩薩開臉(bi: 筆具, 型: 面輪型 = '雞子'): void {
  const { M, L, Qk, C, S, 骨, 細, 以 } = 運筆(bi);
  const W_OUT = bi.W_OUT;

  // ── 面輪：雞子上豐下斂；滿月頰頦俱圓 ──
  骨(() => {
    if (型 === '雞子') {
      M(-5.05, 9.4); C(-5.35, 11.4, -5.45, 13.6, -5.0, 15.9);
      C(-4.55, 18.2, -3.3, 19.75, 0, 20.5);
      C(3.3, 19.75, 4.55, 18.2, 5.0, 15.9);
      C(5.45, 13.6, 5.35, 11.4, 5.05, 9.4); S();
    } else {
      M(-5.35, 9.5); C(-5.75, 11.8, -5.7, 14.4, -5.0, 16.7);
      C(-4.35, 18.7, -3.0, 19.95, 0, 20.45);
      C(3.0, 19.95, 4.35, 18.7, 5.0, 16.7);
      C(5.7, 14.4, 5.75, 11.8, 5.35, 9.5); S();
    }
  });
  // 二重頦：頤下一弧
  細(() => bi.dim(0.7, () => { M(-1.5, 19.55); Qk(0, 20.0, 1.5, 19.55); S(); }));

  細(() => {
    // 五官一族之弧：眉瞼同軸呼應，梢齊平微揚（垂目非垂神）；
    // 鼻翼承目間，口角承睛下——氣韻在弧與弧之應答，不在線多
    // 眉：初月一筆，梢與瞼梢同其揚
    M(-0.85, 12.3); Qk(-2.5, 11.55, -4.5, 12.2); S();
    M(0.85, 12.3); Qk(2.5, 11.55, 4.5, 12.2); S();
    // 上瞼長弓一筆（獨重）：弓背向上，兩端齊平而外梢微揚一麥
    以(W_OUT * 0.19, () => {
      M(-0.95, 13.98); Qk(-2.4, 13.38, -3.95, 13.92); S();
      M(0.95, 13.98); Qk(2.4, 13.38, 3.95, 13.92); S();
    });
    // 垂目之睛：弓下微露一弧（瞳候點睛）
    bi.A(-2.4, 13.98, 0.4, Math.PI * 0.12, Math.PI * 0.88);
    bi.A(2.4, 13.98, 0.4, Math.PI * 0.12, Math.PI * 0.88);
    // 下瞼淡承，梢亦微揚（與上瞼同族）
    bi.dim(0.8, () => { M(-1.3, 14.42); Qk(-2.4, 14.6, -3.6, 14.35); S(); M(1.3, 14.42); Qk(2.4, 14.6, 3.6, 14.35); S(); });
    // 鼻：樑自目間而下（承眉頭），翼止於大眼角之下（承目），孔藏不點
    M(-0.55, 13.6); C(-0.62, 14.3, -0.7, 14.95, -0.88, 15.5); Qk(-1.38, 15.95, -1.02, 16.3); S();
    M(0.55, 13.6); C(0.62, 14.3, 0.7, 14.95, 0.88, 15.5); Qk(1.38, 15.95, 1.02, 16.3); S();
    // 人中
    M(0, 16.75); L(0, 17.2); S();
    // 唇靜三筆：上唇弓一筆（雙峰相連）、合線獨重而平、下唇淡收——
    // 口角（±1.2）正承睛弧之下
    M(-1.15, 17.55); C(-0.45, 17.28, 0.45, 17.28, 1.15, 17.55); S();
    以(W_OUT * 0.19, () => { M(-1.2, 17.9); Qk(0, 18.12, 1.2, 17.9); S(); });
    bi.dim(0.8, () => { M(-0.85, 18.52); Qk(0, 18.86, 0.85, 18.52); S(); });
  });
  // 白毫：頂下十二指，右旋
  bi.dot(0, 面部.白毫.z, 0.28);
  細(() => bi.A(0, 面部.白毫.z, 0.5));
}

