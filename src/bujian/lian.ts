// 耳：輪郭有折，垂埵穿璫（洞門依經對小眼角之側，筆從簡）
export function 垂埵耳(bi: 筆具): void {
  const { M, Qk, C, S, 衣, 細 } = 運筆(bi);
  衣(() => {
    for (const d of [-1, 1] as const) {
      M(d * 5.0, 12.2); C(d * 6.6, 12.3, d * 6.75, 13.6, d * 6.5, 14.7);
      C(d * 6.3, 15.7, d * 5.95, 16.5, d * 5.8, 17.4);
      Qk(d * 5.7, 18.6, d * 5.85, 19.5); S();
      細(() => {
        M(d * 5.45, 13.0); Qk(d * 6.1, 13.4, d * 5.95, 14.55); S();  // 耳輪內折
        bi.A(d * 5.15, 15.0, 0.32, 0, 7);                            // 耳珠
        bi.A(d * 5.6, 19.9, 0.58); bi.dot(d * 5.6, 20.55, 0.2);      // 耳璫
      });
    }
  });
}

// 頸三道：依經距（頷根下一足半、再一指一足、再二指——相好之目）
export function 頸三道(bi: 筆具): void {
  const { M, Qk, S, 細 } = 運筆(bi);
  const [上, 中, 下] = 面部.頸紋;
  細(() => {
    M(-2.1, 上); Qk(0, 上 + 0.6, 2.1, 上); S();
    M(-2.5, 中); Qk(0, 中 + 0.68, 2.5, 中); S();
    bi.dim(0.8, () => { M(-2.85, 下); Qk(0, 下 + 0.75, 2.85, 下); S(); });
  });
}
