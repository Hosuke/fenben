// ── 忿怒開臉（簡忿之式，2026-07-09 主人裁甲）──────────────────────────────────
// 守簡筆之魂而破垂目之靜：怒眉倒豎二筆・圓睜之睛・三目一豎・獠牙二點・
// 眉間怒紋一道——面上仍寧少筆，忿之勢投於焰髮焰光甲械（彼屬髮部件與尊筆）。
// 面輪方頤（威怒之骨），三庭錨位不移（白毫12 鼻16 頦20——量度是律）。
export function 忿怒開臉(bi: 筆具): void {
  const { M, L, Qk, C, S, 骨, 細, 以 } = 運筆(bi);
  const W_OUT = bi.W_OUT;
  // 面輪：方頤閣腮，額廣頦短促
  骨(() => {
    M(-5.4, 9.5); C(-5.85, 11.8, -5.75, 14.6, -5.1, 16.6);
    C(-4.6, 18.4, -3.1, 19.6, 0, 20.1);
    C(3.1, 19.6, 4.6, 18.4, 5.1, 16.6);
    C(5.75, 14.6, 5.85, 11.8, 5.4, 9.5); S();
  });
  細(() => {
    // 怒眉倒豎：內端沉外梢揚（與寂靜眉反其勢），眉間怒紋一豎
    以(W_OUT * 0.19, () => {
      M(-0.75, 13.3); Qk(-2.3, 12.3, -4.15, 11.4); S();
      M(0.75, 13.3); Qk(2.3, 12.3, 4.15, 11.4); S();
    });
    M(0, 12.5); L(0, 13.35); S();
    // 圓睜之睛：正圓怒目（瞳候忿怒點睛），下瞼一托
    bi.A(-2.4, 14.3, 0.78);
    bi.A(2.4, 14.3, 0.78);
    bi.dim(0.8, () => { M(-3.3, 15.1); Qk(-2.4, 15.4, -1.5, 15.1); S(); M(3.3, 15.1); Qk(2.4, 15.4, 1.5, 15.1); S(); });
    // 三目：眉間一豎目（杏核直立，瞳候點睛）
    M(0, 11.3); Qk(0.4, 12.15, 0, 13.0); S();
    M(0, 11.3); Qk(-0.4, 12.15, 0, 13.0); S();
    // 鼻：樑短翼張（怒息之鼻）
    M(-0.6, 13.9); C(-0.7, 14.6, -0.85, 15.2, -1.05, 15.6); Qk(-1.6, 16.0, -1.15, 16.35); S();
    M(0.6, 13.9); C(0.7, 14.6, 0.85, 15.2, 1.05, 15.6); Qk(1.6, 16.0, 1.15, 16.35); S();
    // 口：咧而見牙——口線一橫，上獠二牙覆下唇
    以(W_OUT * 0.19, () => { M(-1.75, 17.7); Qk(0, 18.25, 1.75, 17.7); S(); });
    M(-1.15, 17.78); L(-0.95, 18.7); L(-0.72, 17.86); S();
    M(1.15, 17.78); L(0.95, 18.7); L(0.72, 17.86); S();
    bi.dim(0.8, () => { M(-1.0, 19.15); Qk(0, 19.5, 1.0, 19.15); S(); });
  });
  // 白毫位讓於三目（忿怒像三目居印堂，白毫不點）
}

