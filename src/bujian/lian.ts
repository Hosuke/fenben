// 眼：大眼角±1、小眼角±5（長四指入鬢）、縫僅一足「為入定之相，形似長弓」；
// 眉：前梢±0.375 起白毫下三麥半、長四指「眉如初月牙」、中寬二麥兩梢漸細；
// 鼻：寬二指一足、柱孔各半指；口：長四指、角上翹、上唇頻婆形、人中闊⅓；
// 耳：洞門±6 正對小眼角、尖齊眉中、垂底19；頸紋三道依經距。
// 面輪四型（T1419 附圖，藏地卷考）：滿月（如來）・雞子（菩薩）——先立二型，
// 芝麻（佛母）・四方（明王）候補。眼型四分候擴（今皆弓眼）。
// 點睛析出為獨立末筆（點睛殿後——開眼即開光，跨傳統共法，畫風考§二5）。親筆。
