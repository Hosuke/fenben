// 藏地-噶瑪嘎孜・框飾層——景／飾（空尊位）
// 十一面塔域、十一面、八主臂與千手光輪悉歸主坊；手亦身也，本檔不畫。

import { 造線筆 } from './lib/白描幀具.js';

export const 尊位 = Object.freeze({
  W: 1800,
  H: 2500,
  頂y: 560,
  指: 11,
  中x: 900,
  足底: 1880,
});

const 墨 = '#d8b36a';
const 青綠 = '#789f98';
const 冷青 = '#718e9d';

function 斷(真, 訊) {
  console.assert(真, 訊);
  if (!真) throw new Error(訊);
}

function 驗助(助) {
  斷(助 && 助.尊位, '藏地-噶瑪嘎孜框飾層：缺 助.尊位');
  斷(助.W === 尊位.W && 助.H === 尊位.H, `畫幅失守：${助.W}×${助.H}`);
  for (const 名 of ['W', 'H', '頂y', '指', '中x', '足底'])
    斷(助.尊位[名] === 尊位[名], `尊位失守：${名}`);
  斷(尊位.頂y + 120 * 尊位.指 === 尊位.足底, '足底非頂y+120指');
  return 助.尊位;
}

// 光帶可繞尊位，卻不得在主坊千手域內預留人形洞或落一線。
function 限靜域外(ctx, 落筆) {
  ctx.save();
  try {
    ctx.beginPath();
    ctx.rect(0, 0, 尊位.W, 尊位.H);
    ctx.rect(430, 330, 940, 1570);
    ctx.clip('evenodd');
    落筆();
  } finally {
    ctx.restore();
  }
}

function 山雲(ctx) {
  const 山筆 = 造線筆(ctx, { 墨: 青綠, 骨: 1.7, 衣: 1.05, 鬚: .55 });
  const 雲筆 = 造線筆(ctx, { 墨: 冷青, 骨: 1.4, 衣: .85, 鬚: .48 });
  // 天際僅三道遠岫與兩縷橫雲；皆在 y120–300，透明不逾 .4。
  山筆.線([[118,286],[214,254],[300,268],[390,214],[474,249],[566,190],[650,238],[734,218]], 1.08, false, .27);
  山筆.線([[1058,244],[1148,202],[1236,230],[1322,174],[1416,224],[1510,196],[1682,270]], .94, false, .23);
  山筆.線([[280,292],[420,273],[548,289],[676,254],[806,282],[954,250],[1092,280],[1228,263],[1386,291]], .68, false, .17);
  雲筆.貝([82,158],[214,130],[304,178],[438,151], .82, .32);
  雲筆.貝([1326,137],[1448,125],[1554,166],[1720,139], .78, .29);
  for (const [x,y,r,a] of [[188,153,18,.22],[382,153,15,.2],[1408,139,17,.2],[1642,142,19,.21]])
    雲筆.圓(x, y, r, .55, a, .12, Math.PI * 1.48);
}

function 光帶(ctx) {
  const 外 = 造線筆(ctx, { 墨, 骨: 1.7, 衣: .95, 鬚: .52 });
  const 內 = 造線筆(ctx, { 墨: 冷青, 骨: 1.3, 衣: .72, 鬚: .45 });
  限靜域外(ctx, () => {
    // 外緣約 x300–1500・y210–2010；雙細線只定一帶，不作千手光輪。
    外.橢(900, 1110, 600, 900, 1.12, 0, .38);
    內.橢(900, 1110, 578, 874, .72, 0, .28);
    const 卷 = [
      [[376,690],[326,664],[326,608],[382,600],[420,596],[432,626],[406,647]],
      [[344,1134],[292,1108],[302,1054],[356,1050],[398,1048],[408,1080],[382,1100]],
      [[392,1588],[338,1568],[342,1514],[398,1506],[432,1504],[444,1538],[418,1558]],
      [[1424,724],[1474,698],[1470,644],[1416,636],[1378,634],[1368,666],[1394,686]],
      [[1456,1170],[1504,1142],[1494,1090],[1440,1084],[1402,1082],[1390,1114],[1418,1136]],
      [[1410,1602],[1460,1576],[1454,1524],[1400,1516],[1366,1516],[1354,1548],[1382,1568]],
    ];
    for (let i = 0; i < 卷.length; i++) {
      const p = 卷[i];
      ctx.save();
      ctx.beginPath(); ctx.moveTo(...p[0]);
      ctx.bezierCurveTo(...p[1], ...p[2], ...p[3]);
      ctx.bezierCurveTo(...p[4], ...p[5], ...p[6]);
      ctx.strokeStyle = 冷青; ctx.lineWidth = .62;
      ctx.globalAlpha = .22 + (i % 2) * .035; ctx.lineCap = 'round'; ctx.stroke();
      ctx.restore();
      const [x,y] = 卷[i][3];
      內.圓(x, y, 10 + i % 3 * 2, .5, .18, .2, Math.PI * 1.55);
    }
  });
}

function 淨域(ctx, 落) {
  ctx.save();
  try {
    ctx.globalCompositeOperation = 'destination-out';
    ctx.fillStyle = '#000';
    ctx.beginPath();
    落(ctx);
    ctx.fill();
  } finally {
    ctx.restore();
  }
}

function 蓮臺(ctx, 位) {
  const 金 = 造線筆(ctx, { 墨, 骨: 3.8, 衣: 1.8, 鬚: .75 });
  const 青 = 造線筆(ctx, { 墨: 青綠, 骨: 2.2, 衣: 1.05, 鬚: .52 });
  const S = 位.足底;
  const 蓮廓 = p => {
    p.moveTo(520, S); p.quadraticCurveTo(900, S - 7, 1280, S);
    p.quadraticCurveTo(1272, S + 110, 1218, S + 180);
    p.quadraticCurveTo(900, S + 196, 582, S + 180);
    p.quadraticCurveTo(528, S + 110, 520, S); p.closePath();
  };
  const 水廓 = p => p.rect(330, 2060, 1140, 100);
  // 鐵令：蓮臺、水帶各淨其域，乃各自描之。
  淨域(ctx, 蓮廓);
  金.貝([520,S],[680,S-7],[1120,S-7],[1280,S], 3.8, .92);
  金.貝([540,S+31],[700,S+58],[1100,S+58],[1260,S+31], 1.45, .61);
  const 仰 = [[570,74,94],[686,82,104],[814,88,111],[946,88,110],[1078,82,103],[1190,72,92]];
  for (const [cx,rx,h] of 仰) {
    金.貝([cx-rx,S+91],[cx-rx*.58,S+30],[cx-rx*.2,S+20],[cx,S+2], 1.35, .7);
    金.貝([cx,S+2],[cx+rx*.2,S+20],[cx+rx*.58,S+30],[cx+rx,S+91], 1.35, .7);
  }
  金.貝([540,S+92],[690,S+114],[1110,S+114],[1260,S+92], 2.1, .75);
  const 覆 = [[586,69,61],[702,78,70],[826,82,74],[958,82,74],[1082,77,68],[1198,66,58]];
  for (const [cx,rx,h] of 覆) {
    青.貝([cx-rx,S+101],[cx-rx*.55,S+139],[cx-rx*.18,S+166],[cx,S+180], 1.05, .58);
    青.貝([cx,S+180],[cx+rx*.18,S+166],[cx+rx*.55,S+139],[cx+rx,S+101], 1.05, .58);
  }
  金.貝([582,S+180],[730,S+196],[1070,S+196],[1218,S+180], 2.3, .78);

  淨域(ctx, 水廓);
  const 波 = [2062,2082,2104,2128,2152];
  for (let row = 0; row < 波.length; row++) {
    const y = 波[row], start = 360 + row * 17, end = 1440 - row * 13;
    for (let x = start, i = 0; x < end; x += 126, i++) {
      const e = Math.min(x + 92 + (i % 3) * 9, end);
      青.貝([x,y],[x+22,y-8-(row%2)*2],[e-24,y+8+(i%2)*2],[e,y], .78, .31 + row * .025);
    }
  }
  金.貝([410,2160],[650,2148],[1150,2148],[1390,2160], 1.05, .34);
  斷(S === 1880, `蓮臺足底錨失守：${S}`);
}

export function 景(ctx, 助) {
  驗助(助);
  ctx.save();
  try { 山雲(ctx); 光帶(ctx); } finally { ctx.restore(); }
}

export function 飾(ctx, 助) {
  const 位 = 驗助(助);
  ctx.save();
  try { 蓮臺(ctx, 位); } finally { ctx.restore(); }
}
