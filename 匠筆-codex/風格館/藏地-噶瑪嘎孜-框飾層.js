function 淡山剪影(ctx) {
  const 山筆 = 造線筆(ctx, { 墨: 青綠, 骨: 1.55, 衣: .92, 鬚: .48 });
  const 六巒 = [
    { y:286, a:.12, 鹽:59, 高:58, 節:144, 遠:true },
    { y:354, a:.22, 鹽:3, 高:82, 節:166 },
    { y:920, a:.24, 鹽:11, 高:126, 節:194 },
    { y:1210, a:.20, 鹽:17, 高:104, 節:154 },
    { y:1540, a:.23, 鹽:29, 高:142, 節:212 },
    { y:1880, a:.18, 鹽:43, 高:116, 節:178 },
  ];
  限靜域外(ctx, () => {
    for (let 層 = 0; 層 < 六巒.length; 層++) {
      const 巒 = 六巒[層];
      const 點 = [[138, 巒.y]];
      for (let x = 138, 峰 = 0; x < 1662; 峰++) {
        x = Math.min(1662, x + 巒.節 + 籽變(峰, 巒.鹽, 68));
        點.push([x, 巒.y - 巒.高 + 籽變(峰, 巒.鹽 + 9, 68)]);
        if (x < 1662) {
          x = Math.min(1662, x + 74 + 籽變(峰, 巒.鹽 + 21, 74));
          點.push([x, 巒.y - 18 - 籽變(峰, 巒.鹽 + 33, 34)]);
        }
      }
      ctx.beginPath(); ctx.moveTo(...點[0]);
      for (let i = 1; i < 點.length; i++) {
        const 前 = 點[i - 1], 今 = 點[i];
        ctx.quadraticCurveTo((前[0] + 今[0]) / 2, Math.min(前[1], 今[1]) - 9, ...今);
      }
      ctx.strokeStyle = 青綠; ctx.lineWidth = 1.05 + 層 * .08;
      ctx.globalAlpha = 巒.a; ctx.lineCap = 'round'; ctx.stroke();
      // 遠巒只留一皴，近巒較舊稿各添一二，仍與波幕分出前後。
      const 近序 = 層 - 1;
      const 皴數 = 巒.遠 ? 1 : 3 + (近序 & 1) + (近序 % 3 === 0 ? 1 : 0);
      for (let 皴 = 0; 皴 < 皴數; 皴++) {
        const yy = 巒.y - 28 - 皴 * 22;
        const x0 = 160 + 籽變(皴, 巒.鹽, 120);
        山筆.貝([x0,yy],[430,yy-24-層*3],[1320,yy+23+皴*4],[1640,yy-7],
          .58 + 皴 * .08, 巒.a);
      }
    }
  });
  ctx.globalAlpha = 1;
}

