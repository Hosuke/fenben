// 杳眇・風劫不動 · 框飾層
// 一勢而已：風自左下行於右上；近身前壓、兩肩分流，過身則留漸闊之靜域。
// 此層唯線與點，不鋪色場。籽只定線位與疏密，同幅同筆，不借亂數。

export const 尊位 = { W: 1800, H: 2500, 軸x: 900, 頂y: 840, 指: 8, 足底: 1800,
  身橢: { 心: [900, 1300], 徑: [150, 480] },
  流: { 自: [120, 2380], 至: [1700, 120] } };

const { W, H } = 尊位;
const [心甲, 心乙] = 尊位.身橢.心;
const [橢甲徑, 橢乙徑] = 尊位.身橢.徑;
const [流自甲, 流自乙] = 尊位.流.自;
const [流至甲, 流至乙] = 尊位.流.至;
const 流長 = Math.hypot(流至甲 - 流自甲, 流至乙 - 流自乙);
const 順甲 = (流至甲 - 流自甲) / 流長;
const 順乙 = (流至乙 - 流自乙) / 流長;
const 橫甲 = -順乙;
const 橫乙 = 順甲;

const 金 = '#d8b36a';
const 風灰 = [184, 198, 218];
const 核隙 = 24;
// 抽樣折線之弦會略切曲線，故造線時守 38px；誦戒仍以契約之 24px 驗之。
const 造線隙 = 38;
const 籽 = 0x6f2d4b19;

function 斷(真, 訊) {
  console.assert(真, 訊);
  if (!真) throw new Error(訊);
}

function 色([赤, 綠, 藍], 透) {
  return `rgba(${赤},${綠},${藍},${透})`;
}

function 籽值(序, 鹽 = 0) {
  let 值 = 籽 ^ Math.imul(序 + 1, 0x45d9f3b) ^ Math.imul(鹽 + 1, 0x27d4eb2d);
  值 ^= 值 >>> 16;
  值 = Math.imul(值, 0x7feb352d);
  值 ^= 值 >>> 15;
  值 = Math.imul(值, 0x846ca68b);
  值 ^= 值 >>> 16;
  return (值 >>> 0) / 4294967296;
}

function 緩入出(值) {
  const 定 = Math.max(0, Math.min(1, 值));
  return 定 * 定 * (3 - 2 * 定);
}

function 畫幅內([甲, 乙]) {
  return 甲 >= 0 && 甲 <= W && 乙 >= 0 && 乙 <= H;
}

function 轉點(順距, 橫距) {
  return [
    心甲 + 順甲 * 順距 + 橫甲 * 橫距,
    心乙 + 順乙 * 順距 + 橫乙 * 橫距,
    順距,
    橫距,
  ];
}

// 解 (順距·順 + 橫距·橫) 與「身橢各徑外加造線隙」之交。
// 橫距若落在兩根之間，便推至所屬肩側；不另造旋渦或回頭之勢。
function 避核(順距, 橫距, 側) {
  const 甲徑 = 橢甲徑 + 造線隙;
  const 乙徑 = 橢乙徑 + 造線隙;
  const 甲徑方 = 甲徑 * 甲徑;
  const 乙徑方 = 乙徑 * 乙徑;
  const 二次 = 橫甲 * 橫甲 / 甲徑方 + 橫乙 * 橫乙 / 乙徑方;
  const 一次 = 2 * 順距 * (順甲 * 橫甲 / 甲徑方 + 順乙 * 橫乙 / 乙徑方);
  const 常 = 順距 * 順距 * (順甲 * 順甲 / 甲徑方 + 順乙 * 順乙 / 乙徑方) - 1;
  const 判 = 一次 * 一次 - 4 * 二次 * 常;
  if (判 <= 0) return 橫距;
  const 根 = Math.sqrt(判);
  const 小 = (-一次 - 根) / (2 * 二次);
  const 大 = (-一次 + 根) / (2 * 二次);
  if (橫距 <= 小 || 橫距 >= 大) return 橫距;
  return 側 > 0 ? 大 + 5 : 小 - 5;
}

function 尾跡半寬(順距) {
  return 326 + Math.max(0, 順距 - 80) * 0.19;
}

function 常流橫距(順距, 原橫, 序, 留尾 = false) {
  const 漂 = (籽值(序, 31) - 0.5) * 15 * (順距 / 1700)
    + (籽值(序, 32) - 0.5) * 7;
  let 橫距 = 原橫 + 漂;
  const 側 = 原橫 === 0 ? (序 % 2 ? 1 : -1) : Math.sign(原橫);

  // 過身諸線不得重新填滿中央：舊流束退至漸闊扇緣，僅另錄數縷淡尾。
  if (!留尾 && 順距 > 80) {
    const 展 = 緩入出((順距 - 80) / 390);
    const 扇緣 = 尾跡半寬(順距) + Math.min(92, Math.abs(原橫) * 0.22) + (序 % 5) * 3.5;
    if (Math.abs(橫距) < 扇緣) 橫距 += (側 * 扇緣 - 橫距) * 展;
  }
  return 避核(順距, 橫距, 側);
}

function 採線({ 自, 至, 步 = 10, 原橫, 序, 留尾 = false, 橫法 = null }) {
  const 點 = [];
  for (let 順距 = 自; 順距 <= 至 + 0.001; 順距 += 步) {
    const 橫距 = 橫法
      ? 避核(順距, 橫法(順距), 橫法(順距) < 0 ? -1 : 1)
      : 常流橫距(順距, 原橫, 序, 留尾);
    點.push(轉點(順距, 橫距));
  }
  return 點;
}

const 流線簿 = [];

// 全幅常流：橫距有籽而無亂數；各線皆由順距單調遞增，唯服從同一斜勢。
for (let 序 = 0; 序 < 144; 序++) {
  const 原橫 = -1570 + (序 + 0.5) * (3140 / 144) + (籽值(序, 1) - 0.5) * 8;
  const 階 = (序 + 2 * (序 % 7)) % 5;
  流線簿.push({
    類: '常',
    點: 採線({ 自: -1660, 至: 1690, 原橫, 序 }),
    寬: [0.72, 0.82, 0.94, 1.06, 1.18][階],
    透: [0.15, 0.18, 0.22, 0.27, 0.33][階],
    金否: false,
  });
}

// 迎流面另添短束，至肩前即收：使前壓密而不把第二節律帶過身後。
for (let 序 = 0; 序 < 48; 序++) {
  const 原橫 = -455 + (序 + 0.5) * (910 / 48) + (籽值(序, 7) - 0.5) * 11;
  流線簿.push({
    類: '壓',
    點: 採線({ 自: -1190 - (序 % 4) * 24, 至: 150 + (序 % 3) * 18, 原橫, 序: 300 + 序 }),
    寬: [0.9, 1.08, 1.28, 1.48][序 % 4],
    透: [0.31, 0.39, 0.49, 0.58][序 % 4],
    金否: false,
  });
}

// 金只刻身後高速剪切緣；內外四縷同向拉長，毋作滿幅金網。
for (const 側 of [-1, 1]) {
  for (let 層 = 0; 層 < 4; 層++) {
    流線簿.push({
      類: '剪',
      點: 採線({
        自: 120 + 層 * 16,
        至: 1690,
        步: 8,
        序: 500 + 層 + (側 > 0 ? 10 : 0),
        橫法: 順距 => 側 * (尾跡半寬(順距) + 7 + 層 * 11),
      }),
      寬: [1.7, 1.42, 1.18, 0.96][層],
      透: [0.7, 0.62, 0.54, 0.44][層],
      金否: true,
    });
  }
}

// 靜域非全無風：只餘五縷甚淡長尾，彼此漸離，正顯周遭之空闊。
const 尾位 = [-0.58, -0.27, 0, 0.3, 0.61];
for (let 序 = 0; 序 < 尾位.length; 序++) {
  const 份 = 尾位[序];
  流線簿.push({
    類: '尾',
    點: 採線({
      自: 500 + (序 % 2) * 35,
      至: 1690,
      步: 12,
      序: 600 + 序,
      留尾: true,
      橫法: 順距 => 份 * 尾跡半寬(順距) + (籽值(序, 19) - 0.5) * 9,
    }),
    寬: 0.72 + (序 % 2) * 0.1,
    透: 0.15 + (序 % 3) * 0.035,
    金否: false,
  });
}

function 橢戒([甲, 乙]) {
  const 差甲 = 甲 - 心甲;
  const 差乙 = 乙 - 心乙;
  const 心距 = Math.hypot(差甲, 差乙);
  const 橢距 = Math.hypot(差甲 / 橢甲徑, 差乙 / 橢乙徑);
  const 射線徑 = 心距 / 橢距;
  return { 橢距, 射線徑, 實隙: 心距 - 射線徑 };
}

function 在迎流域(點) {
  const [, , 順距, 橫距] = 點;
  return 畫幅內(點) && 順距 >= -1050 && 順距 <= -220 && Math.abs(橫距) < 540;
}

function 在背流域(點) {
  const [, , 順距, 橫距] = 點;
  return 畫幅內(點) && 順距 >= 420 && 順距 <= 1250
    && Math.abs(橫距) < 尾跡半寬(順距) * 0.72;
}

斷(流線簿.length > 60, `風劫破戒：流線僅 ${流線簿.length}`);
for (let 線序 = 0; 線序 < 流線簿.length; 線序++) {
  const 點列 = 流線簿[線序].點;
  斷(點列.length > 1, `風劫破戒：第 ${線序} 線無長度`);
  for (let 點序 = 0; 點序 < 點列.length; 點序++) {
    const 戒 = 橢戒(點列[點序]);
    斷(戒.橢距 > 1 + 核隙 / 戒.射線徑 && 戒.實隙 > 核隙,
      `風劫破戒：第 ${線序} 線第 ${點序} 點入身橢+24`);
    if (點序) {
      const 前 = 點列[點序 - 1];
      const 今 = 點列[點序];
      const 順進 = (今[0] - 前[0]) * 順甲 + (今[1] - 前[1]) * 順乙;
      斷(順進 > 0, `風劫破戒：第 ${線序} 線回頭，另生旋律`);
    }
  }
}

const 迎流點數 = 流線簿.reduce((和, 線) => 和 + 線.點.filter(在迎流域).length, 0);
const 背流點數 = 流線簿.reduce((和, 線) => 和 + 線.點.filter(在背流域).length, 0);
const 迎背密度比 = 背流點數 / 迎流點數;
斷(迎流點數 > 0, '風劫破戒：迎流域無抽樣點');
斷(迎背密度比 < 0.4,
  `風劫破戒：尾跡未空（背 ${背流點數} / 迎 ${迎流點數} = ${迎背密度比.toFixed(3)}）`);

const 誦戒 = Object.freeze({
  流線數: 流線簿.length,
  迎流點數,
  背流點數,
  迎背密度比,
});

function 畫線(x, 線, 線序) {
  const 點列 = 線.點;
  x.beginPath();
  x.moveTo(點列[0][0], 點列[0][1]);
  for (let 序 = 1; 序 < 點列.length; 序++) x.lineTo(點列[序][0], 點列[序][1]);
  x.lineWidth = 線.寬;
  x.strokeStyle = 線.金否 ? 金 : 色(風灰, 線.透);
  if (線.金否) x.globalAlpha = 線.透;
  x.stroke();
  x.globalAlpha = 1;

  // 點亦附著於原流束：迎面飛沫密，剪切緣僅疏列數珠；無一點另成色場。
  const 點步 = 線.類 === '壓' ? 17 + (線序 % 6) : 線.類 === '剪' ? 31 + (線序 % 5) : 0;
  if (!點步) return;
  x.fillStyle = 線.金否 ? 金 : 色(風灰, Math.min(0.7, 線.透 + 0.08));
  if (線.金否) x.globalAlpha = 線.透;
  for (let 序 = 7 + (線序 % 9); 序 < 點列.length; 序 += 點步) {
    const [甲, 乙] = 點列[序];
    x.beginPath();
    x.arc(甲, 乙, 線.金否 ? 1.05 : 0.72 + (線序 % 3) * 0.15, 0, Math.PI * 2);
    x.fill();
  }
  x.globalAlpha = 1;
}

function 驗助(助) {
  if (!助) return;
  斷(助.W === W && 助.H === H, `風劫畫幅失守：${助.W}×${助.H}`);
  if (助.尊位) 斷(助.尊位 === 尊位 || JSON.stringify(助.尊位) === JSON.stringify(尊位), '風劫尊位失守');
}

export function 景(x, 助) {
  驗助(助);
  x.save();
  try {
    x.globalAlpha = 1;
    x.globalCompositeOperation = 'source-over';
    x.lineCap = 'round';
    x.lineJoin = 'round';
    for (let 序 = 0; 序 < 流線簿.length; 序++) 畫線(x, 流線簿[序], 序);
  } finally {
    x.restore();
  }
}

Object.defineProperty(景, '誦戒', { value: 誦戒, enumerable: true });

export function 飾(x, 助) { /* 無飾；風勢本身即框。 */ }
