function 環焰譜(層) {
  const 法 = [
    { n:46, rx:548, ry:684, cy:965, 長:166, 幅:23, 偏:.17 },
    { n:42, rx:444, ry:594, cy:976, 長:142, 幅:20, 偏:.41 },
    { n:38, rx:342, ry:505, cy:984, 長:118, 幅:17, 偏:.73 },
  ][層];
  const 譜 = [];
  for (let i = 0; i < 法.n; i++) {
    const a = -Math.PI / 2 + Math.PI * 2 * (i + 法.偏) / 法.n;
    const nx = Math.cos(a), ny = Math.sin(a), tx = -ny, ty = nx;
    const 波 = Math.sin(i * 1.91 + 層 * .83), 次 = Math.sin(i * .77 + 層 * 1.13);
    const 根 = [900 + nx * (法.rx + 波 * 13), 法.cy + ny * (法.ry + 次 * 16)];
    const 型 = 六焰型[(i * 5 + 層 * 2) % 六焰型.length];
    const 長 = (型 === '長飄' ? 1.28 : 型 === '短簇' ? .72 : 1) *
      (法.長 + (i % 7) * 7 + 波 * 12);
    const 拗 = (22 + (i * 17) % 47) * (i % 2 ? 1 : -1) + 次 * 13;
    const P = [
      根,
      [根[0] + nx * 長 * .25 + tx * 拗 * .52, 根[1] + ny * 長 * .25 + ty * 拗 * .52],
      [根[0] + nx * 長 * .68 - tx * 拗, 根[1] + ny * 長 * .68 - ty * 拗],
      [根[0] + nx * 長 + tx * 拗 * .24, 根[1] + ny * 長 + ty * 拗 * .24],
    ];
    譜.push({ P, 幅:法.幅 + (i * 11) % 9, 相:i * 1.21 + 層 * 19.7, 型 });
  }
  return 譜;
}

