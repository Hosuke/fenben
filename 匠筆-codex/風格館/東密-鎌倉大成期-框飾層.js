function 雷紋橫(筆, x0, x1, y, { 格 = 28, h = 9 } = {}) {
  筆.線([[x0, y - h - 5], [x1, y - h - 5]], 1.15, false, .72);
  筆.線([[x0, y + h + 5], [x1, y + h + 5]], 1.15, false, .72);
  for (let x = x0; x + 格 <= x1 + .1; x += 格) {
    筆.線([[x,y+h],[x+格*.72,y+h],[x+格*.72,y-h],[x+格*.28,y-h],
      [x+格*.28,y+h*.28],[x+格*.55,y+h*.28],[x+格*.55,y-h*.28],
      [x+格*.45,y-h*.28],[x+格*.45,y]], .86, false, .66);
  }
}

