export function 景(ctx, 助) {
  驗助(助);
  ctx.save();
  const 筆 = 造線筆(ctx, { 墨, 骨: 3.8, 衣: 2, 鬚: .8 });
  for (const 舌 of 焰舌譜) 焰舌(筆, 舌);
  for (const 舌 of 外焰增密譜) 焰舌(筆, 舌);
  for (const 舌 of 內焰舌譜) 焰舌(筆, 舌);
  for (let i = 0; i < 焰根譜.length; i++) {
    const P = 焰根譜[i];
    筆.貝(P[0], P[1], P[2], P[3], .72 + i % 3 * .16, .3 + i % 4 * .045);
  }
  頂喙(筆);
  ctx.restore();
}

export function 飾(ctx, 助) {
  驗助(助);
  ctx.save();
  const 筆 = 造線筆(ctx, { 墨, 骨: 3.8, 衣: 2, 鬚: .8 });
  簡瑟瑟座(筆);
  簡水波岩基(筆);
  ctx.restore();
}
