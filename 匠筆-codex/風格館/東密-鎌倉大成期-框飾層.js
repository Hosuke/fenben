function 驗助(助) {
  斷(助 && 助.尊位, '東密-鎌倉大成期框飾層：缺 助.尊位');
  斷(助.W === 尊位.W && 助.H === 尊位.H, `畫幅失守：${助.W}×${助.H}`);
  for (const 名 of ['W', 'H', '頂y', '指', '中x', '座面']) {
    斷(助.尊位[名] === 尊位[名], `尊位失守：${名}`);
  }
  斷(尊位.頂y + 68 * 尊位.指 === 尊位.座面, '座面非頂y+68指');
}

function 貝點(P, t) {
  const s = 1 - t;
  return [
    s ** 3 * P[0][0] + 3 * s * s * t * P[1][0] + 3 * s * t * t * P[2][0] + t ** 3 * P[3][0],
    s ** 3 * P[0][1] + 3 * s * s * t * P[1][1] + 3 * s * t * t * P[2][1] + t ** 3 * P[3][1],
  ];
}

// 各舌只共用運筆法，四點骨勢逐舌另定，毋以旋轉、平移複拓。
function 焰舌(筆, { P, 幅, 相 }) {
  const 左 = [], 右 = [], n = 22;
  for (let i = 0; i <= n; i++) {
    const t = i / n, p = 貝點(P, t);
    const p0 = 貝點(P, Math.max(0, t - .015));
    const p1 = 貝點(P, Math.min(1, t + .015));
    const dx = p1[0] - p0[0], dy = p1[1] - p0[1], d = Math.hypot(dx, dy) || 1;
    const 呼吸 = .84 + .16 * Math.sin(t * Math.PI * 2.7 + 相);
    const w = 幅 * (Math.sin(Math.PI * t) ** .72 + .08 * (1 - t)) * 呼吸;
    左.push([p[0] - dy / d * w, p[1] + dx / d * w]);
    右.push([p[0] + dy / d * w, p[1] - dx / d * w]);
  }
  筆.線(左, 2.45, false, .82);
  筆.線(右, 1.65, false, .68);
  筆.貝(P[0], 貝點(P, .24), 貝點(P, .52), 貝點(P, .78), .8, .43);
}

