function 驗助(助) {
  斷(助 && 助.尊位, '東密-鎌倉大成期框飾層：缺 助.尊位');
  斷(助.W === 尊位.W && 助.H === 尊位.H, `畫幅失守：${助.W}×${助.H}`);
  for (const 名 of ['W', 'H', '頂y', '指', '中x', '座面']) {
    斷(助.尊位[名] === 尊位[名], `尊位失守：${名}`);
  }
  斷(尊位.頂y + 68 * 尊位.指 === 尊位.座面, '座面非頂y+68指');
}

function 貝點(P, t) {
  const s = 1 - t;
  return [
    s ** 3 * P[0][0] + 3 * s * s * t * P[1][0] + 3 * s * t * t * P[2][0] + t ** 3 * P[3][0],
    s ** 3 * P[0][1] + 3 * s * s * t * P[1][1] + 3 * s * t * t * P[2][1] + t ** 3 * P[3][1],
  ];
}

function 焰勢點(P, t, 幅, 相) {
  const p = 貝點(P, t), p72 = 貝點(P, .72), 梢 = P[3];
  const dx = 梢[0] - p72[0], dy = 梢[1] - p72[1], d = Math.hypot(dx, dy) || 1;
  const u = Math.max(0, (t - .72) / .28), 回 = u * u * (3 - 2 * u);
  const 側 = Math.sin(相 * 1.731) >= 0 ? 1 : -1;
  // 末三成先抽長、後偏鋒回鉤：梢尖仍承本舌骨勢，不移根點與層位。
  return [p[0] + dx / d * 幅 * .2 * 回 - dy / d * 幅 * .68 * 側 * 回,
    p[1] + dy / d * 幅 * .2 * 回 + dx / d * 幅 * .68 * 側 * 回];
}

function 焰界(P, 幅, 相) {
  const 左 = [], 右 = [], n = 28;
  for (let i = 0; i <= n; i++) {
    const t = i / n, p = 焰勢點(P, t, 幅, 相);
    const p0 = 焰勢點(P, Math.max(0, t - .012), 幅, 相);
    const p1 = 焰勢點(P, Math.min(1, t + .012), 幅, 相);
    const dx = p1[0] - p0[0], dy = p1[1] - p0[1], d = Math.hypot(dx, dy) || 1;
    const 呼吸 = .84 + .16 * Math.sin(t * Math.PI * 2.7 + 相);
    const 尖收 = t > .72 ? 1 - (t - .72) / .28 * .28 : 1;
    const w = 幅 * (Math.sin(Math.PI * t) ** .72 + .07 * (1 - t)) * 呼吸 * 尖收;
    const 偏鋒 = Math.sin(t * Math.PI * 2.35 + 相) * .13;
    左.push([p[0] - dy / d * w * (1 + 偏鋒), p[1] + dx / d * w * (1 + 偏鋒)]);
    右.push([p[0] + dy / d * w * (1 - 偏鋒), p[1] - dx / d * w * (1 - 偏鋒)]);
  }
  return { 左, 右 };
}

function 芯線(筆, { P, 幅, 相, 側 = 0, 起 = .04, 止 = .9, 寬 = .72, 濃 = .5 }) {
  const 點 = [], n = 13;
  for (let i = 0; i <= n; i++) {
    const t = 起 + (止 - 起) * i / n;
    const p = 焰勢點(P, t, 幅, 相);
    const p0 = 焰勢點(P, Math.max(0, t - .012), 幅, 相);
    const p1 = 焰勢點(P, Math.min(1, t + .012), 幅, 相);
    const dx = p1[0] - p0[0], dy = p1[1] - p0[1], d = Math.hypot(dx, dy) || 1;
    const 開 = Math.sin(Math.PI * i / n) ** .8;
    點.push([p[0] - dy / d * 幅 * 側 * 開, p[1] + dx / d * 幅 * 側 * 開]);
  }
  筆.線(點, 寬, false, 濃);
}

function 短焰簇(筆, { P, 幅, 相, 濃 }) {
  const p0 = P[0], p32 = 貝點(P, .32);
  const dx = p32[0] - p0[0], dy = p32[1] - p0[1], d = Math.hypot(dx, dy) || 1;
  const nx = -dy / d, ny = dx / d, 數 = 2 + (Math.floor(Math.abs(Math.sin(相)) * 11) % 2);
  for (let i = 0; i < 數; i++) {
    const 側 = i - (數 - 1) / 2, 長 = 幅 * (1.55 + i * .28);
    const 起 = [p0[0] + nx * 幅 * 側 * .55, p0[1] + ny * 幅 * 側 * .55];
    const 腰 = [起[0] + dx / d * 長 * .46 + nx * 幅 * 側 * .28,
      起[1] + dy / d * 長 * .46 + ny * 幅 * 側 * .28];
    const 尖 = [起[0] + dx / d * 長 + nx * 幅 * (側 * .34 + (i % 2 ? .2 : -.2)),
      起[1] + dy / d * 長 + ny * 幅 * (側 * .34 + (i % 2 ? .2 : -.2))];
    const 鉤 = [尖[0] + dx / d * 幅 * .13 - nx * 幅 * (i % 2 ? .22 : -.22),
      尖[1] + dy / d * 幅 * .13 - ny * 幅 * (i % 2 ? .22 : -.22)];
    筆.線([起, 腰, 尖, 鉤], .56 + i * .08, false, (.4 + i * .04) * 濃);
  }
}

// 各舌只共用運筆法，四點骨勢逐舌另定，毋以旋轉、平移複拓。
// 六型之別在收鋒與芯脈，非將同一火印搬移環拱。
function 焰舌(筆, { P, 幅, 相, 型 = '單卷', 濃 = 1 }) {
  const { 左, 右 } = 焰界(P, 幅, 相);
  筆.線(左, 2.8, false, .86 * 濃);
  筆.線(右, 2.0, false, .74 * 濃);
  // 每舌二芯起，長舌三芯；偏鋒、長短與末端亦随勢收尖。
  芯線(筆, { P, 幅, 相, 側:-.08, 起:.03, 止:.94, 寬:1.08, 濃:.6 * 濃 });
  芯線(筆, { P, 幅, 相, 側:.27, 起:.1, 止:.84, 寬:.78, 濃:.46 * 濃 });
  if (型 !== '短簇') {
    const 芯側 = 型 === '雙卷' || 型 === '三叉' ? -.31 : 型 === '鳥喙鉤' ? .38 : -.23;
    const 芯止 = 型 === '長飄' ? .9 : 型 === '鳥喙鉤' ? .87 : .78;
    芯線(筆, { P, 幅, 相:相 + .17, 側:芯側, 起:.18, 止:芯止, 寬:.68, 濃:.42 * 濃 });
  }

  // 基部二三短焰，各自抽尖微鉤，不再以長弧口接主芯。
  短焰簇(筆, { P, 幅, 相, 濃 });
}

function 籽亂(種) {
  let s = 種 >>> 0;
  return () => ((s = (Math.imul(s, 1664525) + 1013904223) >>> 0) / 4294967296);
}

function 燄星層(筆, 譜, { 種, 率, 濃, 眾 }) {
  const 亂 = 籽亂(種);
  for (let i = 0; i < 譜.length; i++) {
    const A = 譜[i], B = 譜[(i + 1) % 譜.length];
    if (亂() > 率) continue;
    const t = .32 + 亂() * .35, a = 貝點(A.P, t), b = 貝點(B.P, t);
    const cx = a[0] + (b[0] - a[0]) * (.4 + 亂() * .2);
    const cy = a[1] + (b[1] - a[1]) * (.4 + 亂() * .2);
    const n = 2 + Math.floor(亂() * 眾);
    for (let k = 0; k < n; k++) {
      const a = 亂() * Math.PI * 2, r = 4 + 亂() * (7 + 眾 * 2);
      筆.點(cx + Math.cos(a) * r, cy + Math.sin(a) * r,
        .85 + 亂() * 1.15, (.3 + 亂() * .22) * 濃);
    }
  }
}

function 淨焰身(ctx, { P, 幅, 相 }) {
  const { 左, 右 } = 焰界(P, 幅 + 3.5, 相);
  ctx.save();
  ctx.globalCompositeOperation = 'destination-out';
  ctx.globalAlpha = 1;
  ctx.beginPath();
  ctx.moveTo(...左[0]);
  for (let i = 1; i < 左.length; i++) ctx.lineTo(...左[i]);
  for (let i = 右.length - 1; i >= 0; i--) ctx.lineTo(...右[i]);
  ctx.closePath(); ctx.fill(); ctx.restore();
}

