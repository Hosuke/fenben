// 杳眇《琉璃地》匠彩：一色主「暮菫」，取紫光與金線相激，不復主坊琉璃青之地。
// 光源仍唯佛身、十二燈、日月與道中一蕾；座前佛光向下析作三股偏心長潮。
// 暖金、月霜、蕾碧只作近源微應，大片天穹與潮間皆還底色，令虛空自存。
// 諸源座標悉由尊身、框飾契約而入；單場峰值不逾 0.22，彩恆居金線之下。
import { 尊位, 日輪, 月輪, 蕾位, 燈列 } from '../../匠筆-主坊/風格館/杳眇-琉璃地-尊身層.js';
import { 尊位 as 框位, 界道簿 } from './杳眇-琉璃地-框飾層.js';

export const 色譜 = {
  暮菫: [126, 82, 188],
  佛金: [238, 204, 146],
  燈金: [224, 170, 104],
  日珀: [232, 184, 112],
  月霜: [180, 218, 232],
  蕾碧: [112, 202, 178],
};
export const 彩律 = { 峰α: 0.22 };

const 斷 = (可, 言) => { console.assert(可, 言); if (!可) throw new Error(言); };
const 染 = ([赤, 綠, 藍], 透) => {
  斷(Number.isFinite(透) && 透 >= 0 && 透 <= 彩律.峰α,
    `彩奪線！α${透}逾${彩律.峰α}`);
  return `rgba(${赤},${綠},${藍},${透})`;
};

export function 彩(x, 助) {
  const { W, H } = 尊位;
  const [佛甲, 佛乙] = 框位.滅點;
  const 地平 = 框位.地平;

  斷(W === 框位.W && H === 框位.H, '琉璃地尊框畫幅不合！');
  斷([佛甲, 佛乙, 地平].every(Number.isFinite), '滅點地平契約畸！');
  斷(佛乙 === 地平, '佛座未繫地平！');
  斷(Array.isArray(燈列) && 燈列.length === 12, `燈列非十二：${燈列?.length}`);
  斷(Array.isArray(界道簿) && 界道簿.length > 1, '界道簿失約！');

  let 場數 = 0;

  const 暈 = (源甲, 源乙, 徑, 色, 峰) => {
    const 漸 = x.createRadialGradient(源甲, 源乙, 0, 源甲, 源乙, 徑);
    漸.addColorStop(0, 染(色, 峰));
    漸.addColorStop(0.38, 染(色, 峰 * 0.58));
    漸.addColorStop(0.72, 染(色, 峰 * 0.18));
    漸.addColorStop(1, 染(色, 0));
    x.fillStyle = 漸;
    x.beginPath(); x.arc(源甲, 源乙, 徑, 0, 2 * Math.PI); x.fill();
    場數++;
  };

  // 內圓立於契約光源，外圓偏向道底：光自佛出而下行，非另立無源色心。
  const 偏潮 = (橫徑, 縱徑, 下偏, 峰) => {
    x.save();
    x.translate(佛甲, 佛乙);
    x.scale(橫徑, 縱徑);
    const 漸 = x.createRadialGradient(0, 0, 0, 0, 下偏, 1 + 下偏);
    漸.addColorStop(0, 染(色譜.暮菫, 峰));
    漸.addColorStop(0.28, 染(色譜.暮菫, 峰 * 0.72));
    漸.addColorStop(0.7, 染(色譜.暮菫, 峰 * 0.22));
    漸.addColorStop(1, 染(色譜.暮菫, 0));
    x.fillStyle = 漸;
    x.beginPath(); x.arc(0, 下偏, 1 + 下偏, 0, 2 * Math.PI); x.fill();
    x.restore();
    場數++;
  };

  x.save();
  x.globalAlpha = 1;
  x.globalCompositeOperation = 'destination-over';

  // 十二燈只照其珠旁，不以暖色連成第二主勢。
  for (const [燈甲, 燈乙, 燈高] of 燈列)
    暈(燈甲, 燈乙 - 燈高 * 1.13, 燈高 * 1.9, 色譜.燈金, 0.075);

  暈(蕾位[0], 蕾位[1] - 尊位.指 * 31, 尊位.指 * 15, 色譜.蕾碧, 0.075);
  暈(日輪[0], 日輪[1], 日輪[2] * 2.35, 色譜.日珀, 0.085);
  暈(月輪[0], 月輪[1], 月輪[2] * 2.35, 色譜.月霜, 0.075);
  暈(佛甲, 佛乙 - 尊位.指 * 24, 尊位.指 * 43, 色譜.佛金, 0.135);

  // 三潮之幅取界道簿全展，深取地平以下之距；毋於彩層重錄透視座標。
  const 界展 = Math.abs(界道簿.at(-1)[0] - 界道簿[0][0]);
  const 地深 = H - 地平;
  const 潮幅 = Math.min(W * 0.92, 界展 * 0.42);
  偏潮(潮幅 * 0.62, 地深 * 0.62, 0.42, 0.115);
  偏潮(潮幅 * 0.38, 地深 * 0.78, 0.48, 0.085);
  偏潮(潮幅 * 0.2, 地深 * 0.92, 0.54, 0.06);

  x.restore();

  斷(場數 === 19, `色場非十九：${場數}`);
  console.log(`琉璃地匠彩✓ 十九場：暮菫佛潮3・佛1・燈12・日月2・蕾1；峰α≤${彩律.峰α}`);
}
