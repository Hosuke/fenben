function 斷(真, 訊) {
  console.assert(真, 訊);
  if (!真) throw new Error(訊);
}

function 驗助(助) {
  斷(助 && 助.尊位, '藏地-欽孜框飾層：缺 助.尊位');
  斷(助.W === 尊位.W && 助.H === 尊位.H, `畫幅失守：${助.W}×${助.H}`);
  for (const 名 of ['W', 'H', '頂y', '指', '中x', '座面']) {
    斷(助.尊位[名] === 尊位[名], `尊位失守：${名}`);
  }
  斷(尊位.頂y + 68 * 尊位.指 === 尊位.座面, '座面非頂y+68指');
  return 助.尊位;
}

function 貝點(P, t) {
  const s = 1 - t;
  return [
    s ** 3 * P[0][0] + 3 * s * s * t * P[1][0] + 3 * s * t * t * P[2][0] + t ** 3 * P[3][0],
    s ** 3 * P[0][1] + 3 * s * s * t * P[1][1] + 3 * s * t * t * P[2][1] + t ** 3 * P[3][1],
  ];
}

// 定種取貌：只供筆勢分歧，不涉 Math.random／Date。五型復以尺度、偏鋒、鏡向
// 各自變形；既有每舌四控制點又皆異，故無整瓣 stamp 複拓。
function 雜湊(seed, 鹽 = 0) {
  let 數 = (seed ^ Math.imul(鹽 + 1, 0x9e3779b1)) | 0;
  數 = Math.imul(數 ^ (數 >>> 16), 0x21f0aaad);
  數 = Math.imul(數 ^ (數 >>> 15), 0x735a2d97);
  return ((數 ^ (數 >>> 15)) >>> 0) / 4294967296;
}

function 焰貌(seed) {
  return {
    型: ((seed % 5) + 5) % 5,
    偏: 雜湊(seed, 7) * 2 - 1,
    鏡: 雜湊(seed, 11) < .5 ? -1 : 1,
    肩: .88 + 雜湊(seed, 13) * .24,
    芯數: 雜湊(seed, 17) < .47 ? 1 : 2,
    基簇: 雜湊(seed, 19) < .5 ? 2 : 3,
  };
}

function 切向法向(P, t) {
  const p0 = 貝點(P, Math.max(0, t - .012)), p1 = 貝點(P, Math.min(1, t + .012));
  const dx = p1[0] - p0[0], dy = p1[1] - p0[1], d = Math.hypot(dx, dy) || 1;
  return { 切: [dx / d, dy / d], 法: [-dy / d, dx / d] };
}

// 一舌一曲線，以分段線寬成提按；簇資料皆逐簇明列，不以格陣或 stamp 複印。
function 頓挫貝(筆, P, { 寬 = 3, 透明 = .78, 相 = 0 } = {}) {
  const 段 = 18;
  for (let i = 0; i < 段; i++) {
    const a = 貝點(P, i / 段), b = 貝點(P, (i + 1) / 段);
    const w = 寬 * (.55 + .45 * Math.abs(Math.sin((i + 相) * .72)));
    筆.線([a, b], w, false, 透明);
  }
}

function 焰基短簇(筆, P, 幅, 相, 透明, seed, 貌) {
  const 根 = 貝點(P, .035), { 切, 法 } = 切向法向(P, .05);
  for (let k = 0; k < 貌.基簇; k++) {
    const 側 = 貌.基簇 === 2 ? (k ? 1 : -1) : k - 1;
    const 抽 = 38 + 9 * k + 雜湊(seed, 31 + k) * 18;
    const 歪 = 側 * (12 + 幅 * .24) + 貌.偏 * 7;
    const 起 = [根[0] + 法[0] * 歪 - 切[0] * (4 + k * 3), 根[1] + 法[1] * 歪 - 切[1] * (4 + k * 3)];
    const 梢 = [起[0] + 切[0] * 抽 + 法[0] * (側 * 8 + 貌.鏡 * 4), 起[1] + 切[1] * 抽 + 法[1] * (側 * 8 + 貌.鏡 * 4)];
    const 半 = 5.5 + 雜湊(seed, 41 + k) * 3.5;
    const 左根 = [起[0] - 法[0] * 半, 起[1] - 法[1] * 半];
    const 右根 = [起[0] + 法[0] * 半, 起[1] + 法[1] * 半];
    const 彎 = 側 * 5 + 貌.偏 * 4;
    頓挫貝(筆, [左根,
      [起[0] + 切[0] * 抽 * .32 - 法[0] * (半 + 彎), 起[1] + 切[1] * 抽 * .32 - 法[1] * (半 + 彎)],
      [梢[0] - 切[0] * 抽 * .28 - 法[0] * 2, 梢[1] - 切[1] * 抽 * .28 - 法[1] * 2], 梢],
    { 寬: 1.25, 透明: 透明 * .64, 相: 相 + k });
    頓挫貝(筆, [右根,
      [起[0] + 切[0] * 抽 * .3 + 法[0] * (半 - 彎), 起[1] + 切[1] * 抽 * .3 + 法[1] * (半 - 彎)],
      [梢[0] - 切[0] * 抽 * .25 + 法[0] * 2, 梢[1] - 切[1] * 抽 * .25 + 法[1] * 2], 梢],
    { 寬: 1.05, 透明: 透明 * .58, 相: 相 + k + 1.2 });
    筆.線([起, 梢], .62, false, 透明 * .35);
  }
}

function 焰舌(筆, P, 幅, 相, 透明, seed) {
  const 貌 = 焰貌(seed);
  const 左 = [], 右 = [], 段 = 24;
  for (let i = 0; i <= 段; i++) {
    const t = i / 段, p = 貝點(P, t);
    const p0 = 貝點(P, Math.max(0, t - .012)), p1 = 貝點(P, Math.min(1, t + .012));
    const dx = p1[0] - p0[0], dy = p1[1] - p0[1], d = Math.hypot(dx, dy) || 1;
    const 波數 = [2.2, 2.7, 3.15, 3.65, 4.1][貌.型];
    const 收束 = [.18, .22, .28, .2, .25][貌.型];
    const 呼吸 = .82 + .13 * Math.sin(t * Math.PI * 波數 + 相) + .05 * 貌.偏 * Math.sin(t * Math.PI * 5);
    const w = 幅 * 貌.肩 * (Math.sin(Math.PI * t) ** (.6 + 貌.型 * .035) * (1 - t) ** 收束 + .13 * (1 - t)) * 呼吸;
    const 偏心 = 幅 * 貌.偏 * .08 * Math.sin(Math.PI * t);
    左.push([p[0] - dy / d * (w - 偏心), p[1] + dx / d * (w - 偏心)]);
    右.push([p[0] + dy / d * (w + 偏心), p[1] - dx / d * (w + 偏心)]);
  }
  for (let i = 0; i < 段; i++) {
    const 提按 = .78 + .22 * Math.abs(Math.sin((i + 相) * .83));
    筆.線([左[i], 左[i + 1]], 3.35 * 提按, false, 透明);
    筆.線([右[i], 右[i + 1]], 2.62 * 提按, false, 透明 * .9);
  }
  筆.線([左[0], 右[0]], 1.42, false, 透明 * .58);
  // 一或二道焰心皆順原舌脊；二芯略錯肩，毋作縮小複瓣。
  for (let k = 0; k < 貌.芯數; k++) {
    const 側 = 貌.芯數 === 1 ? 貌.偏 * .08 : (k ? .16 : -.16);
    const 內 = [.04, .24, .56, .84 - 貌.型 * .012].map((t, j) => {
      const p = 貝點(P, t), { 法 } = 切向法向(P, t);
      const 移 = 側 * 幅 * Math.sin(Math.PI * t) + (j === 2 ? 貌.偏 * 2.4 : 0);
      return [p[0] + 法[0] * 移, p[1] + 法[1] * 移];
    });
    頓挫貝(筆, 內, { 寬: 1.42 - k * .1, 透明: 透明 * (.6 - k * .08), 相: 相 + 2.1 + k });
  }

  // 梢端續原切線翻卷；五型以鉤長、側向、回折各異。
  const 梢 = P[3], { 切, 法 } = 切向法向(P, .985);
  const 鉤側 = 貌.鏡 * (16 + 貌.型 * 3.2), 鉤長 = 20 + 貌.型 * 4 + 雜湊(seed, 23) * 9;
  頓挫貝(筆, [梢,
    [梢[0] + 切[0] * 鉤長 * .8, 梢[1] + 切[1] * 鉤長 * .8],
    [梢[0] + 切[0] * 鉤長 * .52 + 法[0] * 鉤側, 梢[1] + 切[1] * 鉤長 * .52 + 法[1] * 鉤側],
    [梢[0] - 切[0] * 鉤長 * .14 + 法[0] * 鉤側 * .8, 梢[1] - 切[1] * 鉤長 * .14 + 法[1] * 鉤側 * .8]],
  { 寬: 1.18, 透明: 透明 * .7, 相: 相 + 4.7 });
  焰基短簇(筆, P, 幅, 相, 透明, seed, 貌);
}

function 畫焰簇(ctx, 簇) {
  const 筆 = 造線筆(ctx, { 墨: 簇.色 ?? 墨, 骨: 4.2, 衣: 2.2, 鬚: .8 });
  for (let i = 0; i < 簇.舌.length; i++) {
    const P = 簇.舌[i];
    const seed = ((簇.種 ?? Math.round(簇.相 * 1009)) + i * 131 + Math.round(P[0][0] * 3 + P[0][1])) >>> 0;
    焰舌(筆, P, (簇.幅 ?? 34) * (1 - i * .075), 簇.相 + i * 1.7, (簇.透明 ?? .74) - i * .035, seed);
  }
  for (const P of 簇.回鋒 ?? []) 頓挫貝(筆, P, { 寬: 1.2, 透明: .48, 相: 簇.相 + 4 });
}

function 畫單焰(ctx, 舌) {
  const 筆 = 造線筆(ctx, { 墨: 舌.色 ?? 墨, 骨: 4, 衣: 2, 鬚: .8 });
  const seed = (舌.種 ?? Math.round(舌.相 * 1223 + 舌.P[0][0] * 5 + 舌.P[0][1])) >>> 0;
  焰舌(筆, 舌.P, 舌.幅 ?? 30, 舌.相, 舌.透明 ?? .7, seed);
}

