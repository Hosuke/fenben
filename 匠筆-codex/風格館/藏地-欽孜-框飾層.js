function 曲珠帶(筆, P, seed, 透明 = .62) {
  const 數 = 31 + seed % 4;
  for (let k = 1; k < 數; k++) {
    const t = k / 數, p = 貝點(P, t), { 切 } = 切向法向(P, t);
    const 角 = Math.atan2(切[1], 切[0]);
    筆.橢(p[0], p[1], 4.1 + (k + seed) % 3 * .38, 3.25 + (k * 3 + seed) % 2 * .35,
      .72, 角, 透明 - (k % 4) * .025);
    if ((k + seed) % 2 === 0) 筆.點(p[0], p[1], .92, 透明 * .58);
  }
}

