function 驗焰色比() {
  const 數 = new Map([[墨, 0], [暗朱, 0], [冷灰, 0]]);
  const 型 = new Set();
  for (const 簇 of 焰簇) {
    數.set(簇.色 ?? 墨, 數.get(簇.色 ?? 墨) + 簇.舌.length);
    for (let i = 0; i < 簇.舌.length; i++) {
      const P = 簇.舌[i];
      型.add(焰貌(((簇.種 ?? Math.round(簇.相 * 1009)) + i * 131 + Math.round(P[0][0] * 3 + P[0][1])) >>> 0).型);
    }
  }
  for (const 舌 of [...內緣焰舌, ...穿插小焰舌]) {
    數.set(舌.色 ?? 墨, 數.get(舌.色 ?? 墨) + 1);
    型.add(焰貌((舌.種 ?? Math.round(舌.相 * 1223 + 舌.P[0][0] * 5 + 舌.P[0][1])) >>> 0).型);
  }
  const [金, 朱, 灰] = [數.get(墨), 數.get(暗朱), 數.get(冷灰)];
  斷(金 === 54 && 朱 === 19 && 灰 === 19, `焰色比失守：${金}:${朱}:${灰}`);
  斷(型.size === 5, `焰瓣五型失守：${[...型].sort().join(',')}`);
}

