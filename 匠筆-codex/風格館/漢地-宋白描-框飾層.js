// 據 Met 1980.276 王振鵬《維摩不二圖》之榻制；人物悉歸主坊。

import { 造線筆 } from './lib/白描幀具.js';

export const 尊位 = Object.freeze({
  W: 1800,
  H: 2500,
  頂y: 640,
  指: 11,
  中x: 940,
  座面: 1400,
});

const 墨 = '#d8b36a';

function 斷(真, 訊) {
  console.assert(真, 訊);
  if (!真) throw new Error(訊);
}

function 驗助(助) {
  斷(助 && 助.尊位, '宋白描框飾層：缺 助.尊位');
  斷(助.W === 尊位.W && 助.H === 尊位.H, `畫幅失守：${助.W}×${助.H}`);
  for (const 名 of ['W', 'H', '頂y', '指', '中x', '座面'])
    斷(助.尊位[名] === 尊位[名], `尊位失守：${名}`);
}

function 淨域(ctx, 落) {
  ctx.save();
  ctx.globalCompositeOperation = 'destination-out';
  ctx.beginPath(); 落(ctx); ctx.fill();
  ctx.restore();
}

