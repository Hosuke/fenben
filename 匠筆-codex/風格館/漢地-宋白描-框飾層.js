// 漢地・宋白描「維摩不二」框飾層
// 據 Met 1980.276 王振鵬《維摩不二圖》之長榻木作；人物悉歸主坊。

import { 造線筆 } from './lib/白描幀具.js';

export const 尊位 = Object.freeze({
  W: 3200,
  H: 1120,
  頂y: 150,
  指: 8,
  中x: 2000,
  天女軸: 550,
  座面: 703,
});

const 墨 = '#d8b36a';

function 斷(真, 訊) {
  console.assert(真, 訊);
  if (!真) throw new Error(訊);
}

function 驗助(助) {
  斷(助 && 助.尊位, '宋白描框飾層：缺 助.尊位');
  斷(助.W === 尊位.W && 助.H === 尊位.H, `畫幅失守：${助.W}×${助.H}`);
  for (const 名 of ['W', 'H', '頂y', '指', '中x', '天女軸', '座面'])
    斷(助.尊位[名] === 尊位[名], `尊位失守：${名}`);
}

// 足几在榻足之前，惟以實形自蔽；透明布仍不鋪底。
function 淨域(ctx, 落) {
  ctx.save();
  ctx.globalAlpha = 1;
  ctx.globalCompositeOperation = 'destination-out';
  ctx.fillStyle = '#000';
  ctx.beginPath();
  落(ctx);
  ctx.fill();
  ctx.restore();
}

function 地平(ctx) {
  const 筆 = 造線筆(ctx, { 墨, 骨: 1.1, 衣: .8, 鬚: .55 });
  // 僅卷尾兩段淡地平；天女側不立石、不設几、不添雲。
  筆.貝([240, 1041], [430, 1037], [690, 1045], [900, 1040], .78);
  筆.貝([2750, 1040], [2850, 1036], [2990, 1044], [3100, 1039], .78);
}

function 圓紋(筆, cx, cy) {
  筆.圓(cx, cy, 42, 1.65);
  筆.圓(cx, cy, 32, .92);
  筆.圓(cx, cy, 8, .72);
}

function 長榻(ctx) {
  const 筆 = 造線筆(ctx, { 墨, 骨: 3.6, 衣: 1.7, 鬚: .78 });

  // 後欄板：欄紋故意後繪過維摩軀；主坊修層自蔽而重描坐身。
  筆.線([[1520, 700], [1520, 560], [2700, 560], [2700, 700]], 3.4);
  筆.線([[1532, 692], [1532, 572], [2688, 572], [2688, 692]], 1.25);
  筆.線([[1520, 584], [2700, 584]], 1.05);
  筆.線([[1520, 680], [2700, 680]], 1.05);

  for (let cx = 1580; cx <= 2640; cx += 106) 圓紋(筆, cx, 632);
  // 圓列之間以成對短櫺相承，不以密線填空。
  for (let cx = 1633; cx < 2640; cx += 106) {
    筆.線([[cx - 4, 586], [cx - 4, 596]], .72);
    筆.線([[cx + 4, 586], [cx + 4, 596]], .72);
    筆.線([[cx - 4, 668], [cx - 4, 678]], .72);
    筆.線([[cx + 4, 668], [cx + 4, 678]], .72);
  }

  // 榻面正守 y=703；前沿雙木橫材。
  筆.線([[1520, 703], [2700, 703]], 4.0);
  筆.線([[1520, 714], [2700, 714]], 1.65);
  筆.線([[1520, 714], [1542, 786], [2678, 786], [2700, 714]], 2.8);
  筆.線([[1542, 773], [2678, 773]], 1.05);

  // 榻沿木紋只作斷續六痕，毋以通線塞滿橫材。
  for (const [p0, c1, c2, p1] of [
    [[1582, 744], [1635, 738], [1684, 750], [1738, 743]],
    [[1782, 758], [1826, 752], [1872, 762], [1916, 756]],
    [[2050, 741], [2100, 735], [2152, 748], [2204, 741]],
    [[2260, 758], [2308, 751], [2356, 762], [2406, 755]],
    [[2460, 741], [2500, 736], [2542, 747], [2584, 742]],
    [[2608, 758], [2630, 754], [2650, 759], [2668, 756]],
  ]) 筆.貝(p0, c1, c2, p1, .72);

  // 四足各具壸門弧；足底齊近 y=1010。
  for (const [x0, x1] of [[1548, 1742], [1840, 2034], [2186, 2380], [2480, 2674]]) {
    const 中 = (x0 + x1) / 2;
    筆.線([[x0, 786], [x0, 1002]], 3.0);
    筆.線([[x1, 786], [x1, 1002]], 3.0);
    筆.貝([x0, 1002], [x0 + 42, 1000], [x0 + 45, 902], [中, 896], 2.15);
    筆.貝([中, 896], [x1 - 45, 902], [x1 - 42, 1000], [x1, 1002], 2.15);
    筆.貝([x0 + 11, 991], [x0 + 43, 988], [x0 + 52, 916], [中, 908], .72);
    筆.貝([中, 908], [x1 - 52, 916], [x1 - 43, 988], [x1 - 11, 991], .72);
    筆.線([[x0 - 10, 1010], [x1 + 10, 1010]], 1.9);
  }
}

function 足几(ctx) {
  const 筆 = 造線筆(ctx, { 墨, 骨: 3.2, 衣: 1.55, 鬚: .72 });

  // 几與履遮住其後榻足，蔽域限於器物本形，不侵空場。
  淨域(ctx, p => {
    p.rect(1877, 946, 286, 72);
    p.ellipse(1956, 929, 59, 26, -.08, 0, Math.PI * 2);
    p.ellipse(2080, 927, 59, 26, -.06, 0, Math.PI * 2);
  });

  // 几面雙緣，外廓嚴守 x1880–2160、y950–1015。
  筆.線([[1880, 950], [2160, 950]], 3.5);
  筆.線([[1880, 960], [2160, 960]], 1.5);
  筆.線([[1888, 955], [2152, 955]], .7);
  筆.線([[1892, 960], [1892, 1008]], 2.7);
  筆.線([[2148, 960], [2148, 1008]], 2.7);
  筆.線([[1901, 963], [1901, 1001]], .72);
  筆.線([[2139, 963], [2139, 1001]], .72);
  筆.貝([1892, 1008], [1914, 1007], [1922, 984], [1944, 982], 1.55);
  筆.貝([2096, 982], [2118, 984], [2126, 1007], [2148, 1008], 1.55);
  筆.線([[1880, 1015], [2160, 1015]], 1.9);

  // 几上雙履，履頭俱向左；止取輪廓、口沿、底沿三筆。
  const 描履 = (x, y, s) => {
    筆.貝([x + 102 * s, y + 36 * s], [x + 82 * s, y + 12 * s], [x + 48 * s, y + 5 * s], [x + 18 * s, y + 12 * s], 1.75);
    筆.貝([x + 18 * s, y + 12 * s], [x - 2 * s, y + 20 * s], [x - 4 * s, y + 34 * s], [x + 10 * s, y + 39 * s], 1.75);
    筆.貝([x + 10 * s, y + 39 * s], [x + 39 * s, y + 48 * s], [x + 82 * s, y + 48 * s], [x + 108 * s, y + 39 * s], 1.55);
    筆.貝([x + 108 * s, y + 39 * s], [x + 112 * s, y + 31 * s], [x + 110 * s, y + 22 * s], [x + 101 * s, y + 16 * s], 1.25);
    筆.貝([x + 100 * s, y + 17 * s], [x + 83 * s, y + 28 * s], [x + 65 * s, y + 31 * s], [x + 48 * s, y + 28 * s], .78);
    筆.貝([x + 11 * s, y + 42 * s], [x + 42 * s, y + 53 * s], [x + 82 * s, y + 53 * s], [x + 107 * s, y + 43 * s], .72);
  };
  描履(1902, 900, .9);
  描履(2027, 898, .9);
}

export function 景(ctx, 助) {
  驗助(助);
  ctx.save();
  try {
    ctx.globalAlpha = 1;
    ctx.globalCompositeOperation = 'source-over';
    地平(ctx);
  } finally {
    ctx.restore();
  }
}

export function 飾(ctx, 助) {
  驗助(助);
  ctx.save();
  try {
    ctx.globalAlpha = 1;
    ctx.globalCompositeOperation = 'source-over';
    長榻(ctx);
    足几(ctx);
  } finally {
    ctx.restore();
  }
}
