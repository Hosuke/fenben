// 杳眇-破暗地藏 · 框飾層（暗層與殘階——景也；匠筆候單）
// 契約：珠心＝(840, 1030)（地藏左掌明珠——全幅唯一光源，光環自此而出）；
// 暗層唯在光弱處（距珠愈遠愈密），近珠讓光；畫底三成純黑無筆。
export const 尊位 = { W: 1800, H: 2500, 珠心: [840, 1030] };

const { W, H } = 尊位;
const [珠甲, 珠乙] = 尊位.珠心;
const 暗紋色 = [184, 194, 199];
const 階石色 = [205, 207, 199];
const 人間色 = [214, 220, 220];
const 斷紋籽 = 0x4f3a29;

function 色([赤, 綠, 藍], 透) {
  return `rgba(${赤},${綠},${藍},${透})`;
}

// 整數籽只定相位與斷處；同幅同筆，絕不借亂數。
function 籽值(層, 序, 鹽 = 0) {
  let 值 = 斷紋籽 ^ Math.imul(層 + 1, 73856093) ^ Math.imul(序 + 1, 19349663) ^ Math.imul(鹽 + 1, 83492791);
  值 ^= 值 >>> 13;
  值 = Math.imul(值, 1274126177);
  值 ^= 值 >>> 16;
  return (值 >>> 0) / 4294967296;
}

function 畫線(ctx, 甲一, 乙一, 甲二, 乙二, 寬, 墨) {
  ctx.beginPath();
  ctx.moveTo(甲一, 乙一);
  ctx.lineTo(甲二, 乙二);
  ctx.lineWidth = 寬;
  ctx.strokeStyle = 墨;
  ctx.stroke();
}

function 線距珠(甲一, 甲二, 乙) {
  const 最近甲 = Math.max(甲一, Math.min(珠甲, 甲二));
  return Math.hypot(最近甲 - 珠甲, 乙 - 珠乙);
}

function 暗紋透度(距) {
  if (距 < 620) return 0;
  if (距 < 1100) return 0.14 + 0.06 * ((距 - 620) / 480);
  return 0.2 + 0.1 * Math.min(1, (距 - 1100) / 600);
}

function 暗紋密度(距) {
  if (距 < 620) return 0;
  if (距 < 1100) return 0.16 + 0.18 * ((距 - 620) / 480);
  return 0.36 + 0.44 * Math.min(1, (距 - 1100) / 600);
}

function 畫暗層斷紋(ctx) {
  let 乙 = 286;
  for (let 層 = 0; 乙 <= 2320; 層++) {
    const 槽寬 = 54 + Math.floor(籽值(層, 0, 1) * 13);
    const 相位 = 籽值(層, 0, 2) * 槽寬;
    const 底帶 = 乙 <= 2100 ? 1 : Math.max(0, (2320 - 乙) / 220);

    for (let 序 = -1; 序 * 槽寬 + 相位 < W; 序++) {
      const 甲一 = Math.max(0, 序 * 槽寬 + 相位);
      const 長 = 15 + 籽值(層, 序, 3) * 31;
      const 甲二 = Math.min(W, 甲一 + 長);
      if (甲二 - 甲一 < 5) continue;

      // 以整段至珠之最近距守 620px 光域，線端亦不得偷入。
      const 寬 = 0.72 + 籽值(層, 序, 5) * 0.5;
      const 距 = 線距珠(甲一, 甲二, 乙) - 寬 / 2;
      const 密 = 暗紋密度(距) * 底帶;
      if (密 === 0 || 籽值(層, 序, 4) >= 密) continue;

      const 透 = 暗紋透度(距);
      畫線(ctx, 甲一, 乙, 甲二, 乙, 寬, 色(暗紋色, 透));
    }
    乙 += 27 + Math.floor(籽值(層, 0, 6) * 15);
  }
}

const 殘階 = [
  { 左: 872, 右: 1138, 乙: 1702, 透: 0.45 },
  { 左: 846, 右: 1164, 乙: 1774, 透: 0.41 },
  { 左: 822, 右: 1188, 乙: 1853, 透: 0.37 },
  { 左: 799, 右: 1214, 乙: 1939, 透: 0.33 },
  { 左: 781, 右: 1238, 乙: 2031, 透: 0.3 },
];

function 畫殘階(ctx) {
  for (let 級 = 0; 級 < 殘階.length; 級++) {
    const { 左, 右, 乙, 透 } = 殘階[級];
    const 全 = 右 - 左;
    const 墨 = 色(階石色, 透);
    // 一道前緣分作三殘段；斷口逐級錯置而不相襲。
    const 斷一 = 0.19 + 籽值(級, 0, 10) * 0.08;
    const 斷二 = 0.58 + 籽值(級, 0, 11) * 0.09;
    畫線(ctx, 左, 乙, 左 + 全 * 斷一, 乙, 1.22, 墨);
    畫線(ctx, 左 + 全 * (斷一 + 0.095), 乙, 左 + 全 * 斷二, 乙, 1.22, 墨);
    畫線(ctx, 左 + 全 * (斷二 + 0.075), 乙, 右, 乙, 1.22, 墨);

    const 深 = 15 + 級 * 2;
    畫線(ctx, 左, 乙, 左 + 17, 乙 - 深, 1.05, 墨);
    // 偶級右側只留半截，見其崩殘；仍不添後緣與石紋。
    const 右長 = 級 % 2 === 0 ? 0.54 : 1;
    畫線(ctx, 右, 乙, 右 - 16 * 右長, 乙 - 深 * 右長, 1.05, 墨);
  }
}

function 畫人間一線(ctx) {
  畫線(ctx, 0, 170, W, 170, 0.95, 色(人間色, 0.3));
  畫線(ctx, 0, 164, W, 164, 0.7, 色(人間色, 0.15));
}

export function 景(ctx, 助) {
  ctx.save();
  try {
    ctx.globalAlpha = 1;
    ctx.globalCompositeOperation = 'source-over';
    ctx.lineCap = 'butt';
    ctx.lineJoin = 'round';
    畫暗層斷紋(ctx);
    畫殘階(ctx);
    畫人間一線(ctx);
  } finally {
    ctx.restore();
  }
}
export function 飾(ctx, 助) { /* 無飾 */ }
