  花樹(ctx, { x: 1366, base: 1562, top: 1050, lean: 48, sides: [-1, 1, -1, -1, 1], 色: 墨, 透明: .43 });
  const 龕心 = [258, 472, 686, 900, 1114, 1328, 1542];
  for (const cx of 龕心) 空龕(ctx, cx);
  const 金 = 造線筆(ctx, { 墨, 骨: 2, 衣: 1.05, 鬚: .55 });
  金.線([[154, 464], [1646, 464]], 1.5, false, .7);
