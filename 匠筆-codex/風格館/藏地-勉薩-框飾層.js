  雲帶(ctx, [[1288, 590], [1360, 548], [1414, 582], [1462, 620], [1518, 670], [1580, 614], [1638, 646]], .5, 墨);
  雲帶(ctx, [[164, 936], [248, 878], [326, 944], [408, 922], [482, 900], [544, 954], [616, 932]], .44, 墨);
  雲帶(ctx, [[1188, 882], [1260, 832], [1324, 904], [1388, 876], [1468, 842], [1540, 930], [1638, 898]], .46);
  雲帶(ctx, [[210, 1216], [292, 1164], [382, 1228], [468, 1192], [550, 1160], [628, 1250], [718, 1210]], .38, 冷灰);
  雲帶(ctx, [[1082, 1188], [1168, 1120], [1260, 1210], [1350, 1164], [1430, 1128], [1530, 1234], [1628, 1198]], .4, 墨);
  花樹(ctx, { x: 274, base: 1630, top: 720, lean: 64, sides: [-1, 1, -1, -1, 1, -1, 1], 色: 墨, 透明: .6 });
  花樹(ctx, { x: 1508, base: 1602, top: 674, lean: -82, sides: [1, -1, 1, -1, -1, 1, 1, -1], 色: 冷灰, 透明: .58 });
