function 地界(ctx) {
  // 避開前列二翼靜域，僅於翼外與中道落淡線。
  for (const [x0, x1] of [[80,170],[630,1170],[1630,1720]]) {
    淨矩(ctx, x0, 2115, x1 - x0, 12);
    const 筆 = 造線筆(ctx, { 墨, 骨: 1.3, 衣: .75, 鬚: .55 });
    const dx = x1 - x0;
    筆.貝([x0,2120],[x0 + dx * .34,2116],[x0 + dx * .66,2124],[x1,2120], .8, .3);
  }
}

export function 景(ctx, 助) {
  驗助(助);
  ctx.save();
  try {
    雲帶(ctx);
    身光(ctx);
    圓光(ctx, 430, 980, 120);
    圓光(ctx, 1370, 980, 120);
    圓光(ctx, 900, 660, 210, true);
    寶蓋(ctx);
  } finally {
    ctx.restore();
  }
}

export function 飾(ctx, 助) {
  驗助(助);
  ctx.save();
  try {
    須彌座(ctx);
    側蓮座(ctx, 300, 560);
    側蓮座(ctx, 1240, 1500);
    供桌(ctx);
    踏几(ctx);
    地界(ctx);
  } finally {
    ctx.restore();
  }
}
