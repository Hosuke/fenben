import { 造線筆, 蓮瓣橫 } from './lib/白描幀具.js';

export const 尊位 = Object.freeze({
  W: 1800,
  H: 2500,
  頂y: 520,
  指: 12,
  中x: 900,
  座面: 1144,
});

const 墨 = '#d8b36a';

function 斷(真, 訊) {
  console.assert(真, 訊);
  if (!真) throw new Error(訊);
}

function 驗助(助) {
  斷(助 && 助.尊位, '明水陸框飾層：缺 助.尊位');
  斷(助.W === 尊位.W && 助.H === 尊位.H, `畫幅失守：${助.W}×${助.H}`);
  for (const 名 of ['W', 'H', '頂y', '指', '中x', '座面'])
    斷(助.尊位[名] === 尊位[名], `尊位失守：${名}`);
}

function 淨域(ctx, 落) {
  ctx.save();
  ctx.globalCompositeOperation = 'destination-out';
  ctx.fillStyle = '#000';
  ctx.beginPath();
  落(ctx);
  ctx.fill();
  ctx.restore();
}

function 淨矩(ctx, x, y, w, h) {
  淨域(ctx, p => p.rect(x, y, w, h));
}

function 雲帶(ctx) {
  淨矩(ctx, 80, 326, 1640, 158);
  const 筆 = 造線筆(ctx, { 墨, 骨: 1.8, 衣: 1.05, 鬚: .62 });
  const 一段 = (y, a, shift = 0) => {
    for (let x = 90 + shift; x < 1710; x += 210) {
      筆.貝([x, y + 22], [x + 34, y - 25], [x + 82, y - 25], [x + 112, y + 8], 1.05, a);
      筆.貝([x + 112, y + 8], [x + 137, y - 12], [x + 175, y - 8], [x + 210, y + 22], .82, a * .84);
      筆.貝([x + 18, y + 31], [x + 72, y + 49], [x + 152, y + 48], [x + 198, y + 28], .68, a * .65);
    }
  };
  一段(362, .46);
  一段(430, .34, 82);
}

function 身光(ctx) {
  淨域(ctx, p => p.ellipse(900, 790, 348, 468, 0, 0, Math.PI * 2));
  const 筆 = 造線筆(ctx, { 墨, 骨: 2.5, 衣: 1.25, 鬚: .65 });
  筆.橢(900, 790, 340, 460, 2.45, 0, .68);
  筆.橢(900, 790, 326, 445, 1.0, 0, .48);
  for (let a = .18; a < Math.PI * 2; a += .115) {
    const x = 900 + Math.cos(a) * 333;
    const y = 790 + Math.sin(a) * 452;
    筆.圓(x, y, 3.6, .7, .46);
  }
}

function 圓光(ctx, cx, cy, r, 主 = false) {
  淨域(ctx, p => p.arc(cx, cy, r + 8, 0, Math.PI * 2));
  const 筆 = 造線筆(ctx, { 墨, 骨: 主 ? 2.7 : 2.1, 衣: 1.2, 鬚: .65 });
  筆.圓(cx, cy, r, 主 ? 2.6 : 2.0, 主 ? .82 : .7);
  筆.圓(cx, cy, r - 12, .85, 主 ? .5 : .42);
  const n = 主 ? 36 : 24;
  for (let k = 0; k < n; k++) {
    const a = Math.PI * 2 * k / n;
    筆.圓(cx + Math.cos(a) * (r - 6), cy + Math.sin(a) * (r - 6), 主 ? 3.2 : 2.7, .62, 主 ? .58 : .46);
  }
}

