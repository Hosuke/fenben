function 焰舌(筆, [a, 長, 幅, 偏, 收, 相]) {
  const 根 = 橢輪點(900, 1060, 477, 832, a);
  const nx = Math.cos(a), ny = Math.sin(a), tx = -ny, ty = nx;
  const 尖 = [根[0] + nx * 長 + tx * 偏, 根[1] + ny * 長 + ty * 偏];
  const 左根 = [根[0] + tx * 幅, 根[1] + ty * 幅];
  const 右根 = [根[0] - tx * 幅 * 收, 根[1] - ty * 幅 * 收];
  const 左一 = [根[0] + nx * 長 * .27 + tx * (幅 * 1.24 + 偏 * .18),
    根[1] + ny * 長 * .27 + ty * (幅 * 1.24 + 偏 * .18)];
  const 左二 = [尖[0] - nx * 長 * (.31 + 相 * .018) + tx * 幅 * .28,
    尖[1] - ny * 長 * (.31 + 相 * .018) + ty * 幅 * .28];
  const 右二 = [尖[0] - nx * 長 * (.24 - 相 * .014) - tx * 幅 * .42,
    尖[1] - ny * 長 * (.24 - 相 * .014) - ty * 幅 * .42];
  const 右一 = [根[0] + nx * 長 * .2 - tx * (幅 * 1.08 - 偏 * .12),
    根[1] + ny * 長 * .2 - ty * (幅 * 1.08 - 偏 * .12)];
  筆.貝(左根, 左一, 左二, 尖, 2.35 + 相 * .08, .86);
  筆.貝(尖, 右二, 右一, 右根, 1.7 + (1 - 相) * .16, .72);
  const 內尖 = [尖[0] - nx * 長 * .23 - tx * 偏 * .12,
    尖[1] - ny * 長 * .23 - ty * 偏 * .12];
  筆.貝(根, [根[0] + nx * 長 * .25 - tx * 偏 * .1, 根[1] + ny * 長 * .25 - ty * 偏 * .1],
    [內尖[0] - nx * 長 * .22 + tx * 幅 * .15, 內尖[1] - ny * 長 * .22 + ty * 幅 * .15],
    內尖, .82, .42);
}

