function 四瓣點花(筆, x, y, r, 角 = 0) {
  for (let k = 0; k < 4; k++) {
    const a = 角 + k * Math.PI / 2;
    筆.橢(x + Math.cos(a) * r * .62, y + Math.sin(a) * r * .62,
      r * .58, r * .25, .78, a, .64);
  }
  筆.點(x, y, Math.max(1.05, r * .14), .67);
}

