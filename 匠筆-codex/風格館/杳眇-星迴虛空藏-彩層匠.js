// 杳眇《星迴・虛空藏》匠彩：一色主「孔雀碧」，不用主坊玄青大場染遍虛空。
// 如意寶珠為唯一光源：先作一點暖芯，再將碧光析成三軸、推成三道橢圓回波。
// 軸間、波間皆不著色，讓星線穿行於原底之「無」；色勢因此是迴聲而非天幕。
// 珠位悉由尊身契約而入；七場皆漸歸零，峰值不逾 0.22，且恆居線骨之下。
import { 尊位, 珠心 } from '../../匠筆-主坊/風格館/杳眇-星迴虛空藏-尊身層.js';

export const 色譜 = {
  孔雀碧: [54, 166, 152],
  珠暖: [238, 208, 154],
};
export const 彩律 = { 峰α: 0.22 };

const 斷 = (可, 言) => { console.assert(可, 言); if (!可) throw new Error(言); };
const 染 = ([赤, 綠, 藍], 透) => {
  斷(Number.isFinite(透) && 透 >= 0 && 透 <= 彩律.峰α,
    `彩奪線！α${透}逾${彩律.峰α}`);
  return `rgba(${赤},${綠},${藍},${透})`;
};

export function 彩(x, 助) {
  const { W, H } = 尊位;
  const [珠甲, 珠乙] = 珠心;
  斷(W > 0 && H > 0, '星迴畫幅畸！');
  斷(Array.isArray(珠心) && 珠心.length === 2 && 珠心.every(Number.isFinite), '珠心契約畸！');
  斷(珠甲 > 0 && 珠甲 < W && 珠乙 > 0 && 珠乙 < H, '珠心出畫！');

  let 場數 = 0;

  const 橢場 = (橫徑, 縱徑, 旋, 色, 節) => {
    x.save();
    x.translate(珠甲, 珠乙);
    x.rotate(旋);
    x.scale(橫徑, 縱徑);
    const 漸 = x.createRadialGradient(0, 0, 0, 0, 0, 1);
    for (const [位, 透] of 節) 漸.addColorStop(位, 染(色, 透));
    x.fillStyle = 漸;
    x.beginPath(); x.arc(0, 0, 1, 0, 2 * Math.PI); x.fill();
    x.restore();
    場數++;
  };

  x.save();
  x.globalAlpha = 1;
  x.globalCompositeOperation = 'destination-over';

  // 暖芯只守珠旁，不向外爭主色。
  const 核徑 = 尊位.指 * 13;
  橢場(核徑, 核徑, 0, 色譜.珠暖,
    [[0, 0.18], [0.34, 0.115], [0.72, 0.028], [1, 0]]);

  // 三軸俱以珠心為極；長而窄，彼此錯角，留出大塊未染虛空。
  const 長 = Math.hypot(W, H) * 0.38;
  for (const [旋, 厚, 峰] of [
    [-Math.PI * 0.29, 0.105, 0.09],
    [-Math.PI * 0.06, 0.075, 0.07],
    [ Math.PI * 0.23, 0.055, 0.052],
  ]) 橢場(長, H * 厚, 旋, 色譜.孔雀碧,
    [[0, 峰], [0.24, 峰 * 0.62], [0.62, 峰 * 0.18], [1, 0]]);

  // 三道回波：中心透明、波脊微明、外緣歸零；其圓心仍是契約寶珠。
  for (const [橫徑, 縱徑, 旋, 內, 峰位, 峰] of [
    [W * 0.22, H * 0.14,  Math.PI * 0.08, 0.55, 0.72, 0.058],
    [W * 0.39, H * 0.25, -Math.PI * 0.05, 0.67, 0.82, 0.048],
    [W * 0.61, H * 0.39,  Math.PI * 0.03, 0.75, 0.88, 0.038],
  ]) 橢場(橫徑, 縱徑, 旋, 色譜.孔雀碧,
    [[0, 0], [內, 0], [峰位, 峰], [1, 0]]);

  x.restore();

  斷(場數 === 7, `色場非七：${場數}`);
  console.log(`星迴匠彩✓ 七場：珠暖1・孔雀碧分光3・回波3；峰α≤${彩律.峰α}`);
}
