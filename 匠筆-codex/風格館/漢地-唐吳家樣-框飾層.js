function 驗助(助) {
  斷(助 && 助.尊位, '唐吳家樣框飾層：缺 助.尊位');
  斷(助.W === 尊位.W && 助.H === 尊位.H, `畫幅失守：${助.W}×${助.H}`);
  for (const 名 of ['W', 'H', '頂y', '指', '中x', '足底'])
    斷(助.尊位[名] === 尊位[名], `尊位失守：${名}`);
  斷(尊位.頂y + 120 * 尊位.指 === 尊位.足底, '足底非頂y+120指');
}

function 淨輪廓(ctx, 落) {
  ctx.save();
  ctx.globalCompositeOperation = 'destination-out';
  ctx.beginPath(); 落(ctx); ctx.fill();
  ctx.restore();
}

function 橢輪點(cx, cy, rx, ry, a) {
  return [cx + Math.cos(a) * rx, cy + Math.sin(a) * ry];
}

function 橢軌勻點(cx, cy, rx, ry, 數, 起 = -Math.PI / 2) {
  const 細 = 數 * 12;
  const 表 = [{ a: 起, 長: 0 }];
  let 前 = 橢輪點(cx, cy, rx, ry, 起), 總 = 0;
  for (let i = 1; i <= 細; i++) {
    const a = 起 + i * Math.PI * 2 / 細;
    const 今 = 橢輪點(cx, cy, rx, ry, a);
    總 += Math.hypot(今[0] - 前[0], 今[1] - 前[1]);
    表.push({ a, 長: 總 }); 前 = 今;
  }
  const 果 = [];
  let j = 1;
  for (let k = 0; k < 數; k++) {
    const 求 = 總 * k / 數;
    while (表[j].長 < 求) j++;
    const 左 = 表[j - 1], 右 = 表[j];
    const t = (求 - 左.長) / (右.長 - 左.長 || 1);
    const a = 左.a + (右.a - 左.a) * t;
    果.push({ a, 點: 橢輪點(cx, cy, rx, ry, a) });
  }
  return 果;
}

function 雜湊(號, 鹽 = 0) {
  let 數 = (Math.imul(號 + 1, 0x9e3779b1) ^ Math.imul(鹽 + 17, 0x85ebca6b) ^ 0x54756e67) | 0;
  數 = Math.imul(數 ^ 數 >>> 16, 0x7feb352d);
  數 = Math.imul(數 ^ 數 >>> 15, 0x846ca68b);
  return ((數 ^ 數 >>> 16) >>> 0) / 4294967296;
}

function 葉(ctx, 筆, cx, cy, 長, 闊, 角, alpha = .78) {
  ctx.save(); ctx.translate(cx, cy); ctx.rotate(角);
  筆.貝([0, 0], [長 * .28, -闊], [長 * .78, -闊 * .72], [長, 0], 1.2, alpha);
  筆.貝([長, 0], [長 * .76, 闊 * .7], [長 * .28, 闊], [0, 0], .9, alpha * .88);
  筆.貝([長 * .08, 0], [長 * .38, -闊 * .08], [長 * .7, 闊 * .04], [長 * .91, 0], .62, alpha * .68);
  ctx.restore();
}

