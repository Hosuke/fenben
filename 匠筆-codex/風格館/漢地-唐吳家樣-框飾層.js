function 壼門(筆, cx, y0, w, h) {
  筆.線([[cx-w/2,y0+h],[cx-w/2,y0+h*.43]], 1.15, false, .62);
  筆.貝([cx-w/2,y0+h*.43],[cx-w*.48,y0+h*.12],[cx-w*.2,y0],[cx,y0], 1.15, .66);
  筆.貝([cx,y0],[cx+w*.2,y0],[cx+w*.48,y0+h*.12],[cx+w/2,y0+h*.43], 1.05, .62);
  筆.線([[cx+w/2,y0+h*.43],[cx+w/2,y0+h]], 1.05, false, .58);
  筆.二次([cx-w/2,y0+h],[cx,y0+h*.78],[cx+w/2,y0+h], .76, .42);
  筆.貝([cx-w*.28,y0+h*.42],[cx-w*.16,y0+h*.27],[cx-w*.08,y0+h*.3],[cx,y0+h*.43], .58, .38);
  筆.貝([cx,y0+h*.43],[cx+w*.08,y0+h*.3],[cx+w*.16,y0+h*.27],[cx+w*.28,y0+h*.42], .58, .38);
}

