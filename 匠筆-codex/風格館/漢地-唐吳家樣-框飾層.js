import { 造線筆 } from './lib/白描幀具.js';

export const 尊位 = Object.freeze({
  W: 1800,
  H: 2500,
  頂y: 420,
  指: 12,
  中x: 900,
  足底: 1860,
});

const 墨 = '#d8b36a';

function 斷(真, 訊) {
  console.assert(真, 訊);
  if (!真) throw new Error(訊);
}

function 驗助(助) {
  斷(助 && 助.尊位, '唐吳家樣框飾層：缺 助.尊位');
  斷(助.W === 尊位.W && 助.H === 尊位.H, `畫幅失守：${助.W}×${助.H}`);
  for (const 名 of ['W', 'H', '頂y', '指', '中x', '足底'])
    斷(助.尊位[名] === 尊位[名], `尊位失守：${名}`);
  斷(尊位.頂y + 120 * 尊位.指 === 尊位.足底, '足底非頂y+120指');
}

function 淨輪廓(ctx, 落) {
  ctx.save();
  ctx.globalCompositeOperation = 'destination-out';
  ctx.beginPath(); 落(ctx); ctx.fill();
  ctx.restore();
}

function 橢輪點(cx, cy, rx, ry, a) {
  return [cx + Math.cos(a) * rx, cy + Math.sin(a) * ry];
}

function 葉(ctx, 筆, cx, cy, 長, 闊, 角, alpha = .78) {
  ctx.save(); ctx.translate(cx, cy); ctx.rotate(角);
  筆.貝([0, 0], [長 * .28, -闊], [長 * .78, -闊 * .72], [長, 0], 1.2, alpha);
  筆.貝([長, 0], [長 * .76, 闊 * .7], [長 * .28, 闊], [0, 0], .9, alpha * .88);
  筆.貝([長 * .08, 0], [長 * .38, -闊 * .08], [長 * .7, 闊 * .04], [長 * .91, 0], .62, alpha * .68);
  ctx.restore();
}

function 唐草頭光(ctx, 筆) {
  筆.圓(900, 640, 245, 4.5, .94);
  筆.圓(900, 640, 218, 1.45, .68);
  // 盛唐淨土變通式之輪緣卷葉，候典：枝梗連續，葉勢逐枚錯落，毋作同印。
  const n = 30;
  const 點 = [];
  for (let k = 0; k <= n * 2; k++) {
    const a = -Math.PI / 2 + k * Math.PI / n;
    const r = 231 + Math.sin(k * 1.37) * 2.2;
    點.push([900 + Math.cos(a) * r, 640 + Math.sin(a) * r]);
  }
  筆.線(點, 1.18, false, .7);
  for (let k = 0; k < n; k++) {
    const a = -Math.PI / 2 + k * Math.PI * 2 / n + Math.sin(k * 1.19) * .012;
    const r = 230 + Math.cos(k * .83) * 2;
    const [x, y] = 橢輪點(900, 640, r, r, a);
    const 內外 = k % 2 ? -1 : 1;
    葉(ctx, 筆, x, y, 27 + (k % 4) * 2.2, 7 + (k % 3), a + Math.PI / 2 + 內外 * .72,
      .62 + (k % 5) * .045);
    if (k % 3 === 0) 筆.圓(x, y, 2.7 + (k % 4) * .25, .7, .52);
  }
}

function 身光(ctx, 筆) {
  // 外緣約 x440–1360、y250–1880；內外雙線夾連珠。足前靜域斷開，
  // 兩端落於蓮臺之後；y1870 以下殘弧由後落飾層淨去，毋穿主尊。
  const 描環 = (rx, ry, 寬, alpha) => {
    let 段 = [];
    for (let k = 0; k <= 180; k++) {
      const a = -Math.PI / 2 + k * Math.PI * 2 / 180;
      const 點 = 橢輪點(900, 1065, rx, ry, a);
      const 入靜域 = 點[0] >= 654 && 點[0] <= 1146 && 點[1] >= 434 && 點[1] <= 1876;
      if (入靜域) {
        if (段.length > 1) 筆.線(段, 寬, false, alpha);
        段 = [];
      } else 段.push(點);
    }
    if (段.length > 1) 筆.線(段, 寬, false, alpha);
  };
  描環(460, 815, 4.8, .92);
  描環(426, 780, 1.75, .68);
  for (let k = 0; k < 76; k++) {
    const a = -Math.PI / 2 + k * Math.PI * 2 / 76 + Math.sin(k * 1.7) * .004;
    const [x, y] = 橢輪點(900, 1065, 443 + Math.sin(k * .91) * 1.7,
      797.5 + Math.cos(k * 1.13) * 2.4, a);
    if (x >= 652 && x <= 1148 && y >= 432 && y <= 1878) continue;
    const r = 4.4 + (k % 5) * .38;
    筆.橢(x, y, r, r * (.86 + (k % 3) * .04), 1.02, a, .7 + (k % 4) * .045);
    if (k % 4 === 1) 筆.圓(x, y, 1.1, .5, .48);
  }
}

function 鈴鐸(ctx, 筆, x, y, s, 偏 = 0) {
  筆.貝([x, y], [x - 7 * s, y + 9 * s], [x - 9 * s, y + 15 * s], [x - 12 * s, y + 21 * s], 1.25, .76);
  筆.貝([x, y], [x + 7 * s, y + 9 * s], [x + 9 * s, y + 15 * s], [x + 12 * s, y + 21 * s], 1.05, .7);
  筆.二次([x - 12 * s, y + 21 * s], [x + 偏, y + 26 * s], [x + 12 * s, y + 21 * s], 1.2, .76);
  筆.線([[x + 偏, y + 25 * s], [x + 偏, y + 33 * s]], .78, false, .66);
  筆.圓(x + 偏, y + 36 * s, 2.7 * s, .72, .68);
}

function 寶蓋(ctx) {
  const 筆 = 造線筆(ctx, { 墨, 骨: 4.6, 衣: 2.35, 鬚: 1.05 });
  // 盛唐淨土變華蓋通式，候典；只取構造，不補未核圖像義。
  // 蓋頂寶珠與蓋心同在中軸。
  筆.線([[900, 110], [900, 144]], 2.7, false, .9);
  筆.貝([900, 110], [875, 98], [878, 76], [900, 59], 2.6, .9);
  筆.貝([900, 59], [923, 77], [925, 98], [900, 110], 2.2, .84);
  筆.橢(900, 151, 30, 12, 2.5, 0, .88);
  筆.圓(900, 134, 8, 1.3, .72);
  // 上拱、傘骨、下垂蓋緣。
  筆.貝([520, 265], [620, 154], [1180, 154], [1280, 265], 4.8, .94);
  筆.貝([548, 276], [690, 215], [1110, 215], [1252, 276], 1.65, .7);
  for (const [x, c] of [[570, 720], [660, 780], [760, 835], [1040, 965], [1140, 1020], [1230, 1080]])
    筆.貝([900, 160], [(900 + x) / 2, 190], [c, 232], [x, 271], 1.02, .54);
  筆.二次([520, 265], [900, 354], [1280, 265], 4.2, .92);
  筆.二次([545, 278], [900, 337], [1255, 278], 1.35, .7);
  // 蓋緣連珠，各珠略異；下接垂瓔、流蘇與鈴鐸。
  for (let k = 0; k < 25; k++) {
    const t = k / 24, x = 548 + t * 704;
    const y = 278 + Math.sin(Math.PI * t) * 58;
    const r = 4.5 + (k % 4) * .42;
    筆.橢(x, y, r, r * (.88 + (k % 3) * .035), 1.0, (t - .5) * .18, .74);
  }
  const 垂譜 = [
    [555, 281, 32, .84, -1], [615, 296, 45, .9, 1], [680, 311, 30, .8, 0],
    [752, 326, 42, .88, -1], [828, 334, 29, .78, 1], [900, 337, 44, .94, 0],
    [972, 334, 31, .8, -1], [1048, 326, 43, .88, 1], [1120, 311, 29, .78, 0],
    [1185, 296, 46, .9, -1], [1245, 281, 33, .84, 1],
  ];
  for (let k = 0; k < 垂譜.length; k++) {
    const [x, y, len, a, 偏] = 垂譜[k];
    筆.貝([x, y + 5], [x - 5 - 偏 * 2, y + len * .35], [x + 5 + 偏 * 2, y + len * .72], [x + 偏 * 3, y + len], 1.1, a);
    筆.圓(x + 偏 * 3, y + len + 5, 3.2 + (k % 3) * .4, .85, a * .82);
    for (const dx of [-5, 0, 5]) 筆.貝([x + 偏 * 3, y + len + 8], [x + dx, y + len + 16], [x + dx, y + len + 23], [x + dx + 偏, y + len + 30 + Math.abs(dx) * .35], .62, a * .7);
    if (k % 2 === 1) 鈴鐸(ctx, 筆, x + 偏 * 3, y + len + 9, .65 + (k % 3) * .05, 偏);
  }
}

function 仰瓣(筆, x, y, 半, 高, 偏) {
  const 尖 = [x + 偏, y - 高];
  筆.貝([x - 半, y], [x - 半 * .72, y - 高 * .52], [尖[0] - 半 * .25, 尖[1] + 高 * .12], 尖, 2.35, .88);
  筆.貝(尖, [尖[0] + 半 * .24, 尖[1] + 高 * .1], [x + 半 * .74, y - 高 * .5], [x + 半, y], 1.9, .8);
  筆.貝([x, y - 2], [x - 半 * .08, y - 高 * .4], [尖[0] - 半 * .05, 尖[1] + 高 * .22], 尖, .78, .5);
}

function 覆瓣(筆, x, y, 半, 深, 偏) {
  const 尖 = [x + 偏, y + 深];
  筆.貝([x - 半, y], [x - 半 * .68, y + 深 * .48], [尖[0] - 半 * .24, 尖[1] - 深 * .12], 尖, 2.15, .84);
  筆.貝(尖, [尖[0] + 半 * .22, 尖[1] - 深 * .12], [x + 半 * .7, y + 深 * .44], [x + 半, y], 1.7, .76);
  筆.貝([x, y + 2], [x + 半 * .06, y + 深 * .36], [尖[0] + 半 * .04, 尖[1] - 深 * .2], 尖, .72, .48);
}

function 蓮臺素階(ctx) {
  const 筆 = 造線筆(ctx, { 墨, 骨: 4.6, 衣: 2.4, 鬚: 1.05 });
  const 蓮廓 = p => {
    p.moveTo(575, 1852); p.quadraticCurveTo(900, 1838, 1225, 1852);
    p.quadraticCurveTo(1260, 1938, 1270, 1972); p.quadraticCurveTo(1220, 2034, 900, 2044);
    p.quadraticCurveTo(580, 2034, 530, 1972); p.quadraticCurveTo(540, 1938, 575, 1852); p.closePath();
  };
  const 階廓 = p => { p.rect(450, 2038, 900, 128); };
  // 飾層後落；蓮臺、素階各先 destination-out 淨域，再自描。
  淨輪廓(ctx, 蓮廓);
  淨輪廓(ctx, 階廓);
  筆.貝([575, 1860], [750, 1849], [1050, 1849], [1225, 1860], 5.1, .96);
  筆.貝([550, 1932], [720, 1918], [1080, 1918], [1250, 1932], 3.2, .88);
  const 仰譜 = [[610,1931,58,70,-7],[714,1926,62,64,5],[822,1923,64,67,-4],
    [932,1923,64,66,6],[1042,1926,62,63,-5],[1148,1931,59,69,6],[1225,1934,39,55,-3]];
  for (const 瓣 of 仰譜) 仰瓣(筆, ...瓣);
  筆.貝([535, 1940], [720, 1951], [1080, 1951], [1265, 1940], 3.25, .9);
  筆.貝([528, 1971], [720, 1983], [1080, 1983], [1272, 1971], 3.35, .9);
  for (let k = 0; k < 29; k++) {
    const x = 555 + k * 24.7 + Math.sin(k * 1.31) * 1.8;
    const y = 1957 + Math.sin((x - 555) / 690 * Math.PI) * 5 + Math.cos(k * .77);
    const r = 4.8 + (k % 4) * .42;
    筆.橢(x, y, r, r * (.87 + (k % 3) * .035), 1.15, (k % 5 - 2) * .03, .78);
  }
  const 覆譜 = [[575,1973,45,45,5],[660,1977,55,53,-5],[758,1980,59,57,6],
    [862,1982,61,59,-6],[968,1982,61,58,5],[1074,1980,59,56,-5],[1173,1977,55,52,5],[1250,1973,42,43,-4]];
  for (const 瓣 of 覆譜) 覆瓣(筆, ...瓣);
  筆.貝([530,1972], [610,2025], [760,2040], [900,2043], 4.1, .92);
  筆.貝([900,2043], [1040,2040], [1190,2025], [1270,1972], 4.1, .92);
  // 素階一層，階面留白，不設銘文帶。
  筆.線([[470,2042],[1330,2042],[1330,2158],[470,2158]], 4.6, true, .95);
  筆.線([[450,2058],[1350,2058]], 2.0, false, .7);
  筆.線([[450,2160],[1350,2160]], 4.0, false, .9);
}

export function 景(ctx, 助) {
  驗助(助);
  ctx.save();
  try {
    const 筆 = 造線筆(ctx, { 墨, 骨: 4.6, 衣: 2.35, 鬚: 1.05 });
    身光(ctx, 筆);
    唐草頭光(ctx, 筆);
    寶蓋(ctx);
  } finally { ctx.restore(); }
}

export function 飾(ctx, 助) {
  驗助(助);
  ctx.save();
  try { 蓮臺素階(ctx); } finally { ctx.restore(); }
}
