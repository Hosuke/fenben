// 尼瓦爾-金剛薩埵金密 · 彩層二稿（主坊親筆・料法・破色重運）
// 先生評初稿：「用色粗糙，一片一片的，沒有筆觸細膩……色彩、層次、對比、質感」
// ——中的。補課三理（docs/考據/研色-總綱.md 補課節）：
//   色無平面（達文西 sfumato：形生於明暗煙化，邊有軟硬）；
//   破色（莫內：一片色是數十鄰色之振動，影有色相非黑）；
//   同時對比（Chevreul：色活於關係——鄰補相激，金乃愈亮）。
// 架構改「先制其面」：離屏畫布上如真畫正序作畫（底塗→值場→破色筆觸→
// 分域→罩染→邊沉→顆粒），成面後一印 destination-over 居線下；
// 域以真 clip 剪裁（初稿域溢之疵並除）。線骨恆上如故。
// 色譜仍依 研色-尼瓦爾（十色雙源）為基，破色諸變不離其礦：
// 朱地銀朱之變（洋紅橙朱赭紫影）、石青之變（綠青紫青）、皆同礦異研之義。
// 律：色深護金（場色 luma≤120）；籽定雙鍵hash，不用亂數。
// 面制一次而快取（首幀約一二秒，後幀 drawImage 而已）；彩為料面可點陣，
// 線為骨恆向量——放大線銳而彩如絹面微暈，此正料之質。

export const 彩律 = Object.freeze({ 場明度限: 120 });

// ── 色譜（基色皆研色卷雙源；變色同礦異研）──────────────────────────────
export const 色譜 = {
  朱地: [150, 58, 45],
  朱變: [[138, 42, 58], [168, 74, 38], [128, 64, 40], [104, 40, 60], [180, 92, 52]], // 洋紅・橙朱・赭・紫影・淡金紅
  胭脂: [120, 32, 52],
  胭沉: [90, 26, 44],
  靛玄: [40, 48, 62],
  靛變: [[34, 42, 66], [52, 56, 78], [46, 44, 70], [30, 38, 56]],
  石青: [58, 80, 118],
  青變: [[46, 92, 130], [66, 64, 124], [40, 60, 100], [84, 104, 150]],
  石綠: [62, 78, 62],
  綠變: [[52, 88, 70], [76, 90, 54], [44, 64, 52], [88, 104, 72]],
  暖罩: [160, 100, 55],
  冷罩: [60, 40, 90],
  暖襯: [120, 80, 60],   // 藍下之暖底——文藝復興底層畫之義
  深屑: [60, 20, 16],
  亮屑: [235, 205, 160], // 豁免色深律——點也非場
};
Object.freeze(色譜);                                  // 面快取生後色不可改——凍之（審所正）
for (const v of Object.values(色譜)) Object.freeze(v);

const 斷 = (c, m) => { console.assert(c, m); if (!c) throw new Error(m); };
const 明度 = ([r, g, b]) => r * 0.299 + g * 0.587 + b * 0.114;
const 染 = ([r, g, b], α) => `rgba(${r},${g},${b},${α})`;
const 籽 = (gx, gy) => {
  let t = (Math.imul(gx, 73856093) ^ Math.imul(gy, 19349663)) >>> 0;
  t = Math.imul(t ^ (t >>> 16), 2246822519) >>> 0;
  t = Math.imul(t ^ (t >>> 13), 3266489917) >>> 0;
  return ((t ^ (t >>> 16)) >>> 0) / 4294967296;
};

const W = 1800, H = 2400, 倍 = 1.9;                   // 面制於1.9×（3420×4560＝15.6M px，
                                                      // 居舊Safari 16.77M畫布上限之內——審所正）
const 心 = { 左: 147, 上: 147, 右: 1653, 下: 2253 };

let 面快取 = null;
let 觸數 = 0;

// 一觸：軟緣橢筆——破色之筆觸單元（正序 source-over，如真落一筆）
function 觸(q, cx, cy, 長, 寬, 向, 色, α) {
  q.save();
  q.translate(cx, cy); q.rotate(向);
  q.fillStyle = 染(色, α);
  q.beginPath(); q.ellipse(0, 0, 長 / 2, 寬 / 2, 0, 0, 2 * Math.PI); q.fill();
  q.restore();
  觸數++;
}

// 破色一域：底塗＋筆觸群（值隨場呼吸——中明緣暗，向與長寬皆籽定）
function 破色域(q, 界, 基色, 變列, { 步 = 11, 底α = 0.9, 觸α = 0.13, 值心 = null, 剪 = null } = {}) {
  q.save();
  if (剪) { q.beginPath(); 剪(q); q.clip(); }
  else { q.beginPath(); q.rect(界[0], 界[1], 界[2], 界[3]); q.clip(); }
  q.fillStyle = 染(基色, 底α);
  q.fillRect(界[0] - 4, 界[1] - 4, 界[2] + 8, 界[3] + 8);
  const 諸色 = [基色, ...變列];
  for (let gy = 界[1]; gy < 界[1] + 界[3]; gy += 步) {
    for (let gx = 界[0]; gx < 界[0] + 界[2]; gx += 步) {
      const h = 籽(gx, gy);
      if (h < 0.12) continue;                               // 疏處留底——筆觸有聚散
      const 色 = 諸色[Math.floor(籽(gx + 3, gy) * 諸色.length)];
      let α = 觸α * (0.6 + 籽(gx, gy + 3) * 0.8);
      if (值心) {                                           // 值場：近心愈亮（亮變愈顯），遠心愈沉（暗變愈顯）
        const d = Math.hypot(gx - 值心[0], gy - 值心[1]) / 值心[2];
        const 明變 = 明度(色) > 明度(基色);
        α *= 明變 ? Math.max(0.25, 1.2 - d) : Math.min(1.6, 0.5 + d);
      }
      觸(q, gx + 籽(gx + 1, gy) * 步, gy + 籽(gx, gy + 1) * 步,
        10 + 籽(gx + 2, gy) * 14, 4 + 籽(gx, gy + 2) * 6,
        (籽(gx + 4, gy) - 0.5) * 1.1, 色, Math.min(α, 0.3));
    }
  }
  q.restore();
}

function 制面() {
  for (const 名 of ['朱地', '胭脂', '胭沉', '靛玄', '石青', '石綠', '暖罩', '冷罩', '暖襯', '深屑'])
    斷(明度(色譜[名]) <= 彩律.場明度限, `色奪金！${名} luma${明度(色譜[名]).toFixed(0)}`);
  for (const 列 of [色譜.朱變, 色譜.靛變, 色譜.青變, 色譜.綠變])
    for (const 色 of 列) 斷(明度(色) <= 彩律.場明度限, `變色奪金！luma${明度(色).toFixed(0)}`);

  const c = document.createElement('canvas');
  c.width = Math.round(W * 倍); c.height = Math.round(H * 倍);
  const q = c.getContext('2d');
  斷(!!q, '離屏畫布不得其境（記憶或上限）——料面制不得');
  q.scale(倍, 倍);
  觸數 = 0;

  // ── 一・朱地破色（底塗＋二萬餘觸——莫內之訓：一片紅是數十紅之振動）───
  破色域(q, [心.左, 心.上, 心.右 - 心.左, 心.下 - 心.上], 色譜.朱地, 色譜.朱變,
    { 步: 11, 底α: 0.88, 觸α: 0.12, 值心: [900, 1150, 1400] });

  // ── 二・分域（正序覆於朱地上，各域真 clip——域溢之疵除）──────────────
  // 上欄五龕：石青石綠相間；藍下先施暖襯（底層畫之義），龕心微光（sfumato）
  for (let k = 0; k < 5; k++) {
    const l = 150 + 300 * k, 界 = [l + 13, 262, 274, 231];
    q.save(); q.beginPath(); q.rect(...界); q.clip();
    q.fillStyle = 染(色譜.暖襯, 0.25); q.fillRect(...界);
    q.restore();
    破色域(q, 界, k % 2 ? 色譜.石綠 : 色譜.石青, k % 2 ? 色譜.綠變 : 色譜.青變,
      { 步: 9, 底α: 0.72, 觸α: 0.15, 值心: [l + 150, 370, 260] });
    const g = q.createRadialGradient(l + 150, 372, 0, l + 150, 372, 108);   // 龕心微光——尊後之氣
    g.addColorStop(0, 染(k % 2 ? 色譜.綠變[3] : 色譜.青變[3], 0.22));
    g.addColorStop(1, 染(k % 2 ? 色譜.綠變[3] : 色譜.青變[3], 0));
    q.save(); q.beginPath(); q.rect(...界); q.clip();
    q.fillStyle = g; q.fillRect(...界); q.restore();
  }

  // 中尊域：靛玄破色，胸前暖襯自藍下透——冷中藏暖，金線乃活
  const 尊域 = (p) => {
    p.ellipse(900, 925, 132, 118, 0, 0, 2 * Math.PI);
    p.moveTo(720, 1015); p.bezierCurveTo(630, 1060, 610, 1240, 632, 1405);
    p.bezierCurveTo(560, 1455, 500, 1540, 480, 1650); p.bezierCurveTo(630, 1718, 1170, 1718, 1320, 1650);
    p.bezierCurveTo(1300, 1540, 1240, 1455, 1168, 1405); p.bezierCurveTo(1190, 1240, 1170, 1060, 1080, 1015);
    p.bezierCurveTo(1025, 985, 775, 985, 720, 1015); p.closePath();
  };
  q.save(); q.beginPath(); 尊域(q); q.clip();
  q.fillStyle = 染(色譜.暖襯, 0.3); q.fillRect(480, 800, 840, 920);
  q.restore();
  破色域(q, [480, 800, 840, 920], 色譜.靛玄, 色譜.靛變,
    { 步: 9, 底α: 0.78, 觸α: 0.14, 值心: [900, 1150, 480], 剪: 尊域 });

  // 蓮座帶（石綠破色，橫觸如瓣行）・須彌台七格（青胭相間）・下欄（靛）
  破色域(q, [324, 1708, 1152, 132], 色譜.石綠, 色譜.綠變, { 步: 10, 底α: 0.5, 觸α: 0.12 });
  {
    const SL = 315, cw = (1485 - 315) / 7;
    for (let k = 0; k < 7; k++)
      破色域(q, [SL + cw * k + 2, 1876, cw - 4, 162], k % 2 ? 色譜.胭脂 : 色譜.石青,
        k % 2 ? [色譜.胭沉, 色譜.朱變[0], 色譜.朱變[3]] : 色譜.青變,
        { 步: 9, 底α: 0.6, 觸α: 0.13, 值心: [SL + cw * k + cw / 2, 1957, 150] });
  }
  破色域(q, [170, 2082, 1460, 180], 色譜.靛玄, 色譜.靛變, { 步: 10, 底α: 0.68, 觸α: 0.12 });

  // 雙肩青蓮盤（石青，圓域真clip）
  for (const cx of [565, 1800 - 565]) {
    破色域(q, [cx - 54, 966, 108, 108], 色譜.石青, 色譜.青變,
      { 步: 7, 底α: 0.55, 觸α: 0.16, 剪: (p) => p.arc(cx, 1020, 54, 0, 2 * Math.PI) });
  }

  // ── 三・罩染二層（文藝復興 glazing：透明層疊出深——上左暖、下右冷）───
  q.save(); q.beginPath(); q.rect(心.左, 心.上, 心.右 - 心.左, 心.下 - 心.上); q.clip();
  let g = q.createLinearGradient(心.左, 心.上, 心.右, 心.下);
  g.addColorStop(0, 染(色譜.暖罩, 0.07)); g.addColorStop(0.55, 染(色譜.暖罩, 0));
  q.fillStyle = g; q.fillRect(心.左, 心.上, 心.右 - 心.左, 心.下 - 心.上);
  g = q.createLinearGradient(心.右, 心.下, 心.左, 心.上);
  g.addColorStop(0, 染(色譜.冷罩, 0.08)); g.addColorStop(0.5, 染(色譜.冷罩, 0));
  q.fillStyle = g; q.fillRect(心.左, 心.上, 心.右 - 心.左, 心.下 - 心.上);

  // ── 四・邊沉（胭脂之影有色相——偏紫紅，非變黑；角尤沉）───────────────
  const 寬 = 62;
  for (const [x0, y0, x1, y1, rx, ry, rw, rh] of [
    [心.左, 0, 心.左 + 寬, 0, 心.左, 心.上, 寬, 心.下 - 心.上],
    [心.右, 0, 心.右 - 寬, 0, 心.右 - 寬, 心.上, 寬, 心.下 - 心.上],
    [0, 心.上, 0, 心.上 + 寬, 心.左, 心.上, 心.右 - 心.左, 寬],
    [0, 心.下, 0, 心.下 - 寬, 心.左, 心.下 - 寬, 心.右 - 心.左, 寬],
  ]) {
    const eg = q.createLinearGradient(x0, y0, x1, y1);
    eg.addColorStop(0, 染(色譜.胭沉, 0.34)); eg.addColorStop(1, 染(色譜.胭沉, 0));
    q.fillStyle = eg; q.fillRect(rx, ry, rw, rh);
  }
  for (const [cx, cy] of [[心.左, 心.上], [心.右, 心.上], [心.左, 心.下], [心.右, 心.下]]) {
    const cg = q.createRadialGradient(cx, cy, 0, cx, cy, 170);
    cg.addColorStop(0, 染(色譜.冷罩, 0.16)); cg.addColorStop(1, 染(色譜.冷罩, 0));
    q.fillStyle = cg; q.beginPath(); q.arc(cx, cy, 170, 0, 2 * Math.PI); q.fill();
  }
  q.restore();

  // ── 五・顆粒（礦末居面之最上——大小疏密二態，簇而非勻撒）─────────────
  let 屑數 = 0;
  for (let gy = 心.上 + 3; gy < 心.下 - 3; gy += 6) {
    for (let gx = 心.左 + 3; gx < 心.右 - 3; gx += 6) {
      const h = 籽(gx, gy);
      const 簇 = 籽(Math.floor(gx / 48), Math.floor(gy / 48)) > 0.55 ? 1.8 : 0.6; // 簇域密、疏域稀
      if (h > 1 - 0.035 * 簇) q.fillStyle = 染(色譜.深屑, 0.11);
      else if (h < 0.02 * 簇) q.fillStyle = 染(色譜.亮屑, 0.09);
      else continue;
      const s = 0.9 + 籽(gx + 5, gy) * 1.1;
      q.fillRect(gx + 籽(gx + 1, gy) * 4, gy + 籽(gx, gy + 1) * 4, s, s);
      屑數++;
    }
  }

  斷(觸數 > 20000 && 觸數 < 90000, `筆觸失度：${觸數}`);
  斷(屑數 > 2000 && 屑數 < 15000, `礦末失度：${屑數}`);
  console.log(`金剛薩埵金密二彩✓ 破色${觸數}觸・礦末${屑數}粒・罩染二層・邊沉有色相；場明度≤${彩律.場明度限}護金`);
  return c;
}

export function 彩(x) {
  面快取 ??= 制面();
  x.save();
  x.globalAlpha = 1;
  x.globalCompositeOperation = 'destination-over';   // 面一印居線下——線骨恆上
  x.drawImage(面快取, 0, 0, W, H);
  x.restore();
}
