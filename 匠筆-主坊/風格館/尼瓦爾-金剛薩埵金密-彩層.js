// 尼瓦爾-金剛薩埵金密 · 彩層（主坊親筆・料法首試——設色程第四筆）
// 光料二辨（docs/考據/研色-總綱.md）：此層用**料法**，非杳眇之光法——
// 色是顏料非光：底色統攝（朱地全心）、分域填彩（龕帶格各有其色，勾線為界）、
// 顆粒（籽定散點擬礦末）、邊沉（胭脂罩染擬積料）。
// 色譜依 yanse-liushi 調研（尼瓦爾十色全數雙源過堂，Sacred Visions/
// Bruce-Gardner〈Realizations〉Pigments and Paint 節＋本地參攷實測）：
// 朱地銀朱[150,58,45]實測均值・靛玄[40,48,62]・石青[58,80,118]敷靛底・
// 石綠[62,78,62]・胭脂紫膠[120,32,52]罩染。詳 docs/考據/研色-尼瓦爾.md。
// 律：**色深護金**——場色明度(luma)≤120，金線(#d8b36a, luma≈181)恆浮其上，
// 誦戒斷之；屑者點也非場，亮屑豁免（礦末之閃，α至薄）。
// 域形錄自 金剛薩埵.html（機轉頁無模組出口可契約——改本重轉時此層之域須併校）。
// 域皆近似（矩・橢・錄形之path），非以線稿真界剪裁——試色之法；域緣與勾線
// 之際或有微溢，候印可後議以真域 clip 精修（審所記，納之）。
// destination-over 逆序之法：先落者居前——屑最前，朱地最末。

export const 色譜 = {
  朱地: [150, 58, 45],    // 銀朱（辰砂）——龕外全場統攝之紅地
  胭脂: [120, 32, 52],    // 紫膠紅——邊沉罩染
  靛玄: [40, 48, 62],     // 植物靛藍——中尊域・下欄・龕之底層
  石青: [58, 80, 118],    // 藍銅礦——五龕相間・雙肩青蓮（敷於靛底上）
  石綠: [62, 78, 62],     // 孔雀石——五龕相間・蓮座帶
  深屑: [60, 20, 16],     // 礦末暗粒
  亮屑: [235, 205, 160],  // 礦末閃粒（豁免於色深律——點也，非場）
};
export const 彩律 = { 場明度限: 120 };

const 斷 = (c, m) => { console.assert(c, m); if (!c) throw new Error(m); };
const 明度 = ([r, g, b]) => r * 0.299 + g * 0.587 + b * 0.114;
const 染 = ([r, g, b], α) => `rgba(${r},${g},${b},${α})`;
// 籽定之散——不用亂數（誦戒之律）。雙鍵空間hash（單鍵Knuth乘對步進之列有偏，
// 曾致礦末45k暴出——親驗勻後乃用：87500格中屑約5.4%）。
const 籽 = (gx, gy) => {
  let t = (Math.imul(gx, 73856093) ^ Math.imul(gy, 19349663)) >>> 0;
  t = Math.imul(t ^ (t >>> 16), 2246822519) >>> 0;
  t = Math.imul(t ^ (t >>> 13), 3266489917) >>> 0;
  return ((t ^ (t >>> 16)) >>> 0) / 4294967296;
};

export function 彩(x) {
  const W = 1800, H = 2400;                            // 金剛薩埵.html 之布
  const 心 = { 左: 147, 上: 147, 右: 1653, 下: 2253 }; // 畫心（內框145線之內）

  // ── 誦戒：色深護金（場色皆驗；屑不入場之列）─────────────────────────
  for (const 名 of ['朱地', '胭脂', '靛玄', '石青', '石綠', '深屑'])
    斷(明度(色譜[名]) <= 彩律.場明度限, `色奪金！${名} luma${明度(色譜[名]).toFixed(0)}逾${彩律.場明度限}`);

  let 場數 = 0, 屑數 = 0;

  x.save();
  x.globalAlpha = 1;
  x.globalCompositeOperation = 'destination-over';     // 線骨恆上，結構所保

  // ── 一・顆粒（最前——直承金線之下，礦末之質）─────────────────────────
  for (let gy = 心.上 + 3; gy < 心.下 - 3; gy += 6) {
    for (let gx = 心.左 + 3; gx < 心.右 - 3; gx += 6) {
      const h = 籽(gx, gy);
      if (h > 0.965) x.fillStyle = 染(色譜.深屑, 0.10);
      else if (h < 0.02) x.fillStyle = 染(色譜.亮屑, 0.07);
      else continue;
      x.fillRect(gx + 籽(gx + 1, gy) * 4, gy + 籽(gx, gy + 1) * 4, 1.3, 1.3);
      屑數++;
    }
  }
  場數++;
  斷(屑數 > 3000 && 屑數 < 12000, `礦末失度：${屑數}`);

  // ── 二・雙肩青蓮盤（石青，勾線為界之圓域）───────────────────────────
  for (const cx of [565, 1800 - 565]) {
    x.fillStyle = 染(色譜.石青, 0.6);
    x.beginPath(); x.arc(cx, 1020, 54, 0, 2 * Math.PI); x.fill();
    場數++;
  }

  // ── 三・上欄五龕（石青石綠相間，龕下靛底——石青敷於靛上，調研有據）───
  for (let k = 0; k < 5; k++) {
    const l = 150 + 300 * k;
    x.fillStyle = 染(k % 2 ? 色譜.石綠 : 色譜.石青, 0.75);
    x.fillRect(l + 13, 262, 274, 231);
    場數++;
  }
  x.fillStyle = 染(色譜.靛玄, 0.8);                    // 靛底（居五龕之後）
  x.fillRect(152, 258, 1496, 235);
  場數++;

  // ── 四・中尊域（靛玄——尊浮深靛之上；域形錄自蔽域）───────────────────
  {
    x.fillStyle = 染(色譜.靛玄, 0.85);
    x.beginPath(); x.ellipse(900, 925, 132, 118, 0, 0, 2 * Math.PI); x.fill();
    x.beginPath();
    x.moveTo(720, 1015); x.bezierCurveTo(630, 1060, 610, 1240, 632, 1405);
    x.bezierCurveTo(560, 1455, 500, 1540, 480, 1650); x.bezierCurveTo(630, 1718, 1170, 1718, 1320, 1650);
    x.bezierCurveTo(1300, 1540, 1240, 1455, 1168, 1405); x.bezierCurveTo(1190, 1240, 1170, 1060, 1080, 1015);
    x.bezierCurveTo(1025, 985, 775, 985, 720, 1015); x.closePath(); x.fill();
    場數++;
  }

  // ── 五・蓮座帶（石綠）───────────────────────────────────────────────
  x.fillStyle = 染(色譜.石綠, 0.5);
  x.fillRect(324, 1708, 1152, 132);
  場數++;

  // ── 六・須彌台七格（石青胭脂相間）───────────────────────────────────
  {
    const SL = 315, SR = 1485, cw = (SR - SL) / 7;
    for (let k = 0; k < 7; k++) {
      x.fillStyle = 染(k % 2 ? 色譜.胭脂 : 色譜.石青, 0.55);
      x.fillRect(SL + cw * k + 2, 1876, cw - 4, 162);
      場數++;
    }
  }

  // ── 七・下欄供養帶（靛玄）───────────────────────────────────────────
  x.fillStyle = 染(色譜.靛玄, 0.7);
  x.fillRect(170, 2082, 1460, 180);
  場數++;

  // ── 八・邊沉（胭脂罩染四緣——積料之意）───────────────────────────────
  {
    const 寬 = 46, α = 0.3;
    const 條 = [
      [心.左, 心.上, 寬, 心.下 - 心.上, 心.左, 0, 心.左 + 寬, 0],
      [心.右 - 寬, 心.上, 寬, 心.下 - 心.上, 心.右, 0, 心.右 - 寬, 0],
      [心.左, 心.上, 心.右 - 心.左, 寬, 0, 心.上, 0, 心.上 + 寬],
      [心.左, 心.下 - 寬, 心.右 - 心.左, 寬, 0, 心.下, 0, 心.下 - 寬],
    ];
    for (const [rx, ry, rw, rh, gx0, gy0, gx1, gy1] of 條) {
      const g = x.createLinearGradient(gx0, gy0, gx1, gy1);
      g.addColorStop(0, 染(色譜.胭脂, α));
      g.addColorStop(1, 染(色譜.胭脂, 0));
      x.fillStyle = g;
      x.fillRect(rx, ry, rw, rh);
      場數++;
    }
  }

  // ── 九・朱地（最末最深——銀朱統攝全心，料法之體）─────────────────────
  x.fillStyle = 染(色譜.朱地, 0.85);
  x.fillRect(心.左, 心.上, 心.右 - 心.左, 心.下 - 心.上);
  場數++;

  x.restore();

  // ── 誦戒：場數之驗（屑1蓮2龕5靛1尊1座1台7欄1沉4地1＝二十四場）────────
  斷(場數 === 24, `色場非二十四：${場數}`);
  console.log(`金剛薩埵金密初彩✓ 二十四場，礦末${屑數}粒；場明度≤${彩律.場明度限}護金`);
}
