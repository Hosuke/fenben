    for (const s of [-1, 1]) for (let k = 0; k < 3; k++) {                          // 六環（左右各三，掛於首輪）
      const ex = 杖x + s * (14 + k * 2), ey = 杖頂 + 46 + k * 16;
      弧(ex, ey, 7.5, 0, 2 * Math.PI, 1.0, false, 0.9);
      杖環數++;
    }
    // 拄杖拳（右手垂握——チ鏡向：垂手握杖拇在橈側向軸）
