// 光環七重之簿（首環88，級數1.42逐環取整——彩層共契約，勿抄錄）
export const 環r簿 = (() => { const 簿 = []; let r = 88; for (let j = 0; j < 7; j++) { 簿.push(r); r = Math.round(r * 1.42); } return 簿; })();
// 六空輪之位：[環序, 角]（角自珠心量）
