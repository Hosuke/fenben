function 足(筆, cx0, s, 底y) {
  const { 線, 貝, 弧 } = 筆;
  const 內 = -s;
  貝([cx0 + s * 20, 底y - 66], [cx0 + s * 26, 底y - 40], [cx0 + s * 34, 底y - 16], [cx0 + s * 30, 底y - 2], 2.0); // 外緣踝→踵
  線([[cx0 + s * 30, 底y - 2], [cx0 + 內 * 52, 底y - 2]], 2.0);                    // 掌底貼地
  貝([cx0 + s * 4, 底y - 62], [cx0 + 內 * 6, 底y - 40], [cx0 + 內 * 24, 底y - 20], [cx0 + 內 * 40, 底y - 12], 2.0); // 內緣跗面
  弧(cx0 + 內 * 48, 底y - 8, 6.4, Math.PI * 0.55, Math.PI * 2.45, 1.8);            // 大趾
  弧(cx0 + 內 * 48, 底y - 10, 3.0, Math.PI * 1.1, Math.PI * 1.9, 0.95);            // 大趾甲
  for (let k = 0; k < 4; k++) {
    const tx = cx0 + 內 * (34 - k * 12);
    弧(tx, 底y - 5, 4.6 - k * 0.45, Math.PI * 0.9, Math.PI * 2.2, 1.4);            // 四趾遞短
    貝([tx - 4, 底y - 10], [tx - 1, 底y - 12], [tx + 1, 底y - 12], [tx + 4, 底y - 10], 0.85, 0.7); // 趾根紋
  }
  貝([cx0 + s * 8, 底y - 56], [cx0, 底y - 42], [cx0 + 內 * 8, 底y - 26], [cx0 + 內 * 16, 底y - 16], 0.95, 0.7); // 跗骨稜
  弧(cx0 + s * 14, 底y - 58, 4.2, Math.PI * 1.1, Math.PI * 0.5, 1.1);              // 外踝珠
  弧(cx0 + 內 * 10, 底y - 54, 3.6, Math.PI * 1.5, Math.PI * 0.9, 1.0);             // 內踝珠
}

