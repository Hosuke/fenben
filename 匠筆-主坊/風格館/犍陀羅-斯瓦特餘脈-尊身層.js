// ── 修（合成終審之筆：蓮臺頂線過足內者淨之，足重描）────────────────────
export async function 修(x, 助) {
  const Y足底 = 尊位.頂y + 120 * 尊位.指;
  const 筆 = 具(x);
  const { 蔽 } = 筆;
  for (const [cx0, s] of [[830, -1], [970, 1]]) {
    const 內 = -s;
    蔽(() => {
      x.moveTo(cx0 + s * 26, Y足底 - 60);
      x.quadraticCurveTo(cx0 + s * 36, Y足底 - 20, cx0 + s * 28, Y足底 - 3);
      x.lineTo(cx0 + 內 * 52, Y足底 - 3);
      x.quadraticCurveTo(cx0 + 內 * 56, Y足底 - 10, cx0 + 內 * 44, Y足底 - 16);
      x.quadraticCurveTo(cx0 + 內 * 20, Y足底 - 28, cx0 + s * 2, Y足底 - 58);
      x.closePath();
    });
    足(筆, cx0, s, Y足底);
  }
}
