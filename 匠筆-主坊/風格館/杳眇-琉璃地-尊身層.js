const 墨 = '#d8b36a';

function 具(x) {
  const 線 = (pts, w = 1.1, α = 1) => { x.save(); x.globalAlpha = α; x.strokeStyle = 墨; x.lineWidth = w; x.lineCap = 'round'; x.lineJoin = 'round'; x.beginPath(); x.moveTo(pts[0][0], pts[0][1]); for (let i = 1; i < pts.length; i++) x.lineTo(pts[i][0], pts[i][1]); x.stroke(); x.restore(); };
  const 貝 = (p0, c1, c2, p1, w = 1.1, α = 1) => { x.save(); x.globalAlpha = α; x.strokeStyle = 墨; x.lineWidth = w; x.lineCap = 'round'; x.beginPath(); x.moveTo(p0[0], p0[1]); x.bezierCurveTo(c1[0], c1[1], c2[0], c2[1], p1[0], p1[1]); x.stroke(); x.restore(); };
  const 弧 = (cx, cy, r, a0, a1, w = 1.1, ccw = false, α = 1) => { x.save(); x.globalAlpha = α; x.strokeStyle = 墨; x.lineWidth = w; x.lineCap = 'round'; x.beginPath(); x.arc(cx, cy, r, a0, a1, ccw); x.stroke(); x.restore(); };
  const 蔽 = (fn) => { x.save(); x.globalCompositeOperation = 'destination-out'; x.globalAlpha = 1; x.fillStyle = '#000'; x.beginPath(); fn(); x.fill(); x.restore(); };
  return { 線, 貝, 弧, 蔽, ctx: x };
}

const 斷 = (c, m) => { console.assert(c, m); if (!c) throw new Error(m); };

// ── 尊 ──────────────────────────────────────────────────────────────
export async function 尊(x, 助) {
  const { 頂y, 指: u, 中x: cx } = 尊位;
  const Y = { 白毫: 頂y + 12 * u, 頦: 頂y + 20 * u, 心窩: 頂y + 36 * u, 臍: 頂y + 48 * u, 盤線: 頂y + 64 * u, 座面: 頂y + 68 * u };
  斷(Y.白毫 === 756 && Y.頦 === 788 && Y.心窩 === 852 && Y.臍 === 900 && Y.盤線 === 964 && Y.座面 === 980, 'T1419 錨破！');
  const 筆 = 具(x);
  const { 線, 貝, 弧, 蔽 } = 筆;

  // ── 〇・誦戒（滅點歸佛・界道之簿）───────────────────────────────────
  斷(框位.滅點[0] === cx && 框位.滅點[1] === Y.座面, '滅點不在佛座！');
  斷(Array.isArray(界道簿) && 界道簿.length === 17, `界道簿非十七：${界道簿.length}`);
  for (const 端 of 界道簿) 斷(Array.isArray(端) && 端.length === 2 && Number.isFinite(端[0]) && Number.isFinite(端[1]), '界道端畸！');

  // ── 一・遠星（虛空至淡）────────────────────────────────────────────
  {
    const 遠 = [[260, 210, 1.1], [520, 140, 0.9], [790, 250, 1.3], [1060, 120, 1.0],
      [1330, 230, 1.2], [1560, 160, 0.9], [180, 470, 1.0], [700, 420, 0.8],
      [1170, 460, 1.1], [1640, 420, 0.9], [420, 640, 0.9], [1420, 660, 1.0]];
    for (let i = 0; i < 遠.length; i++) { const [sx, sy, sr] = 遠[i];
      弧(sx, sy, sr, 0, 2 * Math.PI, 0.55, false, 0.13 + (i % 3) * 0.05);
      if (i % 5 === 2) for (const a of [0, Math.PI / 2])
        線([[sx - Math.cos(a) * 4.5, sy - Math.sin(a) * 4.5], [sx + Math.cos(a) * 4.5, sy + Math.sin(a) * 4.5]], 0.45, 0.13);
    }
  }

  // ── 二・日輪月輪（日光月光遍照之表——幾何而已）───────────────────────
  {
    const [dx, dy, dr] = 日輪;
    弧(dx, dy, dr, 0, 2 * Math.PI, 1.1, false, 0.75);
    弧(dx, dy, dr - 7, 0, 2 * Math.PI, 0.6, false, 0.35);
    for (let i = 0; i < 16; i++) { const a = i / 16 * 2 * Math.PI;
      線([[dx + Math.cos(a) * (dr + 5), dy + Math.sin(a) * (dr + 5)], [dx + Math.cos(a) * (dr + 12 + (i % 2) * 5), dy + Math.sin(a) * (dr + 12 + (i % 2) * 5)]], 0.6, 0.5); }
    const [mx, my, mr] = 月輪;                                                     // 月輪（內生弦月）
    弧(mx, my, mr, 0, 2 * Math.PI, 1.0, false, 0.7);
    貝([mx - 10, my - mr + 9], [mx + 20, my - 12], [mx + 20, my + 12], [mx - 10, my + mr - 9], 0.8, 0.5); // 弦影
    for (const [sx2, sy2] of [[mx - 16, my - 6], [mx - 8, my + 12], [mx - 20, my + 4]])
      弧(sx2, sy2, 0.9, 0, 2 * Math.PI, 0.6, false, 0.5);                          // 月面微星
  }

  // ── 三・藥師如來（滅點之上——小而精，量度守之）───────────────────────
  {
    蔽(() => { x.ellipse(cx, Y.心窩 - 8, 96, 150, 0, 0, 2 * Math.PI); });          // 佛域淨底（星霧讓尊）
    弧(cx, Y.白毫 + 6, 92, 0, 2 * Math.PI, 1.0, false, 0.55);                      // 大光暈
    弧(cx, Y.白毫 + 2, 58, 0, 2 * Math.PI, 0.9, false, 0.75);                      // 頭光
    弧(cx, Y.白毫 + 2, 50, 0, 2 * Math.PI, 0.55, false, 0.4);
    // 首（指=4 之微軀——筆少而位皆準）
    貝([cx - 15, Y.白毫 - 12], [cx - 16, Y.頦 - 8], [cx - 9, Y.頦], [cx, Y.頦 + 1], 1.0);   // 頰左
    貝([cx + 15, Y.白毫 - 12], [cx + 16, Y.頦 - 8], [cx + 9, Y.頦], [cx, Y.頦 + 1], 1.0);   // 頰右
    貝([cx - 13, Y.白毫 - 14], [cx - 8, Y.白毫 - 19], [cx + 8, Y.白毫 - 19], [cx + 13, Y.白毫 - 14], 0.95); // 髮際
    貝([cx - 8, Y.白毫 - 19], [cx - 5, Y.白毫 - 27], [cx + 5, Y.白毫 - 27], [cx + 8, Y.白毫 - 19], 0.9);    // 髻
    弧(cx, Y.白毫 - 29, 1.3, 0, 2 * Math.PI, 0.7);                                  // 髻珠
    線([[cx - 8, Y.白毫 + 1], [cx - 3, Y.白毫 + 1]], 0.7, 0.9); 線([[cx + 3, Y.白毫 + 1], [cx + 8, Y.白毫 + 1]], 0.7, 0.9); // 垂目二線
    弧(cx, Y.白毫 - 4, 0.9, 0, 2 * Math.PI, 0.6, false, 0.9);                       // 白毫
    貝([cx - 3, Y.頦 - 6], [cx - 1, Y.頦 - 5], [cx + 1, Y.頦 - 5], [cx + 3, Y.頦 - 6], 0.7, 0.85); // 口
    // 軀（通肩簡衣）
    線([[cx - 5, Y.頦 + 1], [cx - 6, Y.頦 + 7]], 0.9); 線([[cx + 5, Y.頦 + 1], [cx + 6, Y.頦 + 7]], 0.9);
    貝([cx - 6, Y.頦 + 7], [cx - 20, Y.頦 + 10], [cx - 28, Y.心窩 - 14], [cx - 30, Y.心窩 + 6], 1.05); // 肩臂左
    貝([cx + 6, Y.頦 + 7], [cx + 20, Y.頦 + 10], [cx + 28, Y.心窩 - 14], [cx + 30, Y.心窩 + 6], 1.05); // 肩臂右
    貝([cx - 30, Y.心窩 + 6], [cx - 32, Y.臍 - 6], [cx - 30, Y.臍 + 14], [cx - 26, Y.臍 + 26], 1.05);
    貝([cx + 30, Y.心窩 + 6], [cx + 32, Y.臍 - 6], [cx + 30, Y.臍 + 14], [cx + 26, Y.臍 + 26], 1.05);
    貝([cx - 14, Y.頦 + 9], [cx - 4, Y.頦 + 13], [cx + 4, Y.頦 + 13], [cx + 14, Y.頦 + 9], 0.75, 0.7);  // 領V
    for (let j = 0; j < 3; j++)                                                     // 胸腹衣紋U三
      貝([cx - 20 + j * 2, Y.心窩 - 8 + j * 14], [cx - 8, Y.心窩 - 2 + j * 14], [cx + 8, Y.心窩 - 2 + j * 14], [cx + 20 - j * 2, Y.心窩 - 8 + j * 14], 0.7, 0.6 - j * 0.1);
    // 盤坐與蓮臺（座面＝地平——佛坐地之盡處）
    貝([cx - 26, Y.臍 + 26], [cx - 44, Y.盤線 - 18], [cx - 48, Y.盤線 - 6], [cx - 44, Y.盤線 + 2], 1.05); // 盤股左
    貝([cx + 26, Y.臍 + 26], [cx + 44, Y.盤線 - 18], [cx + 48, Y.盤線 - 6], [cx + 44, Y.盤線 + 2], 1.05);
    貝([cx - 44, Y.盤線 + 2], [cx - 20, Y.盤線 + 8], [cx + 20, Y.盤線 + 8], [cx + 44, Y.盤線 + 2], 1.0);   // 盤底
    for (let i = 0; i < 7; i++) { const px = cx - 36 + i * 12;                       // 仰蓮七瓣
      貝([px - 5, Y.座面], [px - 3, Y.座面 - 9], [px + 3, Y.座面 - 9], [px + 5, Y.座面], 0.8, 0.8); }
    線([[cx - 44, Y.座面], [cx + 44, Y.座面]], 0.9, 0.85);                           // 座沿（貼地平）
    // 二手：左掌仰於臍前托藥壺、右手與願垂膝
    貝([cx - 9, Y.臍 + 10], [cx - 3, Y.臍 + 14], [cx + 3, Y.臍 + 14], [cx + 9, Y.臍 + 10], 0.85);  // 左仰掌
    貝([cx - 6, Y.臍 + 2], [cx - 7, Y.臍 - 4], [cx - 4, Y.臍 - 8], [cx, Y.臍 - 9], 0.8);           // 藥壺腹
    貝([cx + 6, Y.臍 + 2], [cx + 7, Y.臍 - 4], [cx + 4, Y.臍 - 8], [cx, Y.臍 - 9], 0.8);
    線([[cx - 3, Y.臍 - 10], [cx + 3, Y.臍 - 10]], 0.7); 弧(cx, Y.臍 - 13, 1.6, 0, 2 * Math.PI, 0.6, false, 0.8); // 壺口蓋珠
    貝([cx + 26, Y.臍 + 24], [cx + 30, Y.盤線 - 12], [cx + 28, Y.盤線 - 4], [cx + 24, Y.盤線], 0.85, 0.85); // 右臂垂
    for (let f = 0; f < 3; f++) 線([[cx + 24 - f * 3, Y.盤線], [cx + 25 - f * 3, Y.盤線 + 6]], 0.6, 0.8);   // 與願三指
  }

  // ── 四・十二燈柱（神將守道——近高遠低，列於琉璃道上）─────────────────
  {
    let 燈數 = 0;
    for (const [lx, ly, lh] of 燈列) {
      線([[lx, ly], [lx, ly - lh]], 0.9, 0.7 + lh * 0.004);                         // 柱
      線([[lx - lh * 0.16, ly], [lx + lh * 0.16, ly]], 0.8, 0.6);                   // 座
      弧(lx, ly - lh - lh * 0.13, lh * 0.13, 0, 2 * Math.PI, 0.85, false, 0.9);     // 燈珠
      弧(lx, ly - lh - lh * 0.13, lh * 0.3, 0, 2 * Math.PI, 0.5, false, 0.3);       // 燈暈
      線([[lx, ly - lh - lh * 0.3], [lx, ly - lh - lh * 0.42]], 0.6, 0.6);          // 焰梢
      燈數++;
    }
    斷(燈數 === 12, `十二燈缺：${燈數}`);
  }

  // ── 五・一蕾在道（行者之位——歸程之始）───────────────────────────────
  {
    const [bx, by] = 蕾位;
    貝([bx, by], [bx - 4, by - 36], [bx + 2, by - 68], [bx - 2, by - 96], 1.1);     // 莖
    貝([bx - 2, by - 96], [bx - 9, by - 112], [bx - 7, by - 132], [bx - 2, by - 144], 1.15); // 蕾左瓣
    貝([bx - 2, by - 96], [bx + 6, by - 112], [bx + 5, by - 132], [bx - 2, by - 144], 1.15); // 蕾右瓣
    貝([bx - 2, by - 100], [bx - 3, by - 116], [bx - 2, by - 130], [bx - 2, by - 140], 0.7, 0.6); // 蕾心線
    貝([bx - 1, by - 60], [bx - 14, by - 66], [bx - 22, by - 78], [bx - 24, by - 90], 0.9, 0.75); // 葉一
    貝([bx + 1, by - 44], [bx + 12, by - 50], [bx + 19, by - 60], [bx + 21, by - 72], 0.9, 0.75); // 葉二
    弧(bx - 2, by - 152, 2.2, 0, 2 * Math.PI, 0.7, false, 0.7);                     // 蕾尖露一珠
  }

  console.log(`琉璃地✓ 滅點歸佛(${框位.滅點})；十二燈✓；錨驗 白毫${Y.白毫} 頦${Y.頦} 心窩${Y.心窩} 臍${Y.臍} 盤線${Y.盤線} 座面${Y.座面}＝地平${框位.地平}`);
}

