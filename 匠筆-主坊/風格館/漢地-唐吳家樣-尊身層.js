// ── 修（合成終審之筆：蓮頂線過足內者淨之，足重描）────────────────────
export async function 修(x, 助) {
  const Y足底 = 尊位.頂y + 120 * 尊位.指;
  const 筆 = 具(x);
  const { 蔽 } = 筆;
  for (const [cx0, s] of [[830, -1], [970, 1]]) {
    const 內 = -s;
    蔽(() => {
      x.moveTo(cx0 + s * 24, Y足底 - 55);
      x.quadraticCurveTo(cx0 + s * 34, Y足底 - 18, cx0 + s * 26, Y足底 - 3);
      x.lineTo(cx0 + 內 * 50, Y足底 - 3);
      x.quadraticCurveTo(cx0 + 內 * 54, Y足底 - 10, cx0 + 內 * 42, Y足底 - 15);
      x.quadraticCurveTo(cx0 + 內 * 18, Y足底 - 26, cx0 + s * 0, Y足底 - 53);
      x.closePath();
    });
    足(筆, cx0, s, Y足底);
  }
}
