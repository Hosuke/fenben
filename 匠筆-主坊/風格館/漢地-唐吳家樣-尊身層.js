// ── 蘭葉：變寬一筆（吳家樣之本）──────────────────────────────────────
// 沿三次貝樣條取樣，寬 w(t)=峰寬·sin(π·t^κ)^冪——兩端細中段粗；κ 移腰作提按。
// 以雙緣合圍成閉域而填，故是「一筆」非雙線。回傳 {首,尾,峰寬實值,端寬實值}。
let 蘭葉筆數 = 0;
function 蘭葉(x, p0, c1, c2, p1, 峰寬 = 5, { κ = 1, 冪 = 0.85, n = 26, α = 1 } = {}) {
  const B = (t, a, b, c, d) => { const u = 1 - t; return u * u * u * a + 3 * u * u * t * b + 3 * u * t * t * c + t * t * t * d; };
  const dB = (t, a, b, c, d) => { const u = 1 - t; return 3 * u * u * (b - a) + 6 * u * t * (c - b) + 3 * t * t * (d - c); };
  const 邊A = [], 邊B = [];
  let 峰實 = 0, 端實 = Infinity;
  for (let i = 0; i <= n; i++) {
    const t = i / n;
    const px = B(t, p0[0], c1[0], c2[0], p1[0]), py = B(t, p0[1], c1[1], c2[1], p1[1]);
    let dx = dB(t, p0[0], c1[0], c2[0], p1[0]), dy = dB(t, p0[1], c1[1], c2[1], p1[1]);
    const L = Math.hypot(dx, dy) || 1; dx /= L; dy /= L;
    const w = Math.max(0.5, 峰寬 * Math.pow(Math.sin(Math.PI * Math.pow(t, κ)), 冪)) / 2;
    if (w * 2 > 峰實) 峰實 = w * 2;
    if (i === 0 || i === n) 端實 = Math.min(端實, w * 2);
    邊A.push([px - dy * w, py + dx * w]);
    邊B.push([px + dy * w, py - dx * w]);
  }
  x.save(); x.globalAlpha = α; x.fillStyle = 墨; x.beginPath();
  x.moveTo(邊A[0][0], 邊A[0][1]);
  for (const p of 邊A) x.lineTo(p[0], p[1]);
  for (let i = 邊B.length - 1; i >= 0; i--) x.lineTo(邊B[i][0], 邊B[i][1]);
  x.closePath(); x.fill(); x.restore();
  蘭葉筆數++;
  return { 首: [B(0, p0[0], c1[0], c2[0], p1[0]), B(0, p0[1], c1[1], c2[1], p1[1])],
           尾: [B(1, p0[0], c1[0], c2[0], p1[0]), B(1, p0[1], c1[1], c2[1], p1[1])], 峰實, 端實 };
}

