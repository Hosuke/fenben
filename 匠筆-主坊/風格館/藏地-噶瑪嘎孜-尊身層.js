// ── 小面（面塔部件：向=0 正面、±1 側顔；寬=半幅）───────────────────────
function 小面(筆, fx, fy, 半, 向, { 忿 = false } = {}) {
  const { 線, 貝, 弧 } = 筆;
  const 高 = 半 * 1.35;
  if (向 === 0) {
    貝([fx - 半, fy - 高 * 0.4], [fx - 半 - 2, fy + 高 * 0.3], [fx - 半 * 0.6, fy + 高 * 0.85], [fx, fy + 高], 1.5);
    貝([fx + 半, fy - 高 * 0.4], [fx + 半 + 2, fy + 高 * 0.3], [fx + 半 * 0.6, fy + 高 * 0.85], [fx, fy + 高], 1.5);
    貝([fx - 半, fy - 高 * 0.4], [fx - 半 * 0.6, fy - 高 * 0.75], [fx + 半 * 0.6, fy - 高 * 0.75], [fx + 半, fy - 高 * 0.4], 1.4); // 額髮際
    if (忿) {
      弧(fx - 半 * 0.42, fy, 半 * 0.2, 0, 2 * Math.PI, 1.2); 弧(fx + 半 * 0.42, fy, 半 * 0.2, 0, 2 * Math.PI, 1.2); // 圓瞋目
      弧(fx - 半 * 0.42, fy, 半 * 0.07, 0, 2 * Math.PI, 0.9); 弧(fx + 半 * 0.42, fy, 半 * 0.07, 0, 2 * Math.PI, 0.9);
      貝([fx - 半 * 0.5, fy + 高 * 0.5], [fx - 半 * 0.2, fy + 高 * 0.36], [fx + 半 * 0.2, fy + 高 * 0.36], [fx + 半 * 0.5, fy + 高 * 0.5], 1.2); // 齜口
      線([[fx - 半 * 0.22, fy + 高 * 0.42], [fx - 半 * 0.16, fy + 高 * 0.58]], 0.9); 線([[fx + 半 * 0.22, fy + 高 * 0.42], [fx + 半 * 0.16, fy + 高 * 0.58]], 0.9); // 牙
    } else {
      貝([fx - 半 * 0.62, fy - 半 * 0.16], [fx - 半 * 0.42, fy - 半 * 0.3], [fx - 半 * 0.2, fy - 半 * 0.28], [fx - 半 * 0.08, fy - 半 * 0.16], 0.95); // 左眉目線
      貝([fx + 半 * 0.62, fy - 半 * 0.16], [fx + 半 * 0.42, fy - 半 * 0.3], [fx + 半 * 0.2, fy - 半 * 0.28], [fx + 半 * 0.08, fy - 半 * 0.16], 0.95);
      線([[fx - 半 * 0.5, fy + 半 * 0.02], [fx - 半 * 0.14, fy + 半 * 0.04]], 0.85); 線([[fx + 半 * 0.5, fy + 半 * 0.02], [fx + 半 * 0.14, fy + 半 * 0.04]], 0.85); // 目縫
      貝([fx - 半 * 0.2, fy + 高 * 0.52], [fx - 半 * 0.06, fy + 高 * 0.6], [fx + 半 * 0.06, fy + 高 * 0.6], [fx + 半 * 0.2, fy + 高 * 0.52], 1.0); // 靜口
    }
  } else {
    const s = 向;                                                                  // 側顔（外向剪影）
    貝([fx - s * 半 * 0.3, fy - 高 * 0.72], [fx + s * 半 * 0.75, fy - 高 * 0.6], [fx + s * 半 * 1.02, fy - 高 * 0.1], [fx + s * 半 * 0.86, fy + 高 * 0.18], 1.4); // 額鼻稜
    貝([fx + s * 半 * 0.86, fy + 高 * 0.18], [fx + s * 半 * 0.7, fy + 高 * 0.34], [fx + s * 半 * 0.72, fy + 高 * 0.5], [fx + s * 半 * 0.5, fy + 高 * 0.66], 1.3);  // 唇頦
    貝([fx + s * 半 * 0.5, fy + 高 * 0.66], [fx + s * 半 * 0.1, fy + 高 * 0.95], [fx - s * 半 * 0.3, fy + 高 * 0.8], [fx - s * 半 * 0.4, fy + 高 * 0.4], 1.3);    // 頦頰回
    線([[fx + s * 半 * 0.3, fy - 半 * 0.1], [fx + s * 半 * 0.62, fy - 半 * 0.06]], 0.85);  // 側目縫
  }
}

// ── 足（立像赤足，尊修共用——承斯瓦特之型）────────────────────────────
function 足(筆, cx0, s, 底y) {
  const { 線, 貝, 弧 } = 筆;
  const 內 = -s;
  貝([cx0 + s * 16, 底y - 56], [cx0 + s * 22, 底y - 34], [cx0 + s * 30, 底y - 12], [cx0 + s * 26, 底y], 2.0);
  線([[cx0 + s * 26, 底y], [cx0 + 內 * 48, 底y]], 2.0);                            // 掌底貼地（線心即足底錨）
  貝([cx0 + s * 2, 底y - 54], [cx0 + 內 * 8, 底y - 36], [cx0 + 內 * 22, 底y - 17], [cx0 + 內 * 36, 底y - 10], 2.0);
  弧(cx0 + 內 * 44, 底y - 7, 5.8, Math.PI * 0.55, Math.PI * 2.45, 1.8);
  for (let k = 0; k < 4; k++) 弧(cx0 + 內 * (31 - k * 11), 底y - 4.5, 4.2 - k * 0.4, Math.PI * 0.9, Math.PI * 2.2, 1.35);
}

