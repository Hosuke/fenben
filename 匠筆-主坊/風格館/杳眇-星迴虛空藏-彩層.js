// 杳眇-星迴虛空藏 · 彩層（主坊親筆・設色程第二筆）
// 一幅一色主：玄青——虛空之色。三場而已，至簡至謐：
// 珠暉（明星光入——求聞持法之典，虛空藏之光自珠而出）、
// 身暈（月白微光承尊身——福智二藏之靜）、
// 玄青大場（虛空自珠心向外漸深——星迴之勢即光之勢，愈遠愈玄）。
// 星與螺旋皆線之事，彩不重敘（色即光場，光皆有源；場外之空恆虛）。
import { 尊位, 珠心 } from './杳眇-星迴虛空藏-尊身層.js';

export const 色譜 = {
  玄青: [58, 72, 140],
  珠暖: [232, 200, 140],
  月白: [190, 215, 230],
};
export const 彩律 = { 峰α: 0.22 };

const 斷 = (c, m) => { console.assert(c, m); if (!c) throw new Error(m); };
// 染即戒：一切色停必經此手——峰α之律無可繞。
const 染 = ([r, g, b], α) => {
  斷(Number.isFinite(α) && α >= 0 && α <= 彩律.峰α, `彩奪線！α${α}逾${彩律.峰α}`);
  return `rgba(${r},${g},${b},${α})`;
};

export function 彩(x, 助) {
  const { W, H, 頂y, 指: u, 中x } = 尊位;

  // ── 誦戒：契約 ───────────────────────────────────────────────────────
  斷(W === 1800 && H === 2500, '星迴布非1800×2500！');
  斷(珠心[0] === 848 && 珠心[1] === 1604, `珠心失約：${珠心}`);

  let 場數 = 0;

  const 暈 = (cx, cy, r, 色, α) => {
    const g = x.createRadialGradient(cx, cy, 0, cx, cy, r);
    g.addColorStop(0, 染(色, α));
    g.addColorStop(0.55, 染(色, α * 0.45));
    g.addColorStop(1, 染(色, 0));
    x.fillStyle = g;
    x.beginPath(); x.arc(cx, cy, r, 0, 2 * Math.PI); x.fill();
    場數++;
  };

  x.save();
  x.globalAlpha = 1;
  x.globalCompositeOperation = 'destination-over';   // 線骨恆上，結構所保

  // ── 一・珠暉（明星光入——全幅唯一暖處）───────────────────────────────
  暈(珠心[0], 珠心[1], 170, 色譜.珠暖, 0.16);

  // ── 二・身暈（月白承尊——心窩錨（頂y＋36指）為心，靜而不奪）──────────
  暈(中x, 頂y + 36 * u, 300, 色譜.月白, 0.08);

  // ── 三・玄青大場（虛空自珠而深——珠近則明，愈遠愈玄）─────────────────
  {
    const g = x.createRadialGradient(珠心[0], 珠心[1], 0, 珠心[0], 珠心[1], 1700);
    g.addColorStop(0, 染(色譜.玄青, 0));
    g.addColorStop(0.45, 染(色譜.玄青, 0.03));
    g.addColorStop(1, 染(色譜.玄青, 0.15));
    x.fillStyle = g;
    x.fillRect(0, 0, W, H);
    場數++;
  }

  x.restore();

  // ── 誦戒：場數之驗（珠1身1空1＝三場，無源之色不得落）─────────────────
  斷(場數 === 3, `色場非三：${場數}`);
  console.log(`星迴初彩✓ 三場：珠暉・身暈・玄青大場；峰α≤${彩律.峰α}`);
}
