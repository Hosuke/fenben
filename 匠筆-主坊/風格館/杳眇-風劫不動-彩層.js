// 杳眇-風劫不動 · 彩層（主坊親筆・設色程第五筆——第四作之彩）
// 一幅一色主：焰赭——力之色。三場而已，光法（色即光場，光皆有源，暗處無色）：
// 迎流暖場（風擊此身而力聚，故身之迎流側最明——「力經此物」之光；沿流向而streaks，
//   迎流半幅稍濃，愈遠愈斂）、
// 劍鋒寒光（滿幅暖流之中唯一冷點——不動之心，錨自尊身層之劍尖）、
// 尾跡靜光（身後靜寂尾域之至淡微光——動之所過，反顯其靜）。
// 風縷皆線之事，彩不重敘（場外之空恆虛，尾域之靜以無色顯）。
import { 尊位, 劍 } from './杳眇-風劫不動-尊身層.js';
import { 尊位 as 框位 } from '../../匠筆-codex/風格館/杳眇-風劫不動-框飾層.js';

export const 色譜 = {
  焰赭: [214, 116, 62],   // 暖主——力之色
  月霜: [176, 208, 234],  // 冷點——劍鋒之寒
  尾淡: [198, 172, 148],  // 至淡——靜域之息
};
export const 彩律 = { 峰α: 0.22 };

const 斷 = (c, m) => { console.assert(c, m); if (!c) throw new Error(m); };
// 染即戒：一切色停必經此手——峰α之律無可繞。
const 染 = ([r, g, b], α) => {
  斷(Number.isFinite(α) && α >= 0 && α <= 彩律.峰α, `彩奪線！α${α}逾${彩律.峰α}`);
  return `rgba(${r},${g},${b},${α})`;
};

export function 彩(x, 助) {
  const { W, H } = 尊位;
  const [心甲, 心乙] = 框位.身橢.心;
  const [自甲, 自乙] = 框位.流.自, [至甲, 至乙] = 框位.流.至;

  // ── 誦戒：契約（座標皆鎖——漂移或重合則報，免 NaN 悄敗）───────────────
  斷(W === 1800 && H === 2500, '風劫布非1800×2500！');
  斷(劍.x === 1002 && 劍.尖y === 776, `劍失約：${劍.x},${劍.尖y}`);
  斷(心甲 === 900 && 心乙 === 1300, `身橢契約破：${框位.身橢.心}`);
  斷(自甲 === 120 && 自乙 === 2380 && 至甲 === 1700 && 至乙 === 120, `流錨失約：${框位.流.自}→${框位.流.至}`);

  // 流之單位向（自→至）：順沿流，逆為迎流之向
  const 長 = Math.hypot(至甲 - 自甲, 至乙 - 自乙);
  斷(Number.isFinite(長) && 長 > 0, '流長非正——流錨重合或非數！');
  const 順甲 = (至甲 - 自甲) / 長, 順乙 = (至乙 - 自乙) / 長;
  const 流角 = Math.atan2(順乙, 順甲);

  let 場數 = 0;

  // 圓暈：一源之光，中明緣零
  const 暈 = (cx, cy, r, 色, α) => {
    const g = x.createRadialGradient(cx, cy, 0, cx, cy, r);
    g.addColorStop(0, 染(色, α));
    g.addColorStop(0.55, 染(色, α * 0.45));
    g.addColorStop(1, 染(色, 0));
    x.fillStyle = g;
    x.beginPath(); x.arc(cx, cy, r, 0, 2 * Math.PI); x.fill();
    場數++;
  };
  // 流暈：暈沿流向拉長（扁其橫），streaks 如風——「沿流向」之義
  const 流暈 = (cx, cy, r, 扁, 色, α) => {
    x.save();
    x.translate(cx, cy); x.rotate(流角); x.scale(1, 扁);
    const g = x.createRadialGradient(0, 0, 0, 0, 0, r);
    g.addColorStop(0, 染(色, α));
    g.addColorStop(0.5, 染(色, α * 0.5));
    g.addColorStop(1, 染(色, 0));
    x.fillStyle = g;
    x.beginPath(); x.arc(0, 0, r, 0, 2 * Math.PI); x.fill();
    x.restore();
    場數++;
  };

  x.save();
  x.globalAlpha = 1;
  x.globalCompositeOperation = 'destination-over';   // 線骨恆上，結構所保

  // ── 一・迎流暖場（力聚於身之迎流側——自身心逆流而偏，沿流向拉長為主色）─
  {
    const 偏 = 150;                                                         // 逆流偏移：源在迎流側
    const cx = 心甲 - 順甲 * 偏, cy = 心乙 - 順乙 * 偏;                       // [814,1423] 附近
    流暈(cx, cy, 1240, 0.60, 色譜.焰赭, 0.19);                              // 一色主——當廣而稍濃
  }

  // ── 二・劍鋒寒光（滿幅唯一冷點——不動之心，錨自劍尖；收為一星不奪暖主）─
  // α0.15：與此處暖場（約0.078）疊合後合成≈0.216，猶在單場0.22之律內——
  // destination-over 已保線骨恆上，此更令疊處不逾律（審所正）。
  暈(劍.x, 劍.尖y, 118, 色譜.月霜, 0.15);

  // ── 三・尾跡靜光（身後靜寂尾域——順流而偏，至淡如息）───────────────────
  {
    const 偏 = 280;                                                         // 順流偏移：尾在身後
    const cx = 心甲 + 順甲 * 偏, cy = 心乙 + 順乙 * 偏;                       // [1060,1071] 附近
    暈(cx, cy, 560, 色譜.尾淡, 0.05);
  }

  x.restore();

  // ── 誦戒：場數之驗（暖1劍1尾1＝三場，無源之色不得落）─────────────────
  斷(場數 === 3, `色場非三：${場數}`);
  console.log(`風劫初彩✓ 三場：迎流暖場・劍鋒寒光・尾跡靜光；一色主焰赭，唯一冷點在劍鋒；峰α≤${彩律.峰α}`);
}
