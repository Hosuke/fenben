// 杳眇-琉璃地 · 彩層（主坊親筆・設色程第一筆）
// 依研色卷-杳眇三律：色即光場（皆漸變之暈，無平塗無輪廓界）・一幅一色主
// （琉璃青）・彩不奪線（destination-over 居線下，單場峰值 α≤0.22，誦戒斷之）。
// 逆序之法：destination-over 之下先落者居前——故先燈暈蕾暈（最前之光），
// 次佛暉日月（中景之光），末道光琉璃地（最深之場）。天穹除諸暈外不設色。
// 光源錨皆契約而入（尊身層出口＋框飾層滅點地平），不抄錄一座標。
import { 尊位, 日輪, 月輪, 蕾位, 燈列 } from './杳眇-琉璃地-尊身層.js';
import { 尊位 as 框位 } from '../../匠筆-codex/風格館/杳眇-琉璃地-框飾層.js';

export const 色譜 = {
  琉璃青深: [30, 80, 150],
  琉璃青明: [38, 100, 170],
  道光青:   [90, 160, 200],
  燈暖:     [216, 160, 90],
  佛暉:     [232, 200, 140],
  月白:     [190, 215, 230],
  日暖:     [224, 176, 110],
  蕾碧:     [150, 205, 185],
};
export const 彩律 = { 峰α: 0.22 };

const 斷 = (c, m) => { console.assert(c, m); if (!c) throw new Error(m); };
// 染即戒：一切色停必經此手——峰α之律無可繞。
const 染 = ([r, g, b], α) => {
  斷(Number.isFinite(α) && α >= 0 && α <= 彩律.峰α, `彩奪線！α${α}逾${彩律.峰α}`);
  return `rgba(${r},${g},${b},${α})`;
};

export function 彩(x, 助) {
  const { W, H } = 尊位;
  const [滅x, 滅y] = 框位.滅點;
  const 地平 = 框位.地平;

  // ── 誦戒：契約與彩律 ─────────────────────────────────────────────────
  斷(W === 1800 && H === 2500, '琉璃地布非1800×2500！');
  斷(滅x === 900 && 滅y === 980 && 地平 === 980, '滅點地平契約破！');
  斷(Array.isArray(燈列) && 燈列.length === 12, `燈列非十二：${燈列 && 燈列.length}`);

  let 場數 = 0;

  const 暈 = (cx, cy, r, 色, α) => {
    const g = x.createRadialGradient(cx, cy, 0, cx, cy, r);
    g.addColorStop(0, 染(色, α));
    g.addColorStop(0.55, 染(色, α * 0.45));
    g.addColorStop(1, 染(色, 0));
    x.fillStyle = g;
    x.beginPath(); x.arc(cx, cy, r, 0, 2 * Math.PI); x.fill();
    場數++;
  };

  x.save();
  x.globalAlpha = 1;
  x.globalCompositeOperation = 'destination-over';   // 線骨恆上，結構所保

  // ── 一・十二燈暈（最前之光——燈珠即源，近大遠小隨燈高）────────────────
  for (const [lx, ly, lh] of 燈列) 暈(lx, ly - lh - lh * 0.13, lh * 2.3, 色譜.燈暖, 0.14);

  // ── 二・蕾暈（行者初心，微而已足）───────────────────────────────────
  暈(蕾位[0] - 2, 蕾位[1] - 124, 64, 色譜.蕾碧, 0.12);

  // ── 三・佛暉（歸處即光源——全幅最暖，蔽身之空由此而暖）────────────────
  暈(滅x, 滅y - 95, 190, 色譜.佛暉, 0.16);

  // ── 四・日暈月暈（遍照之表——日暖月白）───────────────────────────────
  暈(日輪[0], 日輪[1], 日輪[2] * 2.6, 色譜.日暖, 0.12);
  暈(月輪[0], 月輪[1], 月輪[2] * 2.5, 色譜.月白, 0.11);

  // ── 五・道光（歸路自佛而明——四重嵌楔各施薄彩，積成柔緣，無輪廓之界）──
  for (const [左, 右] of [[560, 1240], [640, 1160], [720, 1080], [800, 1000]]) {
    const g = x.createLinearGradient(0, 滅y, 0, H);
    g.addColorStop(0, 染(色譜.道光青, 0.008));
    g.addColorStop(1, 染(色譜.道光青, 0.035));
    x.fillStyle = g;
    x.beginPath(); x.moveTo(滅x, 滅y); x.lineTo(左, H); x.lineTo(右, H); x.closePath(); x.fill();
    場數++;
  }

  // ── 六・琉璃地場（最深之場——地平以下，近深遠淡，玻璃自內生光）────────
  {
    const g = x.createLinearGradient(0, 地平, 0, H);
    g.addColorStop(0, 染(色譜.琉璃青深, 0.05));
    g.addColorStop(1, 染(色譜.琉璃青明, 0.22));
    x.fillStyle = g;
    x.fillRect(0, 地平, W, H - 地平);
    場數++;
  }

  x.restore();

  // ── 誦戒：場數之驗（燈12＋蕾1＋佛1＋日月2＋道4＋地1＝二十一場，無源之色不得落）──
  斷(場數 === 21, `色場非二十一：${場數}`);
  console.log(`琉璃地初彩✓ 二十一場：燈12蕾1佛1日1月1道4地1；峰α≤${彩律.峰α}`);
}
