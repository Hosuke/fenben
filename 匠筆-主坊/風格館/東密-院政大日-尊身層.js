function 具(x) {
  // 呼吸線：變寬填形之筆（提按）——沿貝塞曲線取樣，法向偏移成閉形而填。
  // 寬廓：端細中沉，峰在 峰位；此院政線之魂。
  const 呼吸 = (p0, c1, c2, p1, 峰寬 = 3, 端寬 = 0.8, 峰位 = 0.5) => {
    峰位 = Math.min(0.9, Math.max(0.1, 峰位));                   // 夾峰位——免 s=0/0 生 NaN
    const N = 26;
    const B = (t) => {
      const u = 1 - t;
      return [
        u * u * u * p0[0] + 3 * u * u * t * c1[0] + 3 * u * t * t * c2[0] + t * t * t * p1[0],
        u * u * u * p0[1] + 3 * u * u * t * c1[1] + 3 * u * t * t * c2[1] + t * t * t * p1[1],
      ];
    };
    const dB = (t) => {
      const u = 1 - t;
      return [
        3 * u * u * (c1[0] - p0[0]) + 6 * u * t * (c2[0] - c1[0]) + 3 * t * t * (p1[0] - c2[0]),
        3 * u * u * (c1[1] - p0[1]) + 6 * u * t * (c2[1] - c1[1]) + 3 * t * t * (p1[1] - c2[1]),
      ];
    };
    const 寬 = (t) => {                                            // 端細中沉，峰在 峰位
      const s = t < 峰位 ? t / 峰位 : (1 - t) / (1 - 峰位);
      return 端寬 + (峰寬 - 端寬) * Math.pow(Math.max(0, s), 0.7);
    };
    const 左 = [], 右 = [];
    let 前nx = 0, 前ny = 1, 有前 = false;                        // 導數退化時取前法向為後備——免輪郭塌心生尖刺
    for (let i = 0; i <= N; i++) {
      const t = i / N, [bx, by] = B(t), [dx, dy] = dB(t);
      const 長 = Math.hypot(dx, dy);
      let nx, ny;
      if (長 > 1e-4) { nx = -dy / 長; ny = dx / 長; 前nx = nx; 前ny = ny; 有前 = true; }
      else { nx = 前nx; ny = 前ny; }                            // 退化：襲前法向（初端則預設縱法向 [0,1]）
      const w = 寬(t) / 2;
      左.push([bx + nx * w, by + ny * w]);
      右.push([bx - nx * w, by - ny * w]);
    }
    x.beginPath();
    x.moveTo(左[0][0], 左[0][1]);
    for (let i = 1; i <= N; i++) x.lineTo(左[i][0], 左[i][1]);
    for (let i = N; i >= 0; i--) x.lineTo(右[i][0], 右[i][1]);
    x.closePath();
    x.fillStyle = 墨; x.fill();
  };
  // 呼吸折：多節呼吸線（一氣連數段，節點自續）
  const 呼吸折 = (節, 峰寬 = 3, 端寬 = 0.8) => {
    for (let i = 0; i + 3 < 節.length; i += 3)
      呼吸(節[i], 節[i + 1], 節[i + 2], 節[i + 3], 峰寬, 端寬, 0.5);
  };
  // 細線：勻線（眼・小嚴飾之細部，不施呼吸）
  const 線 = (pts, w = 1, α = 1) => { x.save(); x.globalAlpha = α; x.strokeStyle = 墨; x.lineWidth = w; x.lineCap = 'round'; x.lineJoin = 'round'; x.beginPath(); x.moveTo(pts[0][0], pts[0][1]); for (let i = 1; i < pts.length; i++) x.lineTo(pts[i][0], pts[i][1]); x.stroke(); x.restore(); };
  const 弧 = (cx, cy, r, a0 = 0, a1 = 2 * Math.PI, w = 1, α = 1) => { x.save(); x.globalAlpha = α; x.strokeStyle = 墨; x.lineWidth = w; x.lineCap = 'round'; x.beginPath(); x.arc(cx, cy, r, a0, a1); x.stroke(); x.restore(); };
  const 貝 = (p0, c1, c2, p1, w = 1, α = 1) => { x.save(); x.globalAlpha = α; x.strokeStyle = 墨; x.lineWidth = w; x.lineCap = 'round'; x.beginPath(); x.moveTo(p0[0], p0[1]); x.bezierCurveTo(c1[0], c1[1], c2[0], c2[1], p1[0], p1[1]); x.stroke(); x.restore(); };
  const 圓 = (cx, cy, r, w = 1, α = 1) => 弧(cx, cy, r, 0, 2 * Math.PI, w, α);
  const 蔽 = (fn) => { x.save(); x.globalCompositeOperation = 'destination-out'; x.globalAlpha = 1; x.fillStyle = '#000'; x.beginPath(); fn(); x.fill(); x.restore(); };
  return { 呼吸, 呼吸折, 線, 弧, 貝, 圓, 蔽 };
}
